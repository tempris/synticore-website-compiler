'use strict';

/**
 * @fileoverview
 * Custom require hook for resolving `@custom/*` module specifiers in Synticore.
 *
 * Why this exists:
 * - Your codebase uses imports like: `require('@custom/cure-fs')`
 * - Your packages physically live under: `source/node_modules_custom/<pkg>`
 * - Node does not natively map `@custom/*` to that directory unless:
 *   - you actually have `source/node_modules_custom/@custom/<pkg>` on disk, OR
 *   - you preload a hook to rewrite `@custom/<pkg>` → `<node_modules_custom>/<pkg>`
 *
 * This hook:
 * - Intercepts Node's resolution pipeline (Module._resolveFilename)
 * - Rewrites requests beginning with `@custom/` to an absolute path inside
 *   `source/node_modules_custom/`
 *
 * Notes:
 * - This file is designed to be loaded via:
 *     NODE_OPTIONS=--require <abs-path-to-register.js>
 * - It is intentionally minimal and deterministic.
 *
 * @module _custom-require-hook/register
 * @since 1.0.0
 */

const NODE_PATH = require('path');
const NODE_MODULE = require('module');

/**
 * Resolve the absolute `source/node_modules_custom` directory, based on *this file* location.
 *
 * register.js lives at:
 *   source/node_modules_custom/_custom-require-hook/register.js
 *
 * So node_modules_custom is one directory up.
 *
 * @returns {string}
 * @since 1.0.0
 */
function getNodeModulesCustomDir() {
  return NODE_PATH.resolve(__dirname, '..');
}

/**
 * Rewrite `@custom/<pkg>/...` into `<node_modules_custom>/<pkg>/...`.
 *
 * @param {string} request
 * @param {string} nodeModulesCustomDir
 * @returns {string|null}
 * @since 1.0.0
 */
function rewriteCustomRequest(request, nodeModulesCustomDir) {
  if (typeof request !== 'string') return null;
  if (!request.startsWith('@custom/')) return null;

  // Strip "@custom/" prefix.
  const rest = request.slice('@custom/'.length);

  // Map to: source/node_modules_custom/<rest>
  return NODE_PATH.join(nodeModulesCustomDir, rest);
}

// -----------------------------------------------------------------------------
// Hook install
// -----------------------------------------------------------------------------

/** @type {any} */
const NODE_MODULE_ANY = NODE_MODULE;

const ORIGINAL_RESOLVE_FILENAME = NODE_MODULE_ANY._resolveFilename;
const NODE_MODULES_CUSTOM_DIR = getNodeModulesCustomDir();

NODE_MODULE_ANY._resolveFilename = function patchedResolveFilename(request, parent, isMain, options) {
  const rewritten = rewriteCustomRequest(request, NODE_MODULES_CUSTOM_DIR);
  if (rewritten) {
    // Hand absolute path back into Node's resolver, so it will honor package.json/index.js rules.
    return ORIGINAL_RESOLVE_FILENAME.call(this, rewritten, parent, isMain, options);
  }
  return ORIGINAL_RESOLVE_FILENAME.call(this, request, parent, isMain, options);
};
