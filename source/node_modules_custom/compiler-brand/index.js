/**
 * Normalize an alignment value to 2-D form.
 * @param {Align2D | AlignAxis} a
 * @returns {Align2D}
 */
function toAlign2D(a) {
  return (a && typeof a === 'object')
    ? /** @type {Align2D} */ (a)
    : /** @type {Align2D} */ ({ x: /** @type {AlignAxis} */ (a), y: /** @type {AlignAxis} */ (a) });
}

/**
 * Composite a resized/cropped background with a resized foreground into `outputSize`,
 * honoring independent alignment for both layers.
 *
 * @private
 * @async
 * @function _createCompositeImage
 * @memberof module:synticore-brand
 * @param {import('sharp').Sharp} foreground - Sharp instance for foreground image.
 * @param {import('sharp').Sharp} background - Sharp instance for background image.
 * @param {SizePx} outputSize - Target canvas size.
 * @param {Align2D|AlignAxis|'center'} [fgAlign='center'] - Foreground alignment.
 * @param {Align2D|AlignAxis|'center'} [bgAlign='center'] - Background alignment.
 * @returns {Promise<Buffer>} PNG/RGBA buffer of the composed image.
 * @throws {Error} On Sharp processing failures.
 * @since 1.0.0
 */
async function _createCompositeImage(foreground, background, outputSize, fgAlign = "center", bgAlign = "center") {
  function parseAlignment(value) {
    if (typeof value === "string") {
      switch (value.toLowerCase()) {
        case "left": return 0;
        case "center": return 50;
        case "right": return 100;
        case "top": return 0;
        case "bottom": return 100;
        default: return 50;
      }
    }
    return Math.min(100, Math.max(0, value));
  }

  const fgA = toAlign2D(fgAlign);
  const bgA = toAlign2D(bgAlign);

  const fgAlignX = parseAlignment(fgA.x);
  const fgAlignY = parseAlignment(fgA.y);
  const bgAlignX = parseAlignment(bgA.x);
  const bgAlignY = parseAlignment(bgA.y);

  const fgMetadata = await foreground.metadata();
  const bgMetadata = await background.metadata();

  // Determine the scale for the background image
  const scaleX = outputSize.width / bgMetadata.width;
  const scaleY = outputSize.height / bgMetadata.height;
  const scale = Math.max(scaleX, scaleY); // Use the smallest scale that still covers the entire area

  const newBgWidth = Math.ceil(bgMetadata.width * scale);
  const newBgHeight = Math.ceil(bgMetadata.height * scale);

  const resizedBg = await background
    .resize({
      width: newBgWidth,
      height: newBgHeight,
      fit: "fill",
    })
    .toBuffer();

  // Compute background cropping after resizing
  const bgLeft = Math.round((newBgWidth - outputSize.width) * (bgAlignX / 100));
  const bgTop = Math.round((newBgHeight - outputSize.height) * (bgAlignY / 100));

  const croppedBg = await NODE_SHARP(resizedBg)
    .extract({
      left: Math.max(0, bgLeft),
      top: Math.max(0, bgTop),
      width: outputSize.width,
      height: outputSize.height,
    })
    .toBuffer();

  // Resize foreground while maintaining aspect ratio
  const fgAspectRatio = fgMetadata.width / fgMetadata.height;
  const targetAspectRatio = outputSize.width / outputSize.height;

  let fgWidth, fgHeight;
  if (fgAspectRatio > targetAspectRatio) {
    fgWidth = outputSize.width;
    fgHeight = Math.ceil(fgWidth / fgAspectRatio);
  } else {
    fgHeight = outputSize.height;
    fgWidth = Math.ceil(fgHeight * fgAspectRatio);
  }

  const resizedFg = await foreground
    .resize({ width: fgWidth, height: fgHeight, fit: 'inside' })
    .toBuffer();

  const fgLeft = Math.round((outputSize.width - fgWidth) * (fgAlignX / 100));
  const fgTop = Math.round((outputSize.height - fgHeight) * (fgAlignY / 100));

  return NODE_SHARP(croppedBg)
    .composite([{ input: resizedFg, left: fgLeft, top: fgTop }])
    .toBuffer();
}

/**
 * Apply size-aware blur and modulate adjustments to images and produce the final output buffer.
 *
 * Blur scaling:
 * - `BASE_SIZE = 256`. Actual blur = `blur * sqrt((W×H)/(BASE_SIZE×BASE_SIZE))`.
 *
 * @private
 * @async
 * @function _createFinalImage
 * @memberof module:synticore-brand
 * @param {import('sharp').Sharp} fgImg - Foreground Sharp instance.
 * @param {import('sharp').Sharp|null} bgImg - Background Sharp instance or `null`.
 * @param {SizePx} outputSize - Target canvas size.
 * @param {Adjust} fgAdjust - Foreground adjustments.
 * @param {Adjust} bgAdjust - Background adjustments (used only if `bgImg` present).
 * @returns {Promise<Buffer>} PNG/RGBA buffer of the final image.
 * @throws {Error} On Sharp processing failures.
 * @since 1.0.0
 */
async function _createFinalImage(fgImg, bgImg, outputSize, fgAdjust, bgAdjust) {
  let finalBgImage = bgImg ? bgImg.clone() : null;
  let finalFgImage = fgImg.clone();

  // Define a base reference size for consistent blur scaling
  const BASE_SIZE = 256; // The resolution where blur appears "as intended"

  // Calculate blur scaling factor based on output size
  const scaleFactor = Math.sqrt((outputSize.width * outputSize.height) / (BASE_SIZE * BASE_SIZE));

  // Apply background effects (blur, modulate) if a background exists
  if (finalBgImage) {
    if (bgAdjust.blur > 0) {
      const scaledBlur = bgAdjust.blur * scaleFactor;
      finalBgImage = finalBgImage.blur(scaledBlur);
    }
    if (Object.keys(bgAdjust.modulate).length) finalBgImage = finalBgImage.modulate(bgAdjust.modulate);
  }

  // Apply foreground effects (blur, modulate)
  if (fgAdjust.blur > 0) {
    const scaledBlur = fgAdjust.blur * scaleFactor;
    finalFgImage = finalFgImage.blur(scaledBlur);
  }
  if (Object.keys(fgAdjust.modulate).length) {
    const fgBuffer = await finalFgImage.modulate(fgAdjust.modulate).toBuffer();
    finalFgImage = NODE_SHARP(fgBuffer);
  }

  // // Pass directly to _createCompositeImage (which now handles alignment & resizing)
  // return _createCompositeImage(finalFgImage, finalBgImage, outputSize, fgAdjust.alignment, bgAdjust.alignment);

  if (finalBgImage) {
    return _createCompositeImage(finalFgImage, finalBgImage, outputSize, fgAdjust.alignment, bgAdjust.alignment);
  } else {
    // If no background, return just the foreground resized to fit outputSize
    return finalFgImage.resize(outputSize).toBuffer();
  }
}

/**
 * Build `favicon.ico` from multiple square PNG buffers (one per size).
 * Uses temporary PNG files alongside the final ICO path, then deletes them.
 *
 * @private
 * @async
 * @function _generateIco
 * @memberof module:synticore-brand
 * @param {import('sharp').Sharp} foreground - Foreground Sharp instance.
 * @param {import('sharp').Sharp|null} background - Background Sharp instance or null.
 * @param {string} outputIco - Destination `.ico` file path.
 * @param {number[]} icoSizes - Square sizes in pixels (e.g., `[16,32,48,64,128,256]`).
 * @param {Adjust} fgAdjust - Foreground adjustments.
 * @param {Adjust} bgAdjust - Background adjustments.
 * @returns {Promise<void>} Resolves when the ICO file is written.
 * @throws {Error} If no valid sizes are provided or ICO assembly fails.
 * @since 1.0.0
 */
async function _generateIco(foreground, background, outputIco, icoSizes, fgAdjust, bgAdjust) {
  log.info(LOG_TAG_BRAND, "Processing favicon with ICO sizes:", icoSizes);

  const NODE_CURE_FS = require('@custom/cure-fs');
  const tempPngPaths = [];
  const icoSizesBuffers = [];

  // Convert new array format [16, 32, 48, ...] into { width, height } objects
  const formattedIcoSizes = icoSizes.map(size => ({ width: size, height: size }));

  for (const size of formattedIcoSizes) {
    if (!size || !size.width || !size.height) {
      log.error(LOG_TAG_BRAND, "Invalid ICO size entry:", size);
      continue; // Skip invalid size entries
    }

    const tempPath = NODE_PATH.join(NODE_PATH.dirname(outputIco), `_temp_favicon_${size.width}x${size.height}.png`);
    try {
      // Use _createFinalImage to apply blur, modulate, and proper scaling
      const outputBuffer = await _createFinalImage(
        foreground,
        background ? background : null,
        { width: size.width, height: size.height },
        fgAdjust,
        bgAdjust
      );

      await NODE_SHARP(outputBuffer).toFile(tempPath);
      tempPngPaths.push(tempPath);
      icoSizesBuffers.push(NODE_FS.readFileSync(tempPath));
    } catch (err) {
      log.error(LOG_TAG_BRAND, `Failed to create PNG for size ${size.width}x${size.height}:`, err);
    }
  }

  if (icoSizesBuffers.length === 0) {
    throw new Error(`No valid ICO sizes provided, unable to generate ${outputIco}`);
  }

  try {
    const icoBuffer = await NODE_PNG_TO_ICO(icoSizesBuffers);
    NODE_CURE_FS.writeFileSync(outputIco, icoBuffer);
    log.info(LOG_TAG_BRAND, `Saved ${outputIco}`);
  } catch (err) {
    log.error(LOG_TAG_BRAND, `Failed to create ${outputIco}:`, err);
  } finally {
    // Clean up temporary PNG files
    tempPngPaths.forEach((filePath) => {
      try {
        NODE_CURE_FS.deleteSync([filePath], { force: true });
      } catch (err) {
        log.warn(LOG_TAG_BRAND, "Failed to delete temporary file:", filePath, err);
      }
    });
  }
}

