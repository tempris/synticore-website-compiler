/**
 * Generate a set of PNG brand images and a multi-size `favicon.ico` according to settings.
 *
 * Semantics & side effects:
 * - Ensures output directories exist.
 * - Loads foreground (and optional wide/background) images via Sharp.
 * - For each configured PNG size: picks the wide or main foreground based on aspect, optionally applies background,
 *   applies adjustments, and writes the file.
 * - Assembles the ICO from the configured `ico` sizes.
 *
 * @async
 * @function processFavicon
 * @memberof module:synticore-brand
 * @param {ProcessFaviconOptions} options - Paths and settings controlling generation.
 * @returns {Promise<void>} Resolves when all files are written.
 * @throws {Error} If reading inputs, image processing, or writing outputs fails.
 * @since 1.0.0
 *
 * @example
 * const { processFavicon } = require('./index.js');
 * (async () => {
 *   await processFavicon({
 *     inputForeground: 'assets/logo.png',
 *     outputImage: 'dist/brand',
 *     outputIco: 'dist/brand/favicon.ico',
 *     setting: {
 *       size: { image: { 'icon-256.png': { width: 256, height: 256 } }, ico: [16, 32, 48, 256] },
 *       adjust: { foreground: { blur: 0 }, background: { blur: 4, apply: { square: true, wide: true } } }
 *     }
 *   });
 * })();
 */
async function processFavicon(options) {
  const {
    inputForeground,
    inputForegroundWide,
    inputBackground,
    outputImage,
    outputIco,
    setting
  } = options;

  // Extract settings from config
  const icoSizes = setting.size?.ico || [];
  const imageSizes = setting.size?.image || {};
  const fgAdjust = setting.adjust?.foreground || {};
  const bgAdjust = setting.adjust?.background || {};

  // Ensure output directories exist
  NODE_FS.mkdirSync(outputImage, { recursive: true });
  NODE_FS.mkdirSync(NODE_PATH.dirname(outputIco), { recursive: true });

  // Load images
  const mainImage = NODE_SHARP(inputForeground);
  const wideImage = inputForegroundWide && NODE_FS.existsSync(inputForegroundWide) ? NODE_SHARP(inputForegroundWide) : mainImage;
  const backgroundImage = inputBackground && NODE_FS.existsSync(inputBackground) ? NODE_SHARP(inputBackground) : mainImage;

  // NODE_SHARP({
  //   create: {
  //     width: outputSize.width,
  //     height: outputSize.height,
  //     channels: 4,
  //     background: { r: 0, g: 0, b: 0, alpha: 0 }
  //   }
  // })

  // Generate image files
  for (const [name, size] of Object.entries(imageSizes)) {
    const outputPath = NODE_PATH.join(outputImage, name);

    const fgImg = size.width > size.height ? wideImage : mainImage;
    const useBG = size.width > size.height ? bgAdjust.apply?.wide : bgAdjust.apply?.square;
    const outputBuffer = await _createFinalImage(fgImg, useBG ? backgroundImage : null, size, fgAdjust, bgAdjust);

    await NODE_SHARP(outputBuffer).toFile(outputPath);
    log.success(LOG_TAG_BRAND, `Saved ${outputPath}`);
  }

  // Generate favicon.ico
  await _generateIco(mainImage, bgAdjust.apply?.square ? backgroundImage : null, outputIco, icoSizes, fgAdjust, bgAdjust);
}

