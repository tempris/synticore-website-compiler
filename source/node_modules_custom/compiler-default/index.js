/**
 * @fileoverview
 * synticore-default - bootstrap and migrate Synticore config files safely.
 *
 * Purpose:
 * - Ensure required config files exist under `config/` by copying known-good defaults
 *   from `config/default/` (except `config/log.json`, which is initialized as `{}`).
 * - Perform one-time migrations (remove legacy keys, normalize arrays, de-duplicate).
 * - Restore `gui.json` from the default copy when its `dir_recent` list is empty/missing.
 *
 * Runtime & module system:
 * - Runtime: Node.js (LTS recommended).
 * - Module system: CommonJS (CJS). Safe to `require()` from other CJS modules.
 *
 * Side effects on import:
 * - **None.** All I/O runs only when {@link module:synticore-default.configEnsure} is called.
 *
 * Globals & environment:
 * - Uses Node globals: `process`, `__dirname`.
 * - Initializes a logger via `cure-log-hub` and writes to `<project>/_log/synticore-default.log`.
 *
 * Security & permission assumptions:
 * - Reads/writes JSON files under project `config/` paths. Callers must ensure the process has
 *   filesystem permissions. No user-supplied paths are interpolated here.
 *
 * I/O contract:
 * - Reads/writes the following files as UTF-8 JSON:
 *   - Defaults: `config/default/gui.json` (and `log.json` for fallback defaults only).
 *   - Targets:  `config/gui.json`, `config/log.json`.
 * - Creates parent directories with `{ recursive: true }` when missing.
 * - On unrecoverable error, logs and terminates the process with `process.exit(1)`.
 *
 * Concurrency & async:
 * - All operations are synchronous (`fs.*Sync`) and run on the calling thread.
 * - No timers, streams, or parallelism; no backpressure concerns.
 *
 * Compatibility:
 * - Works on modern Node LTS. Uses only built-in `fs`/`path`.
 *
 * Versioning policy:
 * - SemVer. New public symbols carry `@since`. Behavior changes without breaking signatures bump MINOR.
 *
 * @module synticore-default
 * @version 1.0.0
 * @since 1.0.0
 * @license MIT
 * @author Cure Interactive
 *
 * @example <caption>Initialize defaults and run migrations at app startup</caption>
 * const { configEnsure } = require('./index');
 * // Will create config files from defaults if missing,
 * // migrate legacy shapes, and restore empty dir_recent from defaults.
 * configEnsure();
 */

