/**
 * Resolve a path relative to the project root (two levels up from this file).
 *
 * I/O:
 * - Pure path computation; no filesystem access.
 *
 * @param {string} [path='']
 *   Project-root-relative path to resolve.
 * @returns {string}
 *   Absolute path.
 * @since 1.0.0
 * @private
 */
function _getRootPath(path) {
    return NODE_PATH.resolve(__dirname, '../../../', path);
}

