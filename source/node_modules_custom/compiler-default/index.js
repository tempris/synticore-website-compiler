/**
 * Ensure a directory exists (mkdir -p).
 *
 * @param {string} dir
 * @returns {void}
 * @since 1.0.0
 * @private
 */
function _ensureDir(dir) {
  if (!NODE_FS.existsSync(dir)) {
    NODE_FS.mkdirSync(dir, { recursive: true });
  }
}

/**
 * Deep-sort object keys alphabetically (case-insensitive). Arrays are mapped; Buffers untouched.
 *
 * @param {any} value
 * @returns {any}
 * @since 1.0.0
 * @private
 */
function _sortKeysDeep(value) {
  if (Array.isArray(value)) {
    return value.map(_sortKeysDeep);
  }
  if (value && typeof value === 'object' && !Buffer.isBuffer(value)) {
    return Object.keys(value)
      .sort((a, b) => a.localeCompare(b, 'en', { sensitivity: 'base' }))
      .reduce((out, key) => {
        out[key] = _sortKeysDeep(value[key]);
        return out;
      }, {});
  }
  return value;
}

/**
 * Read a JSON file safely; return `{}` on error/missing.
 *
 * @param {string} file
 * @returns {Object<string, any>}
 * @since 1.0.0
 * @private
 */
function _loadJsonSafe(file) {
  try {
    const raw = NODE_FS.readFileSync(file, 'utf8');
    return JSON.parse(raw);
  } catch {
    return {};
  }
}

/**
 * Write a JSON file safely (UTF-8). Optionally deep-sort keys before writing.
 *
 * @param {string} file
 * @param {object} data
 * @param {boolean} [sort=true]
 * @returns {void}
 * @since 1.0.0
 * @private
 */
function _saveJsonSafe(file, data, sort = true) {
  _ensureDir(NODE_PATH.dirname(file));
  const NODE_CURE_FS = require('@custom/cure-fs');
  NODE_CURE_FS.writeFileSync(
    file,
    JSON.stringify(sort ? _sortKeysDeep(data) : data, null, 4)
  );
}

/**
 * Return a copy of the array with falsy values removed and order-preserving de-duplication.
 *
 * @param {string[]} arr
 * @returns {string[]}
 * @since 1.0.0
 * @private
 */
function _dedupePreserve(arr) {
  const seen = new Set();
  const out = [];
  for (const v of arr) {
    if (!v) continue;
    if (seen.has(v)) continue;
    seen.add(v);
    out.push(v);
  }
  return out;
}

/**
 * Whether an object has a **non-empty** `dir_recent` array containing at least one non-blank string.
 *
 * @param {any} obj
 * @returns {boolean}
 * @since 1.0.0
 * @private
 */
function _hasNonEmptyRecents(obj) {
  if (!obj || typeof obj !== 'object') return false;
  if (!Array.isArray(obj.dir_recent)) return false;
  // A "non-empty" recent is any string with at least one non-space character
  return obj.dir_recent.some(v => typeof v === 'string' && v.trim().length > 0);
}

/**
 * If `config/gui.json` exists but has an empty/missing `dir_recent`,
 * restore the entire file from `config/default/gui.json`.
 *
 * Failure behavior:
 * - Logs the error and terminates the process with exit code 1 when the default is missing
 *   or unreadable.
 *
 * @returns {void}
 * @since 1.0.0
 * @private
 */
function _ensureNonEmptyRecentsOrRestore() {
  const targetFilePath  = NODE_PATH.resolve(path_config, 'gui.json');
  const defaultFilePath = NODE_PATH.resolve(path_config_default, 'gui.json');

  const current = _loadJsonSafe(targetFilePath);

  if (!_hasNonEmptyRecents(current)) {
    if (!NODE_FS.existsSync(defaultFilePath)) {
      log.error(LOG_TAG_DEFAULT, 'Cannot restore defaults; missing default file:', defaultFilePath);
      process.exit(1);
    }

    try {
      // Overwrite target with default
      _ensureDir(NODE_PATH.dirname(targetFilePath));
      const rawDefault = NODE_FS.readFileSync(defaultFilePath, 'utf8');
      // Write as-is (but still ensure JSON is valid and sorted)
      const parsedDefault = JSON.parse(rawDefault);
      _saveJsonSafe(targetFilePath, parsedDefault, true);

      log.notice(
        LOG_TAG_DEFAULT,
        'dir_recent was empty or missing; restored gui.json from default.',
        { targetFilePath, defaultFilePath }
      );
    } catch (err) {
      log.error(LOG_TAG_DEFAULT, 'Failed to restore gui.json from default:', err);
      process.exit(1);
    }
  }
}

/**
 * One-time migration routine:
 * - Remove legacy `"dir"` key from `gui.json`.
 * - Normalize and de-duplicate `dir_recent` strings (trim blanks; preserve order).
 *
 * Writes back only when a change is detected.
 *
 * @returns {void}
 * @since 1.0.0
 * @private
 */
function _migrateLegacyConfig() {
  const settingsPath = NODE_PATH.resolve(path_config, 'gui.json');

  const settings = _loadJsonSafe(settingsPath);
  let changed = false;

  // 1) Remove legacy "dir" key if present
  if (Object.prototype.hasOwnProperty.call(settings, 'dir')) {
    const legacyDir = settings.dir;
    delete settings.dir;
    changed = true;
    log.notice(LOG_TAG_DEFAULT, 'Removed legacy "dir" from gui.json', { legacyDir });
  }

  // 2) Normalize and de-dupe dir_recent
  let recents = Array.isArray(settings.dir_recent) ? settings.dir_recent : [];
  recents = recents
    .map(v => (typeof v === 'string' ? v.trim() : ''))
    .filter(Boolean);

  const deduped = _dedupePreserve(recents);
  if (!Array.isArray(settings.dir_recent) || deduped.length !== settings.dir_recent.length) {
    settings.dir_recent = deduped;
    changed = true;
  }

  if (changed) {
    _saveJsonSafe(settingsPath, settings);
  }
}

/**
 * One-time file migration:
 * - If legacy `config/settings.json` exists and `config/gui.json` does not, rename settings -> gui.
 * - If both exist, remove legacy settings to keep gui as single source.
 *
 * @returns {void}
 * @since 1.1.2
 * @private
 */
function _migrateLegacySettingsFileToGui() {
  const legacyPath = NODE_PATH.resolve(path_config, 'settings.json');
  const guiPath = NODE_PATH.resolve(path_config, 'gui.json');

  try {
    if (NODE_FS.existsSync(legacyPath) && !NODE_FS.existsSync(guiPath)) {
      _ensureDir(NODE_PATH.dirname(guiPath));
      NODE_FS.renameSync(legacyPath, guiPath);
      log.notice(LOG_TAG_DEFAULT, 'Migrated legacy settings.json to gui.json.', { from: legacyPath, to: guiPath });
      return;
    }

    if (NODE_FS.existsSync(legacyPath) && NODE_FS.existsSync(guiPath)) {
      NODE_FS.unlinkSync(legacyPath);
      log.notice(LOG_TAG_DEFAULT, 'Removed legacy settings.json (gui.json already exists).', { removed: legacyPath });
    }
  } catch (err) {
    log.warn(LOG_TAG_DEFAULT, 'Legacy settings.json -> gui.json migration failed (continuing).', err);
  }
}

