/**
 * Ensure Synticore defaults and migrations are applied.
 *
 * Steps:
 * 1. Create `config/` directory if missing.
 * 2. For each managed file (currently `log.json`, `gui.json`):
 *    - If `log.json` target is missing -> create `{}`.
 *    - If `gui.json` target is missing and default exists -> copy default to target.
 *    - If both exist -> leave target intact.
 *    - If `gui.json` default is missing when needed -> **log error and exit(1)**.
 * 3. Run legacy migration to remove `"dir"` and normalize `dir_recent`.
 * 4. If `dir_recent` is empty/missing after migration -> restore full `gui.json` from default.
 *
 * Logging:
 * - Uses cure-log-hub facade with tags like `[Defaults]`, emitting `begin/info/success/notice/error/end`.
 *
 * Exit codes:
 * - 0: Completed successfully.
 * - 1: Unrecoverable error (e.g., missing default file when needed, JSON parse failure on restore).
 *
 * @function configEnsure
 * @memberof module:synticore-default
 * @returns {void}
 *   No return value; throws only on programmer errors (synchronous API misuse). Operational
 *   failures are logged and will terminate the process with `process.exit(1)`.
 * @throws {Error}
 *   Only for unexpected programmer errors (e.g., invalid require wiring). Normal operational
 *   failures use logged messages and `process.exit(1)`.
 * @since 1.0.0
 *
 * @example <caption>Run at application boot</caption>
 * const { configEnsure } = require('synticore-default');
 * configEnsure();
 */
function configEnsure() {
  log.begin(LOG_TAG_DEFAULT, 'Running...');

  log.info(LOG_TAG_DEFAULT, 'Config Paths:', {path_config, path_config_default});

  try {
    // Make sure config dir exists before copying
    _ensureDir(path_config);

    // One-time file migration before initialization.
    _migrateLegacySettingsFileToGui();

    // Ensure managed config files exist.
    for (const file of files_compiler) {
      const defaultFilePath = NODE_PATH.resolve(path_config_default, file);
      const targetFilePath  = NODE_PATH.resolve(path_config, file);

      if (!NODE_FS.existsSync(targetFilePath)) {
        // log.json intentionally starts empty so runtime merge pulls from defaults.
        if (file === 'log.json') {
          _saveJsonSafe(targetFilePath, {}, true);
          log.success(LOG_TAG_DEFAULT, 'Initialized file as empty JSON object:', targetFilePath);
        } else if (NODE_FS.existsSync(defaultFilePath)) {
          _ensureDir(NODE_PATH.dirname(targetFilePath));
          NODE_FS.copyFileSync(defaultFilePath, targetFilePath);
          log.success(LOG_TAG_DEFAULT, 'Initialized file from default:', defaultFilePath);
        } else {
          log.error(LOG_TAG_DEFAULT, 'Initialization failed, default file missing:', defaultFilePath);
          process.exit(1);
        }
      } else {
        log.success(LOG_TAG_DEFAULT, 'File already exists:', targetFilePath);
      }
    }

    // Perform one-time migrations to new scheme
    _migrateLegacyConfig();

    // If dir_recent is empty/missing, restore settings from default
    _ensureNonEmptyRecentsOrRestore();

  } catch (error) {
    log.error(LOG_TAG_DEFAULT, 'An error occurred during initialization:', error);
    process.exit(1);
  }

  log.end(LOG_TAG_DEFAULT, 'Complete.');
}

exports.configEnsure = configEnsure;

