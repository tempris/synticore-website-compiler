function _parseCookies(req) {
    const raw = String(req.headers.cookie || '');
    const out = {};
    raw.split(';').forEach((entry) => {
        const [key, ...rest] = entry.split('=');
        const name = String(key || '').trim();
        if (!name) return;
        out[name] = decodeURIComponent(rest.join('=').trim());
    });
    return out;
}

function _normalizeRemoteAddress(address) {
    const value = String(address || '').trim().toLowerCase();
    if (!value) return '';
    if (value.startsWith('::ffff:')) {
        return value.slice('::ffff:'.length);
    }
    return value;
}

function _isLoopbackAddress(address) {
    const normalized = _normalizeRemoteAddress(address);
    return normalized === '::1'
        || normalized === '127.0.0.1'
        || normalized.startsWith('127.');
}

function _requestFromHostMachine(req) {
    const remoteAddress = _normalizeRemoteAddress(
        req && req.socket && req.socket.remoteAddress
            ? req.socket.remoteAddress
            : req && req.connection && req.connection.remoteAddress
    );

    if (!remoteAddress) return false;
    if (_isLoopbackAddress(remoteAddress)) return true;

    const interfaces = NODE_OS.networkInterfaces();
    for (const entries of Object.values(interfaces)) {
        for (const entry of entries || []) {
            if (!entry || !entry.address) continue;
            if (_normalizeRemoteAddress(entry.address) === remoteAddress) {
                return true;
            }
        }
    }

    return false;
}

function _requestAuthorized(req, guiConfig) {
    if (!_networkAuthRequired(guiConfig)) return true;
    if (_requestFromHostMachine(req)) return true;
    const cookies = _parseCookies(req);
    return String(cookies[AUTH_COOKIE_NAME] || '') === _authCookieValue(guiConfig);
}

