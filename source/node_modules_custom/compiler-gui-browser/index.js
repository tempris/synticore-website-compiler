function _parseGulpTasks() {
    const lines = _readText(PATH_FILE_GULP).split(/\r?\n/);
    const tasksByCategory = [];
    const variableMap = new Map();
    let currentCategory = null;
    let categoryFound = false;

    for (const line of lines) {
        const variableMatch = line.match(GULP_VARIABLE_PATTERN);
        if (variableMatch && variableMatch.groups) {
            variableMap.set(variableMatch.groups.var_name, variableMatch.groups.var_value);
            continue;
        }

        const categoryMatch = line.match(GULP_CATEGORY_PATTERN);
        if (categoryMatch && categoryMatch.groups) {
            currentCategory = {
                title: String(categoryMatch.groups.category || '').trim(),
                comment: String(categoryMatch.groups.category_comment || '').trim(),
                tasks: []
            };
            tasksByCategory.push(currentCategory);
            categoryFound = true;
            continue;
        }

        if (!categoryFound || !currentCategory) continue;

        const taskMatch = line.match(GULP_TASK_PATTERN);
        if (!taskMatch || !taskMatch.groups) continue;

        const taskRaw = String(taskMatch.groups.task || '').trim().replace(/^'|'$/g, '');
        const taskResolved = variableMap.get(taskRaw) || taskRaw;
        const taskComment = String(taskMatch.groups.task_comment || '').trim();
        currentCategory.tasks.push({
            name: taskResolved,
            comment: taskComment
        });
    }

    return tasksByCategory;
}

function _isCompilerRepositoryInGit() {
    try {
        const gitResult = NODE_CHILD_PROCESS.spawnSync('git', ['rev-parse', '--is-inside-work-tree'], {
            cwd: PATH_DIR_ROOT,
            encoding: 'utf8',
            stdio: ['ignore', 'pipe', 'pipe'],
            windowsHide: true
        });
        if (gitResult && gitResult.status === 0 && String(gitResult.stdout || '').trim() === 'true') {
            return true;
        }
    } catch {
        // Fall through to filesystem detection when git is unavailable.
    }

    return NODE_FS.existsSync(NODE_PATH.join(PATH_DIR_ROOT, '.git'));
}

