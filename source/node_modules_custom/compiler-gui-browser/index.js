function _normalizeLogNamePart(value, fallback = 'unknown') {
    const normalized = String(value || '')
        .trim()
        .toLowerCase()
        .replace(/[^a-z0-9]+/g, '-')
        .replace(/^-+|-+$/g, '');
    return normalized || fallback;
}

function _getBrowserNameFromUserAgent(userAgent = '') {
    const raw = String(userAgent || '');
    if (!raw) return 'browser';
    if (/edg\//i.test(raw)) return 'edge';
    if (/opr\/|opera/i.test(raw)) return 'opera';
    if (/firefox\/|fxios\//i.test(raw)) return 'firefox';
    if (/chrome\/|crios\//i.test(raw)) return 'chrome';
    if (/safari\//i.test(raw) && !/chrome\/|crios\/|android/i.test(raw)) return 'safari';
    return 'browser';
}

function _getClientLogContext(req, clientInput) {
    const client = clientInput && typeof clientInput === 'object' && !Array.isArray(clientInput)
        ? clientInput
        : {};
    const userAgent = String(client.userAgent || req.headers['user-agent'] || '').trim();
    const browserName = _normalizeLogNamePart(client.browserName || _getBrowserNameFromUserAgent(userAgent), 'browser');
    const deviceType = _normalizeLogNamePart(client.deviceType || 'device', 'device');
    const platformName = _normalizeLogNamePart(client.platformName || client.deviceName || deviceType, deviceType);
    const deviceName = _normalizeLogNamePart(client.deviceName || client.platformName || deviceType, deviceType);
    const clientSlug = `gui-client-${deviceName}-${browserName}`;

    return {
        browserName,
        deviceName,
        deviceType,
        platformName,
        userAgent,
        clientSlug
    };
}

function _getFrontendLogger(clientContext) {
    const slug = _normalizeLogNamePart(
        clientContext && clientContext.clientSlug,
        'gui-client-device-browser'
    );

    if (!FRONTEND_LOGGER_CACHE.has(slug)) {
        const logger = new NODE_CURE_LOG.Log({
            filePath: {
                log: NODE_PATH.join(PATH_DIR_ROOT, '_log', `${slug}.log`),
                config: PATH_FILE_LOG_CONFIG
            }
        });
        FRONTEND_LOGGER_CACHE.set(slug, logger);
        _log('detail', LOG_TAG_GUI_BROWSER_API, 'Created GUI client log target.', {
            clientSlug: slug,
            deviceName: clientContext && clientContext.deviceName || '',
            browserName: clientContext && clientContext.browserName || ''
        });
    }

    return FRONTEND_LOGGER_CACHE.get(slug);
}

function _buildFrontendLogRecord(clientContext, level, tag, message, payload) {
    const logger = _getFrontendLogger(clientContext);
    if (!logger || typeof logger.buildRecord !== 'function') {
        return null;
    }

    try {
        if (payload === undefined) {
            return logger.buildRecord(level, tag, message);
        }
        return logger.buildRecord(level, tag, message, payload);
    } catch {
        return null;
    }
}

function _logFrontend(clientContext, level, tag, message, payload) {
    const logger = _getFrontendLogger(clientContext);
    if (level === 'fatal' && typeof logger.record === 'function') {
        if (payload === undefined) {
            logger.record('fatal', tag, message);
            return;
        }
        logger.record('fatal', tag, message, payload);
        return;
    }
    const method = typeof logger[level] === 'function' ? logger[level].bind(logger) : logger.info.bind(logger);
    if (payload === undefined) {
        method(tag, message);
        return;
    }
    method(tag, message, payload);
}

