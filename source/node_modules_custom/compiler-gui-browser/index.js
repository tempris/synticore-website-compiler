async function _handleApi(req, res) {
    const url = new URL(req.url, 'http://127.0.0.1');
    const guiConfig = _loadGuiConfig();
    _log('debug', LOG_TAG_GUI_BROWSER_API, 'Handling request.', {
        method: req.method,
        path: url.pathname
    });

    if (url.pathname === '/api/auth' && req.method === 'POST') {
        const body = await _parseJsonBody(req);
        if (!_networkAuthRequired(guiConfig)) {
            _jsonResponse(res, 200, { ok: true, authRequired: false, auth: _networkAuthMetadata(guiConfig) });
            return true;
        }

        const submittedToken = _normalizeNetworkToken(
            body.token,
            guiConfig.network_token_charset,
            guiConfig.network_token_group_length,
            guiConfig.network_token_group_count
        );
        if (!submittedToken || submittedToken !== guiConfig.network_token) {
            _log('warn', LOG_TAG_GUI_BROWSER_API, 'Rejected invalid LAN GUI token.');
            _jsonResponse(res, 401, {
                ok: false,
                authRequired: true,
                message: 'Invalid token.',
                auth: _networkAuthMetadata(guiConfig)
            });
            return true;
        }

        _setAuthCookie(res, guiConfig);
        _log('success', LOG_TAG_GUI_BROWSER_API, 'Authenticated LAN GUI session.');
        _jsonResponse(res, 200, { ok: true, authRequired: false, auth: _networkAuthMetadata(guiConfig) });
        return true;
    }

    if (url.pathname === '/api/auth/logout' && req.method === 'POST') {
        _clearAuthCookie(res);
        _jsonResponse(res, 200, { ok: true });
        return true;
    }

    if (!_requestAuthorized(req, guiConfig)) {
        _log('warn', LOG_TAG_GUI_BROWSER_API, 'Rejected unauthorized request.', {
            method: req.method,
            path: url.pathname
        });
        _unauthorizedResponse(res, guiConfig);
        return true;
    }

    if (url.pathname === '/api/bootstrap' && req.method === 'GET') {
        _jsonResponse(res, 200, _getBootstrapPayload());
        return true;
    }

    if (url.pathname === '/api/network-info' && req.method === 'GET') {
        const network = Object.assign({}, _getServerRuntimeForConfig(guiConfig, {
            port: SERVER_RUNTIME.port,
            host: SERVER_RUNTIME.host
        }), {
            trusted: guiConfig.network_trusted,
            public: guiConfig.network_public
        });
        _jsonResponse(res, 200, { ok: true, guiConfig, network });
        return true;
    }

    if (url.pathname === '/api/gui-config' && req.method === 'POST') {
        const body = await _parseJsonBody(req);
        if (!body || typeof body !== 'object' || Array.isArray(body)) {
            _jsonResponse(res, 400, { ok: false, message: 'GUI config patch must be an object.' });
            return true;
        }

        const previousNetworkPublic = Boolean(guiConfig.network_public);
        Object.assign(guiConfig, body);
        _normalizeGuiConfig(guiConfig);

        _saveGuiConfig(guiConfig);
        const restartScheduled = previousNetworkPublic !== Boolean(guiConfig.network_public);
        const network = Object.assign({}, _getServerRuntimeForConfig(guiConfig, {
            port: SERVER_RUNTIME.port,
            host: guiConfig.network_public ? '0.0.0.0' : '127.0.0.1'
        }), {
            trusted: guiConfig.network_trusted,
            public: guiConfig.network_public
        });
        _log('success', LOG_TAG_GUI_BROWSER_API, 'Updated GUI config.', {
            theme: guiConfig.theme,
            showButtonTitles: guiConfig.show_button_titles,
            patchKeys: Object.keys(body),
            restartScheduled
        });
        _jsonResponse(res, 200, { ok: true, guiConfig, network, restartScheduled });
        if (restartScheduled) {
            setTimeout(() => {
                restartBrowserGui().catch((error) => {
                    _log('error', LOG_TAG_GUI_BROWSER, 'Automatic GUI server restart failed.', {
                        message: String(error.message || error),
                        stack: error && error.stack ? String(error.stack) : ''
                    });
                });
            }, 25);
        }
        return true;
    }

    if (url.pathname === '/api/log' && req.method === 'POST') {
        const body = await _parseJsonBody(req);
        const level = String(body.level || 'info').trim().toLowerCase();
        const tag = String(body.tag || '[GUI Browser UI]').trim() || '[GUI Browser UI]';
        const message = String(body.message || '').trim() || 'Browser GUI event';
        const payload = body.payload && typeof body.payload === 'object' ? body.payload : undefined;
        const clientContext = _getClientLogContext(req, body.client);
        const record = _buildFrontendLogRecord(clientContext, level, tag, message, payload);
        _logFrontend(clientContext, level, tag, message, payload);
        _jsonResponse(res, 200, { ok: true, record, client: clientContext });
        return true;
    }

    if (url.pathname === '/api/project-info' && req.method === 'GET') {
        const projectDir = url.searchParams.get('dir') || '';
        _jsonResponse(res, 200, _getProjectVersionInfo(projectDir));
        return true;
    }

    if (url.pathname === '/api/select-project' && req.method === 'POST') {
        const body = await _parseJsonBody(req);
        const requested = String(body.dir || '').trim();
        const guiConfig = _loadGuiConfig();
        const normalized = _normalizeStoredProjectDir(requested);

        if (!_validateProjectDirectory(normalized)) {
            _log('warn', LOG_TAG_GUI_BROWSER_API, 'Rejected invalid project selection.', {
                requested,
                normalized
            });
            _jsonResponse(res, 400, {
                ok: false,
                message: `Invalid directory: ${requested}`
            });
            return true;
        }

        guiConfig.dir_recent = _normalizeRecentProjectList(
            [normalized, ...(Array.isArray(guiConfig.dir_recent) ? guiConfig.dir_recent : [])],
            guiConfig.dir_recent_max
        );
        _saveGuiConfig(guiConfig);
        _log('success', LOG_TAG_GUI_BROWSER_API, 'Selected project.', {
            requested,
            selectedProject: normalized
        });
        const taskPayload = _getTasksPayload();

        _jsonResponse(res, 200, {
            ok: true,
            guiConfig,
            selectedProject: normalized,
            tasksByCategory: taskPayload.tasksByCategory,
            testTaskGroupAdded: taskPayload.testTaskGroupAdded,
            projectVersion: _getProjectVersionInfo(normalized)
        });
        return true;
    }

    if (url.pathname === '/api/dev/recents/archive' && req.method === 'POST') {
        const liveRecents = _normalizeRecentProjectList(guiConfig.dir_recent, guiConfig.dir_recent_max);
        const existingStash = _normalizeRecentProjectList(guiConfig.dev_dir_recent_stash, guiConfig.dir_recent_max);
        const nextStash = _normalizeRecentProjectList(
            [...liveRecents, ...existingStash],
            guiConfig.dir_recent_max
        );

        guiConfig.dev_dir_recent_stash = nextStash;
        guiConfig.dir_recent = [];
        _saveGuiConfig(guiConfig);
        _log('success', LOG_TAG_GUI_BROWSER_API, 'Archived GUI recent projects into dev stash.', {
            archivedCount: liveRecents.length,
            stashCount: nextStash.length
        });
        _jsonResponse(res, 200, {
            ok: true,
            guiConfig,
            archivedCount: liveRecents.length,
            stashCount: nextStash.length
        });
        return true;
    }

    if (url.pathname === '/api/dev/recents/restore' && req.method === 'POST') {
        const liveRecents = _normalizeRecentProjectList(guiConfig.dir_recent, guiConfig.dir_recent_max);
        const stashedRecents = _normalizeRecentProjectList(guiConfig.dev_dir_recent_stash, guiConfig.dir_recent_max);
        const nextRecents = _normalizeRecentProjectList(
            [...stashedRecents, ...liveRecents],
            guiConfig.dir_recent_max
        );

        guiConfig.dir_recent = nextRecents;
        guiConfig.dev_dir_recent_stash = [];
        _saveGuiConfig(guiConfig);
        _log('success', LOG_TAG_GUI_BROWSER_API, 'Restored GUI recent projects from dev stash.', {
            restoredCount: stashedRecents.length,
            recentCount: nextRecents.length
        });
        _jsonResponse(res, 200, {
            ok: true,
            guiConfig,
            restoredCount: stashedRecents.length,
            recentCount: nextRecents.length
        });
        return true;
    }

    if (url.pathname === '/api/browse/directory' && req.method === 'POST') {
        const body = await _parseJsonBody(req);
        const selected = _browseDirectory(String(body.initialDir || ''));
        _log('detail', LOG_TAG_GUI_BROWSER_API, 'Completed directory browse request.', {
            initialDir: String(body.initialDir || ''),
            selectedDir: selected || ''
        });
        _jsonResponse(res, 200, { ok: true, dir: selected });
        return true;
    }

    if (url.pathname === '/api/config' && req.method === 'GET') {
        try {
            const mode = url.searchParams.get('mode') || 'project';
            const dir = url.searchParams.get('dir') || '';
            const payload = _getConfigPayload(mode, dir);
            _log('info', LOG_TAG_GUI_BROWSER_API, 'Loaded config payload.', {
                mode,
                dir,
                path: payload.path
            });
            _jsonResponse(res, 200, payload);
        } catch (error) {
            _log('error', LOG_TAG_GUI_BROWSER_API, 'Failed to load config payload.', {
                message: String(error.message || error),
                mode: url.searchParams.get('mode') || 'project',
                dir: url.searchParams.get('dir') || ''
            });
            _jsonResponse(res, 400, { ok: false, message: String(error.message || error) });
        }
        return true;
    }

    if (url.pathname === '/api/config-model' && req.method === 'GET') {
        try {
            const mode = url.searchParams.get('mode') || 'project';
            const dir = url.searchParams.get('dir') || '';
            const model = _getConfigModel(mode, dir);
            _log('info', LOG_TAG_GUI_BROWSER_API, 'Loaded config model.', {
                mode,
                dir,
                path: model.path,
                schemaEntryCount: model.schemaEntries.length
            });
            _jsonResponse(res, 200, model);
        } catch (error) {
            _log('error', LOG_TAG_GUI_BROWSER_API, 'Failed to load config model.', {
                message: String(error.message || error),
                mode: url.searchParams.get('mode') || 'project',
                dir: url.searchParams.get('dir') || ''
            });
            _jsonResponse(res, 400, { ok: false, message: String(error.message || error) });
        }
        return true;
    }

    if (url.pathname === '/api/wiki' && req.method === 'GET') {
        try {
            const page = url.searchParams.get('page') || WIKI_ROUTE_ROOT;
            const payload = _renderWikiDocument(page);
            _jsonResponse(res, 200, payload);
        } catch (error) {
            _jsonResponse(res, 404, { ok: false, message: String(error.message || error) });
        }
        return true;
    }

    if (url.pathname === '/api/config-watch' && req.method === 'GET') {
        try {
            const mode = url.searchParams.get('mode') || 'project';
            const dir = url.searchParams.get('dir') || '';
            _jsonResponse(res, 200, _getConfigWatchPayload(mode, dir));
        } catch (error) {
            _jsonResponse(res, 400, { ok: false, message: String(error.message || error) });
        }
        return true;
    }

    if (url.pathname === '/api/config-ui-cache' && req.method === 'POST') {
        try {
            const body = await _parseJsonBody(req);
            const mode = String(body.mode || 'project');
            const dir = String(body.dir || '');
            const cache = _saveConfigUiCache(mode, dir, body.cache || {});
            _jsonResponse(res, 200, { ok: true, cache });
        } catch (error) {
            _jsonResponse(res, 400, { ok: false, message: String(error.message || error) });
        }
        return true;
    }

    if (url.pathname === '/api/config' && req.method === 'POST') {
        try {
            const body = await _parseJsonBody(req);
            const mode = String(body.mode || 'project');
            const dir = String(body.dir || '');
            const target = _configPathForMode(mode, dir);
            const normalizedMode = String(mode || '').trim().toLowerCase();
            const isProjectMode = normalizedMode === 'project';
            if (body.data && typeof body.data === 'object') {
                const defaultConfig = _loadJsonSafe(_defaultConfigPathForMode(mode), {});
                const diff = _diffAgainstDefault(body.data, defaultConfig);
                const toWrite = diff === undefined ? {} : diff;
                const sortEnabled = _getConfigValue(body.data, 'option.config.sort', defaultConfig) === true;
                NODE_CURE_JSON.save(target.path, toWrite, { sort: sortEnabled, spacing: 4 });
            } else {
                const content = String(body.content || '');
                JSON.parse(content);
                NODE_CURE_FS.writeFileSync(target.path, `${content.replace(/\s*$/, '')}\n`);
            }

            if (isProjectMode && Object.prototype.hasOwnProperty.call(body, 'projectSchema')) {
                const projectSchemaPath = _projectSchemaPath(dir);
                if (!projectSchemaPath) {
                    throw new Error('Missing project schema path.');
                }
                const schemaDocument = (
                    body.projectSchema
                    && typeof body.projectSchema === 'object'
                    && !Array.isArray(body.projectSchema)
                ) ? _deepClone(body.projectSchema) : {};
                NODE_CURE_JSON.save(projectSchemaPath, schemaDocument, { sort: true, spacing: 4 });
            }

            _log('success', LOG_TAG_GUI_BROWSER_API, 'Saved config.', {
                mode,
                dir,
                path: target.path
            });
            _jsonResponse(res, 200, {
                ok: true,
                config: _getConfigPayload(mode, dir),
                model: _getConfigModel(mode, dir)
            });
        } catch (error) {
            _log('error', LOG_TAG_GUI_BROWSER_API, 'Failed to save config.', {
                message: String(error.message || error)
            });
            _jsonResponse(res, 400, { ok: false, message: String(error.message || error) });
        }
        return true;
    }

    if (url.pathname === '/api/task/run' && req.method === 'POST') {
        const body = await _parseJsonBody(req);
        const task = String(body.task || '').trim();
        const projectDir = String(body.dir || '').trim();
        if (!task) {
            _log('warn', LOG_TAG_GUI_BROWSER_API, 'Rejected task launch with missing task.');
            _jsonResponse(res, 400, { ok: false, message: 'Task is required.' });
            return true;
        }

        const scriptArgs = [task];
        if (projectDir) {
            scriptArgs.push('--project', _pathToPosix(_resolveProjectDir(projectDir)));
        }
        _spawnNpmScript('gulp', scriptArgs);
        _log('success', LOG_TAG_GUI_BROWSER_API, 'Launched task.', {
            task,
            projectDir
        });
        _jsonResponse(res, 200, { ok: true, message: `Launched task: ${task}` });
        return true;
    }

    if (url.pathname === '/api/project/new' && req.method === 'POST') {
        const body = await _parseJsonBody(req);
        const targetDir = String(body.dir || '').trim();
        const template = String(body.template || 'basic').trim() || 'basic';
        const force = Boolean(body.force);
        if (!targetDir) {
            _log('warn', LOG_TAG_GUI_BROWSER_API, 'Rejected new-project launch with missing target directory.');
            _jsonResponse(res, 400, { ok: false, message: 'New project location is required.' });
            return true;
        }

        const scriptArgs = ['--dir', targetDir, '--template', template];
        if (force) scriptArgs.push('--force');
        _spawnNpmScript('project:new', scriptArgs);
        _log('success', LOG_TAG_GUI_BROWSER_API, 'Launched new project flow.', {
            targetDir,
            template,
            force
        });
        _jsonResponse(res, 200, { ok: true, message: `Launched project:new for ${targetDir}` });
        return true;
    }

    if (url.pathname === '/api/open/explorer' && req.method === 'POST') {
        const body = await _parseJsonBody(req);
        const projectDir = _resolveProjectDir(body.dir || '');
        if (!projectDir || !NODE_FS.existsSync(projectDir)) {
            _log('warn', LOG_TAG_GUI_BROWSER_API, 'Rejected explorer open for invalid directory.', {
                requestedDir: String(body.dir || '')
            });
            _jsonResponse(res, 400, { ok: false, message: 'Select a valid directory first.' });
            return true;
        }
        _openExplorer(projectDir);
        _log('success', LOG_TAG_GUI_BROWSER_API, 'Opened directory in file browser.', { projectDir });
        _jsonResponse(res, 200, { ok: true });
        return true;
    }

    if (url.pathname === '/api/open/terminal-root' && req.method === 'POST') {
        _openRootTerminal();
        _log('success', LOG_TAG_GUI_BROWSER_API, 'Opened compiler-root terminal.', { cwd: PATH_DIR_ROOT });
        _jsonResponse(res, 200, { ok: true });
        return true;
    }

    if (url.pathname.startsWith('/resource/') && req.method === 'GET') {
        const relative = url.pathname.slice('/resource/'.length);
        const targetPath = NODE_PATH.join(PATH_DIR_RESOURCE, relative);
        const resolved = NODE_PATH.resolve(targetPath);
        if (!resolved.startsWith(PATH_DIR_RESOURCE) || !NODE_FS.existsSync(resolved)) {
            _textResponse(res, 404, 'Not found', 'text/plain');
            return true;
        }

        const ext = NODE_PATH.extname(resolved).toLowerCase();
        const type = ext === '.png' ? 'image/png'
            : ext === '.svg' ? 'image/svg+xml'
                : ext === '.ico' ? 'image/x-icon'
                    : 'application/octet-stream';
        res.statusCode = 200;
        res.setHeader('Content-Type', type);
        NODE_FS.createReadStream(resolved).pipe(res);
        return true;
    }

    return false;
}

async function _startBrowserGuiInstance(options = {}) {
    const guiConfig = _loadGuiConfig();
    _saveGuiConfig(guiConfig);

    const runtime = await _resolveAvailableRuntime(guiConfig, options);
    const { host, port, localUrl, lanUrl, authRequired } = runtime;
    const open = options.open !== false;
    _applyServerRuntime(runtime);

    _log('begin', LOG_TAG_GUI_BROWSER, 'Starting browser GUI server.', {
        host,
        port,
        open,
        networkPublic: guiConfig.network_public,
        networkTrusted: guiConfig.network_trusted,
        authRequired
    });

    if (!NODE_FS.existsSync(PATH_FILE_GUI_INDEX)) {
        _log('error', LOG_TAG_GUI_BROWSER, 'Built GUI output missing.', { path: PATH_DIR_GUI_WEB });
        throw new Error(
            `Built GUI output not found at ${PATH_DIR_GUI_WEB}. Build source/project/gui first so BrowserSync can serve source/project/gui/out/.`
        );
    }

    if (!NODE_FS.existsSync(PATH_FILE_GUI_LAN_ACCESS)) {
        _log('error', LOG_TAG_GUI_BROWSER, 'Built LAN access GUI output missing.', { path: PATH_FILE_GUI_LAN_ACCESS });
        throw new Error(
            `Built LAN access GUI output not found at ${PATH_FILE_GUI_LAN_ACCESS}. Build source/project/gui so BrowserSync can serve the LAN access page.`
        );
    }

    await new Promise((resolve, reject) => {
        let settled = false;
        const finish = (error) => {
            if (settled) return;
            settled = true;
            if (error) {
                reject(error);
                return;
            }
            resolve();
        };

        const fallbackId = setTimeout(() => {
            finish();
        }, 1500);

        BROWSER_SYNC.init({
            host,
            port,
            open,
            notify: false,
            ghostMode: false,
            ui: false,
            files: [
                NODE_PATH.join(PATH_DIR_GUI_WEB, '**', '*')
            ],
            callbacks: {
                ready() {
                    clearTimeout(fallbackId);
                    finish();
                }
            },
            server: {
                baseDir: PATH_DIR_GUI_WEB,
                middleware: [
                    async (req, res, next) => {
                        try {
                            const url = new URL(req.url, 'http://127.0.0.1');
                            const guiConfig = _loadGuiConfig();

                            if (
                                req.method === 'GET'
                                && !url.pathname.startsWith('/api/')
                            ) {
                                const authorized = _requestAuthorized(req, guiConfig);

                                if (!authorized) {
                                    if (_isPublicGuiPath(url.pathname)) {
                                        next();
                                        return;
                                    }

                                    _log('warn', LOG_TAG_GUI_BROWSER, 'Redirecting unauthorized request to built LAN access page.', {
                                        path: url.pathname
                                    });
                                    res.statusCode = 302;
                                    res.setHeader('Location', '/lan-access/');
                                    res.end();
                                    return;
                                }

                                if (_isLanAccessPagePath(url.pathname)) {
                                    res.statusCode = 302;
                                    res.setHeader('Location', '/');
                                    res.end();
                                    return;
                                }
                            }

                            const handled = await _handleApi(req, res);
                            if (!handled) next();
                        } catch (error) {
                            _log('error', LOG_TAG_GUI_BROWSER_API, 'Unhandled API error.', {
                                message: String(error.message || error),
                                stack: error && error.stack ? String(error.stack) : ''
                            });
                            _jsonResponse(res, 500, {
                                ok: false,
                                message: String(error.message || error)
                            });
                        }
                    }
                ]
            }
        });
    });

    _log('success', LOG_TAG_GUI_BROWSER, 'Browser GUI server ready.', {
        host,
        port,
        url: host === '0.0.0.0' ? localUrl : `http://${host}:${port}`,
        localUrl,
        lanUrl,
        authRequired
    });

    return {
        browserSync: BROWSER_SYNC,
        url: host === '0.0.0.0' ? localUrl : `http://${host}:${port}`,
        localUrl,
        lanUrl,
        port,
        authRequired,
        networkToken: authRequired ? guiConfig.network_token : '',
        networkTrusted: guiConfig.network_trusted
    };
}

async function restartBrowserGui() {
    if (SERVER_RESTART_PROMISE) return SERVER_RESTART_PROMISE;
    const nextOptions = Object.assign({}, ACTIVE_SERVER_OPTIONS || {}, { open: false });
    SERVER_RESTART_PROMISE = (async () => {
        _log('begin', LOG_TAG_GUI_BROWSER, 'Restarting browser GUI server.');
        await _stopBrowserGuiServer();
        const result = await _startBrowserGuiInstance(nextOptions);
        _log('success', LOG_TAG_GUI_BROWSER, 'Restarted browser GUI server.', {
            host: SERVER_RUNTIME.host,
            port: SERVER_RUNTIME.port,
            localUrl: SERVER_RUNTIME.localUrl,
            lanUrl: SERVER_RUNTIME.lanUrl
        });
        return result;
    })();

    try {
        return await SERVER_RESTART_PROMISE;
    } finally {
        SERVER_RESTART_PROMISE = null;
    }
}

async function startBrowserGui(options = {}) {
    ACTIVE_SERVER_OPTIONS = Object.assign({}, options);
    return _startBrowserGuiInstance(ACTIVE_SERVER_OPTIONS);
}

module.exports = {
    startBrowserGui,
    restartBrowserGui
};
