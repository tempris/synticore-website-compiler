function _resolvePathFromRoot(pathInput) {
    const raw = String(pathInput || '').trim().replace(/^['"]|['"]$/g, '');
    if (!raw) return '';
    if (NODE_PATH.isAbsolute(raw)) return NODE_PATH.normalize(raw);
    return NODE_PATH.normalize(NODE_PATH.join(PATH_DIR_ROOT, raw));
}

function _resolveProjectDir(projectDir) {
    return _resolvePathFromRoot(projectDir);
}

function _pathToPosix(pathInput) {
    return String(pathInput || '').replace(/\\/g, '/');
}

function _settingsRelativeDirIfUnderRoot(absDir) {
    const absolute = _resolveProjectDir(absDir);
    if (!absolute) return '';

    let relativeToRoot = '';
    try {
        relativeToRoot = NODE_PATH.relative(PATH_DIR_ROOT, absolute);
    } catch {
        return '';
    }

    if (!relativeToRoot || relativeToRoot.startsWith('..') || NODE_PATH.isAbsolute(relativeToRoot)) {
        return '';
    }

    const normalized = _pathToPosix(relativeToRoot);
    return normalized.startsWith('.') ? normalized : `./${normalized}`;
}

function _normalizeExternalProjectDir(absDir) {
    const absolute = _resolveProjectDir(absDir);
    if (!absolute) return '';
    return _pathToPosix(NODE_PATH.normalize(absolute));
}

function _normalizeStoredProjectDir(projectDirInput) {
    const absolute = _resolveProjectDir(projectDirInput);
    if (!absolute) return '';

    const relative = _settingsRelativeDirIfUnderRoot(absolute);
    if (relative) return relative;

    return _normalizeExternalProjectDir(absolute);
}

function _canonicalRecentProjectKey(projectDirInput) {
    const absolute = _normalizeExternalProjectDir(projectDirInput);
    if (!absolute) return '';

    return process.platform === 'win32'
        ? absolute.toLowerCase()
        : absolute;
}

function _normalizeRecentProjectList(dirRecentInput, dirRecentMax = 64) {
    const recents = Array.isArray(dirRecentInput) ? dirRecentInput : [];
    const maxRecentRaw = Number.parseInt(dirRecentMax, 10);
    const hasLimit = Number.isFinite(maxRecentRaw) ? maxRecentRaw > 0 : true;
    const maxRecent = hasLimit ? maxRecentRaw || 64 : Infinity;
    const seen = new Set();
    const out = [];

    for (const entry of recents) {
        const stored = _normalizeStoredProjectDir(entry);
        const key = _canonicalRecentProjectKey(stored);
        if (!stored || !key || seen.has(key)) continue;
        seen.add(key);
        out.push(stored);
        if (hasLimit && out.length >= maxRecent) break;
    }

    return out;
}

