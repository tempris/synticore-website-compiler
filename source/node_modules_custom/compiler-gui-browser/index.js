function _checkPortAvailable(host, port) {
    return new Promise((resolve) => {
        const server = NODE_NET.createServer();

        server.unref();

        server.once('error', () => {
            resolve(false);
        });

        server.once('listening', () => {
            server.close(() => {
                resolve(true);
            });
        });

        try {
            server.listen(port, host);
        } catch {
            resolve(false);
        }
    });
}

async function _resolveAvailableRuntime(guiConfig, options = {}) {
    const requestedPort = Number.parseInt(options.port, 10) || 3084;
    const host = String(options.host || (guiConfig.network_public ? '0.0.0.0' : '127.0.0.1'));

    for (let offset = 0; offset < PORT_SCAN_MAX_ATTEMPTS; offset += 1) {
        const port = requestedPort + offset;
        const isAvailable = await _checkPortAvailable(host, port);
        if (!isAvailable) continue;

        if (offset > 0) {
            _log('warn', LOG_TAG_GUI_BROWSER, 'Requested GUI port unavailable; using next free port.', {
                requestedPort,
                selectedPort: port,
                host
            });
        }

        return _getServerRuntimeForConfig(guiConfig, Object.assign({}, options, { host, port }));
    }

    throw new Error(`Unable to find a free browser GUI port in range ${requestedPort}-${requestedPort + PORT_SCAN_MAX_ATTEMPTS - 1}.`);
}

