function _getNetworkTokenCharset(charset = 'alphanumeric') {
    return String(charset || '').trim().toLowerCase() === 'numeric'
        ? '0123456789'
        : 'ABCDEFGHJKLMNPQRSTUVWXYZ23456789';
}

function _normalizeNetworkToken(tokenInput, charset, groupLength, groupCount) {
    const allowedCharset = _getNetworkTokenCharset(charset);
    const normalized = String(tokenInput || '')
        .toUpperCase()
        .replace(/[^A-Z0-9]/g, '')
        .split('')
        .filter((character) => allowedCharset.includes(character))
        .join('');

    const expectedLength = Number(groupLength) * Number(groupCount);
    if (normalized.length !== expectedLength) return '';

    const groups = [];
    for (let index = 0; index < groupCount; index += 1) {
        const start = index * groupLength;
        groups.push(normalized.slice(start, start + groupLength));
    }
    return groups.join('-');
}

function _generateNetworkToken(charset, groupLength, groupCount) {
    const allowedCharset = _getNetworkTokenCharset(charset);
    const totalLength = Number(groupLength) * Number(groupCount);
    const groups = [];
    let current = '';

    while (current.length < totalLength) {
        const randomBytes = NODE_CRYPTO.randomBytes(totalLength);
        for (const value of randomBytes) {
            current += allowedCharset[value % allowedCharset.length];
            if (current.length >= totalLength) break;
        }
    }

    for (let index = 0; index < groupCount; index += 1) {
        const start = index * groupLength;
        groups.push(current.slice(start, start + groupLength));
    }

    return groups.join('-');
}

