function _getFileMtimeMs(filePath) {
    const normalizedPath = String(filePath || '').trim();
    if (!normalizedPath) return 0;
    if (!NODE_FS.existsSync(normalizedPath)) return 0;
    const stats = NODE_FS.statSync(normalizedPath);
    return stats.isFile() ? Number(stats.mtimeMs || 0) : 0;
}

function _getConfigValue(configData, path, fallbackRoot = {}) {
    const segments = String(path || '').split('.').filter(Boolean);
    let value = configData;

    for (const segment of segments) {
        if (value && typeof value === 'object' && segment in value) {
            value = value[segment];
        } else {
            value = undefined;
            break;
        }
    }

    if (value !== undefined) return value;

    let fallback = fallbackRoot;
    for (const segment of segments) {
        if (fallback && typeof fallback === 'object' && segment in fallback) {
            fallback = fallback[segment];
        } else {
            fallback = undefined;
            break;
        }
    }

    return fallback;
}

function _diffAgainstDefault(value, defaultValue) {
    if (Array.isArray(value) || Array.isArray(defaultValue)) {
        if (_valuesEqual(value, defaultValue)) return undefined;
        return _deepClone(value);
    }

    if (value && typeof value === 'object') {
        const out = {};
        const keys = Object.keys(value);
        for (const key of keys) {
            const diff = _diffAgainstDefault(value[key], defaultValue ? defaultValue[key] : undefined);
            if (diff !== undefined) {
                out[key] = diff;
            }
        }
        return Object.keys(out).length > 0 ? out : undefined;
    }

    if (_valuesEqual(value, defaultValue)) return undefined;
    return value;
}

