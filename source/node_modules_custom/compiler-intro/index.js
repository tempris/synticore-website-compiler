/**
 * Safely detect 256-color support from the `supports-color` ESM default export.
 *
 * The package types `stdout` as either a color info object or `false`, so callers
 * must narrow before reading capability flags such as `has256`.
 *
 * @param {{ stdout?: false | { has256?: boolean } } | undefined} supportsColorModule
 * @returns {boolean}
 * @since 1.0.0
 * @private
 */
function _has256ColorSupport(supportsColorModule) {
  const stdoutInfo = supportsColorModule && typeof supportsColorModule === 'object'
    ? supportsColorModule.stdout
    : undefined;

  return !!(stdoutInfo && typeof stdoutInfo === 'object' && stdoutInfo.has256);
}

