/**
 * Generate a list of **foreground** ANSI SGR color sequences forming a linear RGB gradient.
 *
 * Semantics & side effects:
 * - No I/O. Computes evenly-spaced color stops (inclusive) from `startColor` to `endColor`.
 * - When `steps === 1`, returns a single mid-point color between start and end.
 *
 * @function _generateGradient
 * @memberof module:synticore-intro
 * @param {HexRGB} startColor - Start color as 6-hex characters (no leading `#`), e.g., `"4CEFF0"`.
 * @param {HexRGB} endColor - End color as 6-hex characters, e.g., `"7259EB"`.
 * @param {number} steps - Number of color stops to generate (number of lines to color). Must be ≥ 1.
 * @returns {AnsiColorString[]} Array of ANSI SGR sequences (`\x1b[38;2;R;G;Bm`) for each step.
 * @throws {TypeError} If `steps < 1` or hex inputs are malformed (length not equal to 6).
 * @since 1.0.0
 * @private
 *
 * @example
 * const colors = _generateGradient('FF0000', '0000FF', 3);
 * // => ['\x1b[38;2;255;0;0m', '\x1b[38;2;128;0;128m', '\x1b[38;2;0;0;255m']
 */
function _generateGradient(startColor, endColor, steps) {
  if (typeof startColor !== 'string' || startColor.length !== 6 ||
      typeof endColor !== 'string' || endColor.length !== 6) {
    throw new TypeError('startColor/endColor must be 6-hex-character strings (no #).');
  }
  if (!Number.isInteger(steps) || steps < 1) {
    throw new TypeError('steps must be an integer >= 1.');
  }

  const startRGB = {
    r: parseInt(startColor.slice(0, 2), 16),
    g: parseInt(startColor.slice(2, 4), 16),
    b: parseInt(startColor.slice(4, 6), 16)
  };

  const endRGB = {
    r: parseInt(endColor.slice(0, 2), 16),
    g: parseInt(endColor.slice(2, 4), 16),
    b: parseInt(endColor.slice(4, 6), 16)
  };

  /** @type {AnsiColorString[]} */
  const gradient = [];

  if (steps === 1) {
    const midRGB = {
      r: Math.round((startRGB.r + endRGB.r) / 2),
      g: Math.round((startRGB.g + endRGB.g) / 2),
      b: Math.round((startRGB.b + endRGB.b) / 2)
    };
    gradient.push(`\x1b[38;2;${midRGB.r};${midRGB.g};${midRGB.b}m`);
    return gradient;
  }

  for (let i = 0; i < steps; i++) {
    const r = Math.round(startRGB.r + (endRGB.r - startRGB.r) * (i / (steps - 1)));
    const g = Math.round(startRGB.g + (endRGB.g - startRGB.g) * (i / (steps - 1)));
    const b = Math.round(startRGB.b + (endRGB.b - startRGB.b) * (i / (steps - 1)));
    gradient.push(`\x1b[38;2;${r};${g};${b}m`);
  }

  return gradient;
}

/**
 * Resolve the list of colors to use for a given line count, based on terminal capability.
 *
 * Semantics & side effects:
 * - If `NO_COLOR` is set (any non-empty value), returns **basic yellow** for all lines (no detection).
 * - If `forceColor256` is `true`, assumes truecolor support and returns a gradient.
 * - Otherwise, dynamically imports `supports-color` and checks `TERM`/stdio to decide.
 * - When truecolor/256 is unavailable, returns basic yellow ANSI sequence per line.
 *
 * @private
 * @async
 * @function getColors
 * @memberof module:synticore-intro
 * @param {boolean} forceColor256 - If `true`, skip detection and assume truecolor/256 support.
 * @param {number} [lineCount=1] - Number of lines that need a color (length of the result array).
 * @default 1
 * @returns {Promise<AnsiColorString[]>} Resolves to an array of per-line color sequences.
 * @throws {Error} If the dynamic import fails unexpectedly.
 * @since 1.0.0
 *
 * @example
 * const { getColors } = require('./index.js');
 * (async () => {
 *   const colors = await getColors(false, 4);
 *   console.log(colors.map(c => c + 'Line' + '\x1b[0m').join('\n'));
 * })();
 */
async function getColors(forceColor256, lineCount = 1) {
  if (!Number.isInteger(lineCount) || lineCount < 1) {
    throw new TypeError('lineCount must be an integer >= 1.');
  }

  const noColor = process.env.NO_COLOR !== undefined && process.env.NO_COLOR !== '';
  if (noColor) {
    return Array.from({ length: lineCount }, () => '');
  }

  var supports256 = forceColor256;

  if (!forceColor256) {
    const supportsColor = await import('supports-color');
    const term = process.env.TERM || '';
    supports256 = /256color/.test(term) || _has256ColorSupport(supportsColor.default);
  }

  /** @type {AnsiColorString[]} */
  const colors = [];

  if (supports256) {
    colors.push(..._generateGradient("4CEFF0", "7259EB", lineCount));
  } else {
    const yellowAnsi = '\x1b[33m';
    for (let i = 0; i < lineCount; i++) {
      colors.push(yellowAnsi);
    }
  }

  return colors;
}

/**
 * Remove only the leading/trailing blank lines from an ASCII art block.
 * Keeps internal blank lines intact.
 *
 * @function _trimOuterBlankLines
 * @memberof module:synticore-intro
 * @param {string} art - Raw ASCII art block (may contain leading/trailing newlines).
 * @returns {string} Art without outer blank lines.
 * @since 1.0.0
 * @private
 */
function _trimOuterBlankLines(art) {
  const lines = String(art).split('\n');

  while (lines.length && lines[0].trim() === '') {
    lines.shift();
  }

  while (lines.length && lines[lines.length - 1].trim() === '') {
    lines.pop();
  }

  return lines.join('\n');
}

/**
 * Colorize a single ASCII art block with either a vertical gradient or a flat color.
 *
 * Semantics & side effects:
 * - Respects `NO_COLOR`: returns the original `art` unchanged (no ANSI) when set.
 * - Otherwise determines color capability (or trusts `forceColor256`) and applies
 *   a line-wise gradient (truecolor) or a flat yellow (basic color).
 * - Trailing newline is trimmed from the returned string.
 *
 * @private
 * @async
 * @function _getArtString
 * @memberof module:synticore-intro
 * @param {boolean} forceColor256 - If `true`, assume truecolor/256 support without detection.
 * @param {string} art - ASCII art to colorize; may contain multiple newline-separated lines.
 * @returns {Promise<string>} Resolves to a colored string ready for printing (includes resets).
 * @since 1.0.0
 *
 * @example
 * const block = "Line A\nLine B\nLine C";
 * (async () => {
 *   const out = await _getArtString(false, block);
 *   console.log(out);
 * })();
 */
async function _getArtString(forceColor256, art) {
  const noColor = process.env.NO_COLOR !== undefined && process.env.NO_COLOR !== '';
  const artTrimmed = _trimOuterBlankLines(art);

  if (noColor) {
    return artTrimmed;
  }

  var supports256 = forceColor256;

  if (!forceColor256) {
    const supportsColor = await import('supports-color');
    const term = process.env.TERM || '';
    supports256 = /256color/.test(term) || _has256ColorSupport(supportsColor.default);
  }

  const lines = artTrimmed.split('\n');
  let result = "";

  if (supports256) {
    const gradientColors = _generateGradient("4CEFF0", "7259EB", lines.length);
    lines.forEach((line, index) => {
      result += `${gradientColors[index]}${line}${_resetColor}\n`;
    });
    return result.trimEnd();
  }

  return `\x1b[33m${artTrimmed}${_resetColor}`;
}

