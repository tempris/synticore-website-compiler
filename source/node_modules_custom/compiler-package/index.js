/** Cache tag appended to package log messages. @type {string} @since 1.0.0 */
const LOG_TAG_PACKAGE_CACHE = LOG_TAG_PACKAGE + ' [🗃️ Cache]';

/**
 * Compute the on-disk path for the package checksum cache within a project directory.
 *
 * @private
 * @function _getChecksumCachePath
 * @memberof module:synticore-package
 * @param {string} PATH_DIR_PROJECT - Absolute path to the project root directory (from settings).
 * @returns {string} Absolute cache file path `<project>/_cache/project/package.json`.
 * @since 1.0.0
 */
function _getChecksumCachePath(PATH_DIR_PROJECT) {
  return NODE_PATH.join(PATH_DIR_PROJECT, '_cache/project/package.json');
}

/**
 * Load (and parse) the checksum cache JSON if present.
 *
 * Behavior:
 * - On success, returns the JSON object and logs entry count.
 * - On missing file, returns `{}` and logs a notice.
 * - On parse or read failure, returns `{}` and logs a warning.
 *
 * @private
 * @function _loadChecksumCache
 * @memberof module:synticore-package
 * @param {string} path - Absolute path to the cache JSON file.
 * @returns {Record<string, {summary: string}>} Parsed cache object or an empty object.
 * @since 1.0.0
 */
function _loadChecksumCache(path) {
  try {
    if (NODE_FS.existsSync(path)) {
      const data = JSON.parse(NODE_FS.readFileSync(path, 'utf-8'));
      log.detail(LOG_TAG_PACKAGE_CACHE, 'Loaded checksum cache.', {
        entries: Object.keys(data).length,
        path
      });
      return data;
    } else {
      log.notice(LOG_TAG_PACKAGE_CACHE, 'No existing checksum cache found.', { path });
    }
  } catch (e) {
    log.warn(LOG_TAG_PACKAGE_CACHE, 'Failed to read checksum cache. Will regenerate.', {
      error: e.toString(),
      path
    });
  }
  return {};
}

/**
 * Persist the checksum cache object to disk (pretty-printed JSON).
 * Ensures the parent directory exists (`recursive: true`).
 *
 * @private
 * @function _saveChecksumCache
 * @memberof module:synticore-package
 * @param {string} path - Absolute path to write.
 * @param {Record<string, {summary: string}>} cacheObj - Cache mapping (e.g., `{ package_suffix: { summary } }`).
 * @returns {void}
 * @since 1.0.0
 */
function _saveChecksumCache(path, cacheObj) {
  const dir = NODE_PATH.dirname(path);
  if (!NODE_FS.existsSync(dir)) {
    NODE_FS.mkdirSync(dir, { recursive: true });
    log.debug(LOG_TAG_PACKAGE_CACHE, 'Created cache directory:', { dir });
  }

  NODE_CURE_FS.writeFileSync(path, JSON.stringify(cacheObj, null, 2));
  log.success(LOG_TAG_PACKAGE_CACHE, 'Checksum cache saved.', {
    entries: Object.keys(cacheObj).length,
    path
  });
}

/**
 * Compute a SHA-256 hash of a file’s contents.
 *
 * @private
 * @function _getFileHash
 * @memberof module:synticore-package
 * @param {string} filePath - Absolute path to a file.
 * @returns {string} Lowercase hex digest of the SHA-256 hash.
 * @since 1.0.0
 */
function _getFileHash(filePath) {
  const buffer = NODE_FS.readFileSync(filePath);
  return NODE_CRYPTO.createHash('sha256').update(buffer).digest('hex');
}

/**
 * Compute a stable summary hash for a set of input files.
 *
 * Algorithm:
 * - Sort absolute file paths.
 * - Hash each file by SHA-256, collect per-file hex digests.
 * - Compute a final SHA-256 over the concatenated hex digests.
 *
 * @private
 * @function _getPackageInputHashes
 * @memberof module:synticore-package
 * @param {string[]} files - Absolute file paths included in the package inputs.
 * @returns {{summary: string, hashes: string[]}} Overall `summary` and the array of per-file `hashes`.
 * @since 1.0.0
 */
function _getPackageInputHashes(files) {
  const sorted = files.sort(); // sort so output is consistent
  const hashes = sorted.map(file => _getFileHash(file));
  const summary = NODE_CRYPTO.createHash('sha256').update(hashes.join('')).digest('hex');
  return { summary, hashes };
}

