/**
 * Import and parse a JSON file with logging and error handling.
 *
 * @private
 * @function _import_json
 * @memberof module:synticore-package
 * @param {string} fullPath - Absolute path to the JSON file.
 * @param {string} [label='config'] - Friendly label shown in logs.
 * @default 'config'
 * @returns {any} Parsed JSON value.
 * @throws {never} Exits the process with code 1 on failure (after logging an error).
 * @since 1.0.0
 */
function _import_json(fullPath, label = 'config') {
  log.info(LOG_TAG_PACKAGE, 'Importing config:', {
    label,
    path: fullPath
  });
  try {
    const JSON_VALUE = NODE_CURE_JSON.load(fullPath, {fatal: false});
    // const JSON_VALUE = JSON.parse(NODE_FS.readFileSync(fullPath, 'utf-8'));
    log.detail(LOG_TAG_PACKAGE, 'Loaded config', { label, path: fullPath });
    log.debug(LOG_TAG_PACKAGE, 'Loaded config:', { label, value: JSON_VALUE });
    return JSON_VALUE;
  } catch (e) {
    log.error(LOG_TAG_PACKAGE, 'Failed to load config:', { label, path: fullPath, error: e.toString() });
    process.exit(1);
  }
}

/**
 * Deeply merge two plain objects (mutates `a`), recursing on nested objects.
 * Arrays and non-plain objects are assigned by reference.
 *
 * @private
 * @function _deep_merge
 * @memberof module:synticore-package
 * @param {Object} a - Target object to mutate/augment.
 * @param {Object} b - Source object whose properties overlay `a`.
 * @returns {Object} The mutated `a`.
 * @since 1.0.0
 */
function _deep_merge(a, b) {
  for (const key in b) {
    if (typeof b[key] === 'object' && b[key] !== null && typeof a[key] === 'object') {
      _deep_merge(a[key], b[key]);
    } else {
      a[key] = b[key];
    }
  }
  return a;
}

/**
 * Determine whether an absolute `filePath` should be excluded based on simple substring rules.
 *
 * Heuristic:
 * - Convert path to a project-relative POSIX-like string.
 * - Return `true` if any entry in `ignorePatterns` is a substring of that relative path.
 *   (Note: this is not micromatch; patterns act as raw substrings.)
 *
 * @private
 * @function _should_exclude
 * @memberof module:synticore-package
 * @param {string} filePath - Absolute file path.
 * @param {string[]} ignorePatterns - Substring patterns (from both global and local entries).
 * @param {string} rootPath - Absolute path used to compute a relative path for matching.
 * @returns {boolean} `true` when the file should be skipped.
 * @since 1.0.0
 */
function _should_exclude(filePath, ignorePatterns, rootPath) {
  try {
    const PATH_RELATIVE = NODE_PATH.relative(rootPath, filePath).replace(/\\/g, '/');
    for (const pattern of ignorePatterns) {
      if (PATH_RELATIVE.includes(pattern)) {
        log.debug(LOG_TAG_PACKAGE, 'Excluding file:', {
          file_path: filePath,
          relative_path: PATH_RELATIVE,
          ignore_pattern_matched: pattern
        });
        return true;
      }
    }
  } catch (e) {
    log.error(LOG_TAG_PACKAGE, 'Error checking exclusion:', {
      file_path: filePath,
      path_root: rootPath,
      error: e.toString()
    });
  }
  return false;
}

/**
 * Optionally remove DOM elements from an HTML file by CSS class before packaging.
 *
 * Behavior:
 * - Parses HTML with comments preserved.
 * - Removes any element whose `class` intersects the `CONFIG_OPTION_PACKAGE_HTML_IGNORE_CLASS` set.
 * - Returns the serialized HTML and a boolean indicating whether modifications occurred.
 *
 * @private
 * @function _modify_html_content
 * @memberof module:synticore-package
 * @param {string} filePath - Absolute path to an HTML file.
 * @param {Set<string>} CONFIG_OPTION_PACKAGE_HTML_IGNORE_CLASS - Class names to remove from the DOM.
 * @param {string} PATH_DIR_PROJECT_OUT - Project output directory (used only for relative log context).
 * @returns {[string|null, boolean]} Tuple `[htmlOrNull, didModify]`.
 * @since 1.0.0
 */
function _modify_html_content(filePath, CONFIG_OPTION_PACKAGE_HTML_IGNORE_CLASS, PATH_DIR_PROJECT_OUT) {
  log.detail(LOG_TAG_PACKAGE, 'Searching HTML file for potential modifications:', { file_path: filePath });
  try {
    if (!NODE_FS.existsSync(filePath)) throw new Error('File not found');
    const HTML_FILE_CONTENT = NODE_FS.readFileSync(filePath, 'utf-8');
    const HTML_FILE_ROOT = NODE_HTML_PARSER.parse(HTML_FILE_CONTENT, { comment: true });
    let didModify = false;
    let changeSummary = [];

    HTML_FILE_ROOT.querySelectorAll('*').forEach(element => {
      const ELEMENT_ATTRIBUTE_CLASS = element.getAttribute('class');
      if (ELEMENT_ATTRIBUTE_CLASS) {
        const ELEMENT_CLASS_SET = new Set(ELEMENT_ATTRIBUTE_CLASS.split(/\s+/));
        const ELEMENT_CLASS_MATCHES = Array.from(ELEMENT_CLASS_SET).filter(c => CONFIG_OPTION_PACKAGE_HTML_IGNORE_CLASS.has(c));
        if (ELEMENT_CLASS_MATCHES.length > 0) {
          changeSummary.push({ action: 'remove', match_preview: element.toString().slice(0, 100) + '...' });
          element.remove();
          didModify = true;
        }
      }
    });

    if (!didModify) {
      log.detail(LOG_TAG_PACKAGE, 'No modification needed.');
    } else {
      log.detail(LOG_TAG_PACKAGE, 'Modified HTML:', {
        file_path_relative: NODE_PATH.relative(PATH_DIR_PROJECT_OUT, filePath),
        Output_content_bytes: Buffer.byteLength(HTML_FILE_ROOT.toString(), 'utf8'),
        change_summary: changeSummary
      });
    }

    return [HTML_FILE_ROOT.toString(), didModify];
  } catch (e) {
    log.error(LOG_TAG_PACKAGE, 'Failed to modify HTML file:', { file_path: filePath, error: e.toString() });
    return [null, false];
  }
}

/**
 * Validate that each configured `paths[*].source` resolves to at least one **existing directory**.
 * Logs success/warnings per pattern.
 *
 * @private
 * @function _all_target_sources_exist
 * @memberof module:synticore-package
 * @param {Object} packageTarget - One target from `option.package.targets`.
 * @param {string} basePath - Project directory used as the base for `pathEntry.source` globs.
 * @returns {boolean} `true` if every `source` glob yields at least one existing directory; otherwise `false`.
 * @since 1.0.0
 */
function _all_target_sources_exist(packageTarget, basePath) {
  for (const pathEntry of packageTarget.paths || []) {
    const sourcePattern = NODE_PATH.join(basePath, pathEntry.source);
    const matches = NODE_GLOB.sync(sourcePattern, { nodir: false });
    if (!matches.length || matches.every(p => !NODE_FS.existsSync(p) || !NODE_FS.statSync(p).isDirectory())) {
      log.warn(LOG_TAG_PACKAGE, 'No valid directories found for source pattern:', {
        source: sourcePattern,
        matches
      });
      return false;
    } else {
      log.success(LOG_TAG_PACKAGE, 'Target source exists:', { source_pattern: sourcePattern });
    }
  }
  log.success(LOG_TAG_PACKAGE, 'Verified all target sources exist.');
  return true;
}

/**
 * Run the Synticore packaging process.
 *
 * Workflow:
 * 1) Load Synticore root settings and project config, merge with defaults.
 * 2) For each packaging target:
 *    - Verify source directories exist.
 *    - Build a full file list honoring ignore rules; compute current summary hash.
 *    - If outputs exist and the cache summary matches, **skip** packaging.
 *    - Otherwise (re)build ZIP and/or directory:
 *      * Optionally prune HTML via {@link _modify_html_content} (by ignore classes).
 *      * Add files to `archiver` and/or mirror to the output directory.
 *    - Save the new summary into the checksum cache.
 * 3) Log completion and shut down the logger.
 *
 * Cache behavior:
 * - Cache key is `package[_suffix]` (based on target `suffix`).
 * - A target is skipped when the cache summary equals the current input hash and an output (zip or dir) exists.
 *
 * @async
 * @function processPackage
 * @memberof module:synticore-package
 * @returns {Promise<void>} Resolves when all targets are processed.
 * @throws {Error} On fatal I/O or archiver errors.
 * @since 1.0.0
 */
async function processPackage() {
  log.init(LOG_TAG_PACKAGE, 'Importing...');

  const PATH_DIR_ROOT = _getRootPath();
  const PATH_FILE_SETTINGS = NODE_PATH.resolve(PATH_DIR_ROOT, 'config/settings.json');
  const PATH_FILE_DEFAULT_CONFIG = NODE_PATH.resolve(PATH_DIR_ROOT, 'config/default/project/config.json');

  const CONFIG_STATICUS = _import_json(PATH_FILE_SETTINGS, 'settings');
  const CONFIG_STATICUS_PROJECT = CONFIG_STATICUS?.dir_recent[0] || '';
  
  if(!CONFIG_STATICUS_PROJECT) {
    log.fatal('No recent project target found in settings.');
  }

  const PATH_DIR_PROJECT = NODE_PATH.resolve(CONFIG_STATICUS_PROJECT);
  const PATH_DIR_PROJECT_OUT = NODE_PATH.resolve(PATH_DIR_PROJECT, 'out');

  const PATH_CHECKSUM_CACHE = _getChecksumCachePath(PATH_DIR_PROJECT);

  log.info(LOG_TAG_PACKAGE_CACHE, 'Using checksum cache:', { path: PATH_CHECKSUM_CACHE });

  const CONFIG_PROJECT_DEFAULT = _import_json(PATH_FILE_DEFAULT_CONFIG, 'default');
  const CONFIG_PROJECT = _import_json(NODE_PATH.resolve(PATH_DIR_PROJECT, 'config.json'), 'project');

  log.info(LOG_TAG_PACKAGE, 'Merging Project Configs...');
  const CONFIG_PROJECT_MERGE = _deep_merge(JSON.parse(JSON.stringify(CONFIG_PROJECT_DEFAULT)), CONFIG_PROJECT);
  log.success(LOG_TAG_PACKAGE, 'Merged Project Configs.');
  log.debug(LOG_TAG_PACKAGE, 'Merged Project Config:', { CONFIG_PROJECT_MERGE });

  log.init(LOG_TAG_PACKAGE, 'Import Complete.');

  const PATTERN_MODIFY = '.html';
  const CONFIG_OPTION_PACKAGE_HTML_IGNORE_CLASS = new Set(CONFIG_PROJECT_MERGE.option?.package?.html?.ignore_class || []);
  const CONFIG_OPTION_PACKAGE_OUTPUT = CONFIG_PROJECT_MERGE.option?.package?.output || {};
  const SHOULD_OUTPUT_ZIP = CONFIG_OPTION_PACKAGE_OUTPUT.zip !== false;
  const SHOULD_OUTPUT_DIR = CONFIG_OPTION_PACKAGE_OUTPUT.directory === true;
  const CONFIG_OPTION_PACKAGE_TARGETS = CONFIG_PROJECT_MERGE.option?.package?.targets || [];

  const CACHE = _loadChecksumCache(PATH_CHECKSUM_CACHE);

  for (const packageTarget of CONFIG_OPTION_PACKAGE_TARGETS) {
    const suffix = packageTarget.suffix || '';
    const file_suffix = suffix ? `_${suffix}` : '';
    const zipOutputPath = NODE_PATH.join(PATH_DIR_PROJECT, '_package', `package${file_suffix}.zip`);
    const dirOutputPath = NODE_PATH.join(PATH_DIR_PROJECT, '_package', `package${file_suffix}`);
    const packageKey = `package${file_suffix}`;

    log.begin(LOG_TAG_PACKAGE, 'Packaging group:', { packageKey, suffix, packageTarget });

    if (!_all_target_sources_exist(packageTarget, PATH_DIR_PROJECT)) {
      log.warn(LOG_TAG_PACKAGE, 'Skipping package due to missing input sources.', { suffix });
      continue;
    }

    let allFilesForHash = [];

    log.info(LOG_TAG_PACKAGE_CACHE, 'Checking cache for changes, this may take some time...');

    for (const pathEntry of packageTarget.paths || []) {
      const source = NODE_PATH.join(PATH_DIR_PROJECT, pathEntry.source);
      const useIgnore = pathEntry.no_ignore !== true;
      const globalIgnore = CONFIG_PROJECT_MERGE.option?.package?.ignore || [];
      const localIgnore = pathEntry.ignore || [];
      const combinedIgnore = useIgnore ? Array.from(new Set(globalIgnore.concat(localIgnore))) : [];

      for (const sourceDir of NODE_GLOB.sync(source, { nodir: false })) {
        const allFiles = NODE_GLOB.sync('**/*', {
          cwd: sourceDir,
          nodir: true,
          absolute: true
        });

        for (const filePath of allFiles) {
          if (useIgnore && _should_exclude(filePath, combinedIgnore, sourceDir)) continue;
          allFilesForHash.push(filePath);
        }
      }
    }

    log.debug(LOG_TAG_PACKAGE_CACHE, `Collected ${allFilesForHash.length} file(s) to hash for '${packageKey}'`, {
      first_5_files: allFilesForHash.slice(0, 5)
    });

    const { summary: currentSummary } = _getPackageInputHashes(allFilesForHash);
    if ((NODE_FS.existsSync(zipOutputPath) || NODE_FS.existsSync(dirOutputPath)) && CACHE[packageKey]?.summary === currentSummary) {
      log.notice(LOG_TAG_PACKAGE_CACHE, `Skipping package, no input changes detected:`, {
        package_key: packageKey,
        previous_hash: CACHE[packageKey]?.summary,
        current_hash: currentSummary,
        path_zip: NODE_FS.existsSync(zipOutputPath) ? zipOutputPath : null,
        path_dir: NODE_FS.existsSync(dirOutputPath) ? dirOutputPath : null
      });
      continue;
    }

    let ZIP_ARCHIVE, zipStream;
    if (SHOULD_OUTPUT_ZIP) {
      if (NODE_FS.existsSync(zipOutputPath)) {
        log.notice(LOG_TAG_PACKAGE, 'Removing existing archive:', { zip_output_path: zipOutputPath });
        NODE_CURE_FS.deleteSync([zipOutputPath], { force: true });
      }

      // Ensure parent dir exists
      const zipDir = NODE_PATH.dirname(zipOutputPath);
      NODE_FS.mkdirSync(zipDir, { recursive: true });

      zipStream = NODE_FS.createWriteStream(zipOutputPath);
      ZIP_ARCHIVE = NODE_ARCHIVER('zip', { zlib: { level: 9 } });

      // forward archiver errors to rejection
      ZIP_ARCHIVE.on('error', (err) => {
        log.error(LOG_TAG_PACKAGE, 'Archiver error:', err);
        throw err;
      });

      ZIP_ARCHIVE.pipe(zipStream);
    }

    if (SHOULD_OUTPUT_DIR) {
      if (NODE_FS.existsSync(dirOutputPath)) {
        log.warn(LOG_TAG_PACKAGE, 'Removing existing directory:', { directory_output_path: dirOutputPath });
        NODE_CURE_FS.deleteSync([dirOutputPath], { force: true });
      }
      NODE_FS.mkdirSync(dirOutputPath, { recursive: true });
    }

    for (const pathEntry of packageTarget.paths || []) {
      log.info(LOG_TAG_PACKAGE, 'Packaging target entry:', { packageTarget, pathEntry });

      const source = NODE_PATH.join(PATH_DIR_PROJECT, pathEntry.source);
      const destination = pathEntry.destination || '';
      const useIgnore = pathEntry.no_ignore !== true;
      const globalIgnore = CONFIG_PROJECT_MERGE.option?.package?.ignore || [];
      const localIgnore = pathEntry.ignore || [];
      const combinedIgnore = useIgnore ? Array.from(new Set(globalIgnore.concat(localIgnore))) : [];

      for (const sourceDir of NODE_GLOB.sync(source, { nodir: false })) {
        if (!NODE_FS.existsSync(sourceDir) || !NODE_FS.statSync(sourceDir).isDirectory()) continue;

        const allFiles = NODE_GLOB.sync('**/*', {
          cwd: sourceDir,
          nodir: true,
          absolute: true
        });

        for (const filePath of allFiles) {
          const relToSource = NODE_PATH.relative(sourceDir, filePath);
          const relOutPath = NODE_PATH.join(destination, relToSource);
          const outFullPath = NODE_PATH.join(dirOutputPath, relOutPath);

          if (useIgnore && _should_exclude(filePath, combinedIgnore, sourceDir)) continue;

          const isHtml = filePath.endsWith(PATTERN_MODIFY);
          const [modifiedHtml, didModify] = isHtml && useIgnore ? _modify_html_content(filePath, CONFIG_OPTION_PACKAGE_HTML_IGNORE_CLASS, PATH_DIR_PROJECT_OUT) : [null, false];

          log.detail(LOG_TAG_PACKAGE, 'Adding file:', { relOutPath });

          if (SHOULD_OUTPUT_ZIP) {
            if (modifiedHtml !== null) {
              ZIP_ARCHIVE.append(modifiedHtml, { name: relOutPath });
            } else {
              ZIP_ARCHIVE.file(filePath, { name: relOutPath });
            }
          }

          if (SHOULD_OUTPUT_DIR) {
            const dir = NODE_PATH.dirname(outFullPath);
            if (!NODE_FS.existsSync(dir)) NODE_FS.mkdirSync(dir, { recursive: true });
            if (modifiedHtml !== null) {
              NODE_CURE_FS.writeFileSync(outFullPath, modifiedHtml);
            } else {
              NODE_FS.copyFileSync(filePath, outFullPath);
            }
          }
        }
      }
    }

    if (SHOULD_OUTPUT_ZIP) {
      log.info(LOG_TAG_PACKAGE, 'Finalizing zip file:', { zipOutputPath });
      await ZIP_ARCHIVE.finalize();
      log.success(LOG_TAG_PACKAGE, 'Packaged ZIP successfully:', { zip_output_path: zipOutputPath });
    }

    if (SHOULD_OUTPUT_DIR) {
      log.success(LOG_TAG_PACKAGE, 'Packaged directory successfully:', { directory_output_path: dirOutputPath });
    }

    // ✅ Save new hash to cache if packaging occurred
    CACHE[packageKey] = { summary: currentSummary };
    _saveChecksumCache(PATH_CHECKSUM_CACHE, CACHE);
  }

  log.end(LOG_TAG_PACKAGE, 'Package process complete.');
  log.shutdown(LOG_TAG_PACKAGE, 'Complete.');
}

