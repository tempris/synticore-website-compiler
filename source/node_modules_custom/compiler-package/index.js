'use strict';

/**
 * @fileoverview
 * synticore-package — package a Synticore project into ZIP archives and/or a directory tree with
 * checksum-based skipping, HTML pruning, and configurable ignore patterns.
 *
 * Purpose:
 * - Load Synticore root settings and project config, deep-merge with defaults, and drive packaging targets.
 * - Walk configured source directories, optionally prune HTML nodes by CSS class, and output:
 *   - a compressed ZIP (`archiver`) and/or
 *   - a mirrored directory tree.
 * - Maintain a per-project checksum cache to skip packaging when inputs are unchanged.
 *
 * Runtime & module system:
 * - Runtime: Node.js (LTS).
 * - Module system: CommonJS (CJS).
 *
 * Side effects on import:
 * - None. All I/O is performed when {@link module:synticore-package.processPackage} runs.
 *
 * Globals & environment:
 * - Uses Node globals (`process`, `__dirname`) and standard libraries only.
 *
 * Security & permission assumptions:
 * - Filesystem-only module (no network/shell). Reads configs and project files, writes to `_package/` and cache path.
 * - Callers must ensure config paths are trusted. Paths are resolved relative to the Synticore repo root / project dir.
 *
 * I/O contract:
 * - Reads JSON at: `config/settings.json`, `config/default/project/config.json`, and `<project>/config.json`.
 * - Reads/writes many project files under `<project>`; writes `_package/package[_suffix].zip` and/or directory.
 * - Persists cache JSON at `<project>/_cache/project/package.json`.
 *
 * Concurrency & async:
 * - High-level driver {@link processPackage} is `async`. Inside, file processing and zipping is sequential per target entry.
 * - `archiver` streams internally; backpressure is handled via its API. No explicit worker pools.
 *
 * Compatibility & performance:
 * - O(N) over matched files. Hashing cost dominates first run; subsequent runs often skip by cache.
 * - HTML pruning uses `node-html-parser` with `comment: true`.
 *
 * Versioning policy:
 * - SemVer. Breaking changes to config shape / output semantics bump MAJOR.
 *
 * @module synticore-package
 * @version 1.0.0
 * @since 1.0.0
 * @license MIT
 * @author Cure Interactive
 *
 * @example <caption>Programmatic usage (runs from Synticore root)</caption>
 * // const { processPackage } = require('synticore-package');
 * // (async () => { await processPackage(); })();
 */

