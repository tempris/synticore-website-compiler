/**
 * Options controlling the behavior of {@link module:synticore-string.titleCase}.
 *
 * Notes:
 * - `acronyms` and `propercaseWords` are matched **case-insensitively**, and the
 *   original provided token (e.g., `'API'`, `'JavaScript'`) is emitted verbatim.
 * - `lowercaseWords` are compared against the lowercased token; provide entries in **lowercase**
 *   (e.g., `['of','and','the']`) to ensure matches.
 * - If `delimiter` is a **string**, it is passed into `new RegExp(delimiter)` and therefore
 *   treated as a **pattern**, not a literal; escape special regex characters if needed.
 *
 * @typedef {{
 *   acronyms?: string[];
 *   lowercaseWords?: string[];
 *   propercaseWords?: string[];
 *   splitCamelCase?: boolean;
 *   delimiter?: RegExp | string;
 *   joinSeparator?: string;
 *   preserveCasing?: boolean;
 * }} TitleCaseOptions
 *
 * @property {string[]} [acronyms=[]]
 *   Words to keep uppercased verbatim (e.g., `['API','HTML','CPU']`). Matched case-insensitively.
 *   @default []
 *
 * @property {string[]} [lowercaseWords=[]]
 *   Connector/stop words to force lowercase **unless they are the first token**. Provide entries in lowercase
 *   (e.g., `['of','and','the','a','an','to','in','on','for','with']`).
 *   @default []
 *
 * @property {string[]} [propercaseWords=[]]
 *   Terms that must be emitted exactly as provided (e.g., `['JavaScript','TypeScript','iOS','eBay']`).
 *   Matched case-insensitively.
 *   @default []
 *
 * @property {boolean} [splitCamelCase=false]
 *   Whether to split camelCase boundaries before processing (e.g., `"myAPICall"` → `"my API Call"`).
 *   @default false
 *
 * @property {RegExp|string} [delimiter=/[\s_\-]+/]
 *   Pattern (or pattern string) used to split the input into tokens.
 *   If a string is supplied, it becomes `new RegExp(delimiter)`.
 *   @default /[\s_\-]+/
 *
 * @property {string} [joinSeparator=' ']
 *   Character(s) used to join the transformed tokens.
 *   @default ' '
 *
 * @property {boolean} [preserveCasing=false]
 *   When `true`, keeps each token’s original inner casing and only uppercases the first character
 *   (e.g., `"eBay"` remains `"EBay"` if not listed in `propercaseWords`). When `false`, tokens are
 *   lowercased before capitalizing (classic Title Case).
 *   @default false
 *
 * @since 1.0.0
 */

/**
 * Convert a string to Title Case with configurable rules.
 *
 * Behavior:
 * 1. (Optional) Split camelCase boundaries if `splitCamelCase` is true.
 * 2. Split the input by `delimiter` (RegExp or string pattern).
 * 3. For each token:
 *    - If it matches `acronyms` (case-insensitive) → emit the acronym verbatim.
 *    - Else if it matches `propercaseWords` (case-insensitive) → emit the provided proper-cased word.
 *    - Else if it is **not the first token** and its lowercase form is contained in `lowercaseWords`
 *      → emit lowercase.
 *    - Else if `preserveCasing` → uppercase only the first character; keep the remainder as-is.
 *    - Else → lowercase the token and uppercase the first character.
 * 4. Join with `joinSeparator`.
 *
 * Edge cases:
 * - Non-string inputs return an empty string `''`.
 * - Empty tokens (e.g., consecutive delimiters) produce empty entries that rejoin naturally.
 *
 * @function titleCase
 * @memberof module:synticore-string
 * @param {string} input
 *   The input string to transform.
 * @param {TitleCaseOptions} [options]
 *   Optional configuration controlling tokenization and casing rules.
 * @returns {string}
 *   The Title Cased output.
 * @since 1.0.0
 *
 * @example <caption>Classic book-style title casing</caption>
 * titleCase('war and peace', { lowercaseWords: ['and'] }); // → "War and Peace"
 *
 * @example <caption>Underscore and dash tokenization with custom join</caption>
 * titleCase('error_not_found--try_again', {
 *   delimiter: /[_-]+/,
 *   joinSeparator: ' '
 * }); // → "Error Not Found Try Again"
 *
 * @example <caption>Preserve inner casing vs. normalize</caption>
 * titleCase('fix iPhone and eBay parser', {
 *   propercaseWords: ['iPhone', 'eBay'],
 *   preserveCasing: false
 * }); // → "Fix iPhone And Ebay Parser"
 * titleCase('fix iPhone and eBay parser', {
 *   propercaseWords: ['iPhone', 'eBay'],
 *   preserveCasing: true
 * }); // → "Fix iPhone And eBay Parser"
 */
function titleCase(input, options) {
  /** Defaults validated against the typedef shape (no missing fields). */
  const DEFAULTS = /** @satisfies {Required<TitleCaseOptions>} */ ({
    acronyms: [],
    lowercaseWords: [],
    propercaseWords: [],
    splitCamelCase: false,
    delimiter: /[\s_\-]+/,
    joinSeparator: ' ',
    preserveCasing: false
  });

  // Merge first so TS sees a complete, known shape.
  const merged = { ...DEFAULTS, ...(options ?? {}) };

  // Destructure AFTER merge; avoids TS “property does not exist” on optional fields.
  const {
    acronyms,
    lowercaseWords,
    propercaseWords,
    splitCamelCase,
    delimiter,
    joinSeparator,
    preserveCasing
  } = merged;

  // --- implementation ---
  if (typeof input !== 'string' || input.length === 0) return '';

  // -----------------------------------------------------------------------------
  // Verbose logging controls (bounded to avoid log spam)
  // -----------------------------------------------------------------------------
  // const LOG_MAX_PREVIEW_CHARS = 200;
  // const LOG_MAX_TOKENS_LIST   = 50;
  // const LOG_SAMPLE_DECISIONS  = 50;

  // /** @param {string} s */
  // const _preview = (s) =>
  //   typeof s === 'string' && s.length > LOG_MAX_PREVIEW_CHARS
  //     ? `${s.slice(0, LOG_MAX_PREVIEW_CHARS)}…`
  //     : s;

  // const hrStart = (typeof process !== 'undefined' && process.hrtime?.bigint)
  //   ? process.hrtime.bigint()
  //   : null;

  // Begin: capture inputs
  // log.begin?.(LOG_TAG_STATICUS_STRING, 'titleCase running:', {
  //   inputType: typeof input,
  //   inputLength: typeof input === 'string' ? input.length : null,
  //   inputPreview: _preview(typeof input === 'string' ? input : ''),
  //   options: {
  //     acronymsCount: acronyms.length,
  //     lowercaseWordsCount: lowercaseWords.length,
  //     propercaseWordsCount: propercaseWords.length,
  //     splitCamelCase,
  //     delimiter: String(delimiter),
  //     joinSeparator,
  //     preserveCasing
  //   }
  // });

  // Guard non-string
  if (typeof input !== 'string') {
    // log.warn?.(LOG_TAG_STATICUS_STRING, 'Non-string input → returning empty string.');
    return '';
  }

  // Build lookup maps
  const ACRONYM_MAP = acronyms.reduce((map, word) => {
    map[word.toLowerCase()] = word;
    return map;
  }, /** @type {Record<string,string>} */ ({}));

  const PROPERCASE_MAP = propercaseWords.reduce((map, word) => {
    map[word.toLowerCase()] = word;
    return map;
  }, /** @type {Record<string,string>} */ ({}));

  // log.debug?.(LOG_TAG_STATICUS_STRING, 'Built lookup maps:', {
  //   acronymKeys: Object.keys(ACRONYM_MAP).length,
  //   propercaseKeys: Object.keys(PROPERCASE_MAP).length
  // });

  // Optional camelCase splitter (e.g. "myAPICall" => "my API Call")
  let working = input;
  if (splitCamelCase) {
    const before = working;
    working = working.replace(/([a-z])([A-Z])/g, '$1 $2');
    if (before !== working) {
      // log.debug?.(LOG_TAG_STATICUS_STRING, 'Applied camelCase split.', {
      //   beforePreview: _preview(before),
      //   afterPreview: _preview(working)
      // });
    }
  }

  // Tokenize
  const splitter = (typeof delimiter === 'string') ? new RegExp(delimiter) : delimiter;
  const words = working.split(splitter);

  if (words.length === 0) {
    // log.info?.(LOG_TAG_STATICUS_STRING, 'No tokens after split; returning empty string.');
    return '';
  }

  // log.debug?.(LOG_TAG_STATICUS_STRING, 'Tokenized input:', {
  //   tokensCount: words.length,
  //   tokensSample: words.slice(0, LOG_MAX_TOKENS_LIST)
  // });

  // Transform with decision tracing (sampled)
  // /** @type {Array<{i:number,src:string,lower:string,out:string,reason:string}>} */
  // const decisions = [];

  const out = words.map((word, index) => {
    const lower = word.toLowerCase();
    let reason = '';
    let emitted = word;

    if (ACRONYM_MAP[lower]) {
      emitted = ACRONYM_MAP[lower];
      reason = 'acronym';
    } else if (PROPERCASE_MAP[lower]) {
      emitted = PROPERCASE_MAP[lower];
      reason = 'propercase';
    } else if (index > 0 && lowercaseWords.includes(lower)) {
      emitted = lower;
      reason = 'connector-lowercase';
    } else if (preserveCasing) {
      emitted = word.charAt(0).toUpperCase() + word.slice(1);
      reason = 'preserveCasing';
    } else {
      emitted = lower.charAt(0).toUpperCase() + lower.slice(1);
      reason = 'normalize';
    }

    // if (decisions.length < LOG_SAMPLE_DECISIONS) {
    //   decisions.push({ i: index, src: word, lower, out: emitted, reason });
    // }

    return emitted;
  }).join(joinSeparator);

  // End + timing
  // const ms =
  //   hrStart && typeof process !== 'undefined' && process.hrtime?.bigint
  //     ? Number(process.hrtime.bigint() - hrStart) / 1e6
  //     : null;

  // log.debug?.(LOG_TAG_STATICUS_STRING, 'Decision sample:', decisions);
  // log.end?.(LOG_TAG_STATICUS_STRING, 'titleCase complete.', {
  //   outputLength: out.length,
  //   outputPreview: _preview(out),
  //   joinSeparator,
  //   tokensOutEstimated: out.split(joinSeparator).length,
  //   durationMs: ms
  // });

  return out;
}

