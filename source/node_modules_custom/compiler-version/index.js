'use strict';

/**
 * Small semantic-version comparison helpers shared by compiler workflows.
 * This module compares dotted numeric versions (e.g. "1.2.3").
 */

const VERSION_COMPARE = Object.freeze({
  OLD: -1,
  EQUAL: 0,
  NEW: 1
});

function getVersionCompareObject(value) {
  const key = Object.keys(VERSION_COMPARE).find((k) => VERSION_COMPARE[k] === value);
  return { key, value };
}

function compareVersions(v1, v2) {
  const v1Parts = String(v1 || '').split('.').map(Number);
  const v2Parts = String(v2 || '').split('.').map(Number);

  for (let i = 0; i < Math.max(v1Parts.length, v2Parts.length); i += 1) {
    const v1Part = v1Parts[i] || 0;
    const v2Part = v2Parts[i] || 0;

    if (v1Part > v2Part) return VERSION_COMPARE.NEW;
    if (v1Part < v2Part) return VERSION_COMPARE.OLD;
  }

  return VERSION_COMPARE.EQUAL;
}

function semverLt(a, b) { return compareVersions(a, b) === VERSION_COMPARE.OLD; }
function semverLe(a, b) { return compareVersions(a, b) !== VERSION_COMPARE.NEW; }
function semverEq(a, b) { return compareVersions(a, b) === VERSION_COMPARE.EQUAL; }

module.exports = {
  VERSION_COMPARE,
  getVersionCompareObject,
  compareVersions,
  semverLt,
  semverLe,
  semverEq
};
