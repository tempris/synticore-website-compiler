/**
 * Internal-only async demo used by the CLI entrypoint.
 *
 * Behavior:
 * - Prints two immediate lines and one delayed line (~3s), then resolves.
 * - Exists solely to demonstrate async logging when running this file directly.
 *
 * Concurrency & timers:
 * - Uses a single `setTimeout`. No cancellation; the demo resolves after timeout.
 *
 * Side effects:
 * - Writes to stdout. No file or network I/O.
 *
 * @function testPerformTask
 * @private
 * @internal
 * @returns {Promise<void>}
 *   A promise that fulfills after the delayed log statement has been printed.
 * @since 1.0.0
 *
 * @example <caption>CLI-only usage</caption>
 * // Invoked by the guard below when: node index.js
 */
function testPerformTask() {
  return new Promise((resolve) => {
    console.log("A string logged in JS.");
    console.log("Another string logged in JS.");
    setTimeout(() => {
      console.log("A delayed string logged in JS.");
      resolve();
    }, 3000);
  });
}

