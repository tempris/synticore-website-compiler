/**
 * Sum an array of numbers and optionally multiply the result.
 *
 * Validation contract:
 * - `numbers` must be an array of **finite** numbers (no `NaN`, `Infinity`, or strings).
 * - `multiplier` must be a **finite** number. No coercion is performed.
 *
 * Concurrency & async:
 * - Declared `async` for ergonomic composition in async pipelines.
 * - No timers, streams, or parallelism; resolves on the next tick.
 *
 * Side effects:
 * - None. Pure deterministic function given valid inputs.
 *
 * @async
 * @function calculateSum
 * @memberof module:cure-bridge-py
 * @param {number[]} [numbers=[1, 2, 3]]
 *   Array of values to sum.
 * @default [1, 2, 3]
 * @param {number} [multiplier=1]
 *   Factor by which to multiply the final sum.
 * @default 1
 * @returns {Promise<number>}
 *   Resolves to `sum(numbers) * multiplier`.
 * @throws {TypeError}
 *   Thrown if `numbers` is not an array of **finite** numbers, or `multiplier` is not a **finite** number.
 * @since 1.0.0
 *
 * @example <caption>Basic</caption>
 * const total = await calculateSum([4, 5, 6], 2); // -> 30
 *
 * @example <caption>Defaults</caption>
 * const total = await calculateSum(); // -> 6
 */
async function calculateSum(numbers = [1, 2, 3], multiplier = 1) {
  const isFiniteNumber = (v) => typeof v === 'number' && Number.isFinite(v);

  if (!Array.isArray(numbers) || numbers.some((n) => !isFiniteNumber(n))) {
    throw new TypeError("Invalid input: 'numbers' must be an array of finite numbers.");
  }
  if (!isFiniteNumber(multiplier)) {
    throw new TypeError("Invalid input: 'multiplier' must be a finite number.");
  }

  const sum = numbers.reduce((acc, num) => acc + num, 0);
  return sum * multiplier;
}

/**
 * Wrap a string with ANSI SGR codes for a basic 8-color **foreground** style.
 *
 * Safety & portability:
 * - Unknown `color` values are treated as **reset** to avoid leaving the terminal colored.
 * - Always appends a final reset sequence.
 *
 * I/O & side effects:
 * - Pure string transformation; does not write to stdout by itself.
 *
 * @function getAnsiString
 * @memberof module:cure-bridge-py
 * @param {string} text
 *   The text to colorize.
 * @param {AnsiColorName|string} color
 *   Foreground color name (case-insensitive). Non-matching strings are treated as reset.
 * @returns {string}
 *   The ANSI-wrapped string ready for `console.log`.
 * @since 1.0.0
 *
 * @example <caption>Success message</caption>
 * console.log(getAnsiString('Success!', 'green'));
 *
 * @example <caption>Unknown color → reset</caption>
 * console.log(getAnsiString('Plain', 'unknown'));
 */
function getAnsiString(text, color) {
  const ansiColors = {
    black: "\x1b[30m",
    red: "\x1b[31m",
    green: "\x1b[32m",
    yellow: "\x1b[33m",
    blue: "\x1b[34m",
    magenta: "\x1b[35m",
    cyan: "\x1b[36m",
    white: "\x1b[37m",
    reset: "\x1b[0m" // Resets to default color
  };

  // Normalize color safely and map; fall back to reset.
  const key = String(color ?? '').toLowerCase();
  const colorCode = ansiColors[key] || ansiColors.reset;

  return `${colorCode}${text}${ansiColors.reset}`;
}

