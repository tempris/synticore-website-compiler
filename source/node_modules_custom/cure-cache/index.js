/**
 * Default asynchronous file signature function.
 *
 * Implementation:
 * - sha256 over file contents.
 * - Normalizes text EOL (CRLF/CR/LF -> LF) in auto text mode for cross-OS stability.
 * - Keeps binary files byte-hashed.
 *
 * Contract:
 * - Must be deterministic for the same file bytes.
 *
 * @param {string} filePath - Absolute path to the file.
 * @returns {Promise<string>} Hex digest.
 * @since 1.0.0
 * @example
 * const sig = await _defaultComputeSignatureAsync('/abs/file.bin');
 */
function _defaultComputeSignatureAsync(filePath) {
  return NODE_CURE_FS.computeFileChecksumAsync(filePath, {
    algorithm: 'sha256',
    normalizeEol: true,
    textMode: 'auto'
  });
}

/**
 * Safely compute optional metadata for a file using a provider.
 *
 * Behavior:
 * - If `fn` is falsy, resolves to `undefined`.
 * - Catches exceptions and logs, returning `undefined` on error.
 *
 * @param {((p:string)=>Promise<*>|*)|undefined} fn - Metadata provider or undefined.
 * @param {string} filePath - Absolute path to the file.
 * @returns {Promise<*>} The metadata value or `undefined`.
 * @since 1.0.0
 * @example
 * const data = await _computeDataSafe(async p => ({ size: (await fs.promises.stat(p)).size }), abs);
 */
function _computeDataSafe(fn, filePath) {
  if (!fn) return Promise.resolve(undefined);
  return Promise.resolve()
    .then(() => fn(filePath))
    .catch(e => {
      log.error(LOG_TAG_CACHE, 'computeData failed:', { filePath, err: String(e?.message || e) });
      return undefined;
    });
}

/**
 * Canonicalize cache keys so path variants map to one entry.
 *
 * Rules:
 * - Convert "\" to "/".
 * - Remove leading "./".
 * - Normalize "." and ".." segments (POSIX semantics for cache keys).
 * - Preserve original path casing (no forced lowercase conversion).
 *
 * @private
 * @param {string} key
 * @returns {string}
 */
function _canonicalizeCacheKey(key) {
  let k = String(key || '').replace(/\\/g, '/');
  k = k.replace(/^\.\//, '');
  k = NODE_PATH.posix.normalize(k);
  if (k === '.') k = '';
  return k;
}

/**
 * Normalize loaded cache entries into canonical key shape.
 *
 * @private
 * @param {Record<string, any>} entries
 * @returns {{ changed: boolean, out: Record<string, any> }}
 */
function _normalizeLoadedEntries(entries) {
  if (!entries || typeof entries !== 'object') {
    return { changed: false, out: {} };
  }

  /** @type {Record<string, any>} */
  const out = {};
  let changed = false;

  for (const [namespace, bucket] of Object.entries(entries)) {
    if (!bucket || typeof bucket !== 'object' || Array.isArray(bucket)) {
      out[namespace] = bucket;
      continue;
    }

    /** @type {Record<string, any>} */
    const nextBucket = {};

    for (const [rawKey, entry] of Object.entries(bucket)) {
      const key = _canonicalizeCacheKey(rawKey);
      if (key !== rawKey) changed = true;

      // Prefer entry with metadata when colliding canonical keys.
      const prior = nextBucket[key];
      if (!prior) {
        nextBucket[key] = entry;
        continue;
      }

      const priorHasData = prior && typeof prior === 'object' && Object.prototype.hasOwnProperty.call(prior, 'data');
      const nextHasData = entry && typeof entry === 'object' && Object.prototype.hasOwnProperty.call(entry, 'data');
      if (!priorHasData && nextHasData) {
        nextBucket[key] = entry;
      }

      changed = true;
    }

    out[namespace] = nextBucket;
  }

  return { changed, out };
}

/**
 * Build a lightweight file stat fingerprint.
 *
 * @private
 * @param {string} filePath
 * @returns {{ exists: boolean, size: number|null, mtimeMs: number|null }}
 */
function _fileStatFingerprintSync(filePath) {
  try {
    const st = NODE_FS.statSync(filePath);
    return { exists: true, size: Number(st.size || 0), mtimeMs: Number(st.mtimeMs || 0) };
  } catch {
    return { exists: false, size: null, mtimeMs: null };
  }
}

/**
 * Deep-clone JSON-like data.
 *
 * @private
 * @param {*} value
 * @returns {*}
 */
function _cloneJsonLike(value) {
  if (typeof value === 'undefined') return undefined;
  return JSON.parse(JSON.stringify(value));
}

/**
 * Compare two JSON-like values, including undefined.
 *
 * @private
 * @param {*} a
 * @param {*} b
 * @returns {boolean}
 */
function _jsonLikeEqual(a, b) {
  if (typeof a === 'undefined' && typeof b === 'undefined') return true;
  if (typeof a === 'undefined' || typeof b === 'undefined') return false;
  return NODE_CURE_JSON.equal(a, b);
}

