/**
 * @typedef {Object} CacheOptions
 * @property {string} file
 * @property {string} root
 * @property {number} [fileIndent=2]
 * @property {'local_wins'|'external_wins'|'fail'} [conflictPolicy='local_wins']
 * @property {CacheProviders} [providers]
 * @property {CacheGates} [gates]
 */

/**
 * Generic, pluggable file cache with optional metadata storage.
 *
 * Responsibilities:
 * - Compute and persist a deterministic signature per file (default: sha256).
 * - Decide if a file should be (re)processed based on signature changes.
 * - Optionally compute and store derived metadata (e.g., image dimensions).
 * - Provide a Gulp-compatible filtering transform.
 *
 * Gates:
 * - Behavior is controlled at runtime via injected {@link CacheGates} functions
 *   (enable/filter/store/sort), so toggles reflect the latest config/CLI flags.
 *
 * Persistence format (on disk):
 * {
 *   "<namespace>": {
 *     "<relative/path.ext>": { "signature": "<hex>", "data": { ... } }
 *   }
 * }
 *
 * @class Cache
 * @since 1.0.0
 * @param {CacheOptions} opts - Construction options.
 * @throws {Error} When required fields are missing (file/root).
 * @example
 * const cache = new Cache({
 *   file: PATH_CACHE_JSON,
 *   root: PATH_INPUT_ROOT,
 *   gates: {
 *     enable: () => true,
 *     filter: () => true,
 *     store:  () => true,
 *     sort:   () => false
 *   },
 *   providers: {
 *     // keep default signature; compute extra metadata:
 *     computeData: async (absPath) => ({ size: (await fs.promises.stat(absPath)).size })
 *   }
 * });
 */
class Cache {
  /**
   * @param {CacheOptions} opts
   */
  constructor(opts) {
    if (!opts || !opts.file || !opts.root) {
      throw new Error('Cache: {file, root} are required.');
    }

    /** @private */ this.pathFile   = opts.file;
    /** @private */ this.pathRoot   = opts.root;
    /** @private */ this.fileIndent = Number(opts.fileIndent ?? 2);

    // /** @private */
    this.providers = {
      computeSignature: _defaultComputeSignatureAsync,
      computeData: undefined,
      ...(opts.providers || {})
    };

    /** @private */
    this.gates = {
      enable: () => true,
      filter: () => true,
      store : () => true,
      ...(opts?.gates || {})
    };

    /**
     * Backing entries map (public by design).
     * Shape: { [namespace]: { [relativeKey]: { signature:string, data?:any } } }
     * @since 1.0.0
     */
    this.entries = {};
    /** @private */ this._dirty = false;
    /** @private */ this._baseEntries = {};
    /** @private */ this._baseChecksum = null;
    /** @private */ this._cacheFingerprint = { exists: false, size: null, mtimeMs: null, checksum: null };
    /** @private */ this._lastFreshCheckAt = 0;
    /** @private */ this._freshCheckIntervalMs = 250;
    /** @private */ this._conflictPolicy = (typeof opts?.conflictPolicy === 'string' && opts.conflictPolicy.trim())
      ? String(opts.conflictPolicy).trim()
      : 'local_wins';
    if (!['local_wins', 'external_wins', 'fail'].includes(this._conflictPolicy)) {
      this._conflictPolicy = 'local_wins';
    }

    // ensure parent dir + load if exists
    const parent = NODE_PATH.dirname(this.pathFile);
    if (!NODE_FS.existsSync(parent)) NODE_FS.mkdirSync(parent, { recursive: true });
    if (NODE_FS.existsSync(this.pathFile)) {
      try {
        const txt = NODE_FS.readFileSync(this.pathFile, 'utf8');
        this.entries = txt ? JSON.parse(txt) : {};
        const normalized = _normalizeLoadedEntries(this.entries);
        this.entries = normalized.out;
        if (normalized.changed) this.save();
      } catch (e) {
        log.error(LOG_TAG_CACHE, 'Failed to parse cache file. Starting empty.', {
          file: this.pathFile, err: String(e?.message || e)
        });
        this.entries = {};
      }
    }

    this._rememberCacheFingerprintSync();
    this._syncBaselineToCurrent();

    log.info(LOG_TAG_CACHE, 'Cache settings:', {
      enable: this.isEnabled(),
      filter: this.isFilterEnabled(),
      store: this.isStoreEnabled(),
      conflictPolicy: this._conflictPolicy,
      file: this.pathFile,
      root: this.pathRoot
    });
  }

  // ---------- Gates ----------
  /**
   * Check if the cache system is globally enabled.
   *
   * Source of truth: {@link Cache.gates}.enable()
   *
   * @returns {boolean} True if enabled.
   * @since 1.0.0
   * @example
   * if (!cache.isEnabled()) { /* bypass caching entirely *\/ }
   */
  isEnabled()       { try { return !!this.gates.enable(); } catch { return true; } }

  /**
   * Check if change-filtering is enabled.
   *
   * Semantics:
   * - Returns true only if {@link Cache.isEnabled} and gates.filter() are both truthy.
   *
   * @returns {boolean}
   * @since 1.0.0
   * @example
   * const should = cache.isFilterEnabled() && cache.shouldProcessSync(ns, file);
   */
  isFilterEnabled() { try { return this.isEnabled() && !!this.gates.filter(); } catch { return this.isEnabled(); } }

  /**
   * Check if cache writes are allowed.
   *
   * Semantics:
   * - Returns true only if {@link Cache.isEnabled} and gates.store() are both truthy.
   *
   * @returns {boolean}
   * @since 1.0.0
   * @example
   * if (cache.isStoreEnabled()) await cache.store(ns, file, true);
   */
  isStoreEnabled()  { try { return this.isEnabled() && !!this.gates.store();  } catch { return this.isEnabled(); } }

  // ---------- Keys / Namespaces ----------

  /**
   * Compute the project-relative cache key for a file path.
   *
   * Prefers `@custom/cure-path.relative` if available; falls back to `path.relative`.
   *
   * @private
   * @param {string} filePath - Absolute path to a file.
   * @returns {string} Relative key under the configured {@link CacheOptions.root}.
   * @since 1.0.0
   */
  _relativeKey(filePath) {
    let NODE_CURE_PATH = null;
    try { NODE_CURE_PATH = require('@custom/cure-path'); } catch {}
    const rel = (NODE_CURE_PATH ? NODE_CURE_PATH.relative : NODE_PATH.relative);
    return _canonicalizeCacheKey(rel(this.pathRoot, filePath));
  }

  /**
   * Public helper returning the project-relative key for a file.
   *
   * Alias for the internal {@link Cache._relativeKey}.
   *
   * @param {string} filePath - Absolute path to a file.
   * @returns {string} Cache key (relative path).
   * @since 1.0.0
   * @example
   * const key = cache.getKey('/abs/project/input/img/logo.png');
   * // e.g. "img/logo.png"
   */
  getKey(filePath) { return this._relativeKey(filePath); }

  /**
   * Compute checksum of the cache file itself.
   *
   * @private
   * @returns {string|null}
   */
  _computeCacheFileChecksumSync() {
    if (!NODE_FS.existsSync(this.pathFile)) return null;
    return NODE_CURE_FS.computeFileChecksumSync(this.pathFile, {
      algorithm: 'sha256',
      normalizeEol: true,
      textMode: 'text'
    });
  }

  /**
   * Capture current cache-file fingerprint (+ checksum) as the remembered baseline.
   *
   * @private
   */
  _rememberCacheFingerprintSync() {
    const fp = _fileStatFingerprintSync(this.pathFile);
    const checksum = fp.exists ? this._computeCacheFileChecksumSync() : null;
    this._cacheFingerprint = { ...fp, checksum };
  }

  /**
   * Update merge baseline from current in-memory entries.
   *
   * @private
   */
  _syncBaselineToCurrent() {
    this._baseEntries = _cloneJsonLike(this.entries) || {};
    this._baseChecksum = this._cacheFingerprint?.checksum ?? null;
  }

  /**
   * Read and normalize current cache entries from disk.
   *
   * @private
   * @returns {Record<string, any>}
   */
  _readDiskEntriesSync() {
    let nextEntries = {};
    if (!NODE_FS.existsSync(this.pathFile)) return nextEntries;
    try {
      const txt = NODE_FS.readFileSync(this.pathFile, 'utf8');
      nextEntries = txt ? JSON.parse(txt) : {};
    } catch (e) {
      log.error(LOG_TAG_CACHE, 'Failed to read cache from disk; using empty baseline.', {
        file: this.pathFile,
        err: String(e?.message || e)
      });
      return {};
    }
    return _normalizeLoadedEntries(nextEntries).out;
  }

  /**
   * Compute local key-level mutations between baseline and current in-memory state.
   *
   * @private
   * @returns {Array<{ namespace: string, key: string, type: 'upsert'|'delete', baseValue: any, localValue: any }>}
   */
  _collectLocalOpsSinceBaseline() {
    const base = (this._baseEntries && typeof this._baseEntries === 'object') ? this._baseEntries : {};
    const local = (this.entries && typeof this.entries === 'object') ? this.entries : {};
    /** @type {Array<{ namespace: string, key: string, type: 'upsert'|'delete', baseValue: any, localValue: any }>} */
    const out = [];

    const namespaces = new Set(Object.keys(base).concat(Object.keys(local)));
    for (const namespace of Array.from(namespaces)) {
      const baseNs = (base[namespace] && typeof base[namespace] === 'object') ? base[namespace] : {};
      const localNs = (local[namespace] && typeof local[namespace] === 'object') ? local[namespace] : {};
      const keys = new Set(Object.keys(baseNs).concat(Object.keys(localNs)));

      for (const key of Array.from(keys)) {
        const baseHas = Object.prototype.hasOwnProperty.call(baseNs, key);
        const localHas = Object.prototype.hasOwnProperty.call(localNs, key);
        if (baseHas && !localHas) {
          out.push({ namespace, key, type: 'delete', baseValue: baseNs[key], localValue: undefined });
          continue;
        }
        if (localHas && (!baseHas || !_jsonLikeEqual(localNs[key], baseNs[key]))) {
          out.push({ namespace, key, type: 'upsert', baseValue: baseNs[key], localValue: localNs[key] });
        }
      }
    }

    return out;
  }

  /**
   * Merge local mutations onto latest disk entries according to conflict policy.
   *
   * @private
   * @param {Record<string, any>} diskEntries
   * @returns {{ entries: Record<string, any>, conflicts: number, applied: number, skipped: number }}
   */
  _mergeLocalOpsOntoDisk(diskEntries) {
    const merged = _cloneJsonLike(diskEntries) || {};
    const ops = this._collectLocalOpsSinceBaseline();
    let conflicts = 0;
    let applied = 0;
    let skipped = 0;

    for (const op of ops) {
      const nsObj = (merged[op.namespace] && typeof merged[op.namespace] === 'object')
        ? merged[op.namespace]
        : null;
      const diskHas = !!nsObj && Object.prototype.hasOwnProperty.call(nsObj, op.key);
      const diskValue = diskHas ? nsObj[op.key] : undefined;

      const externalChanged = !_jsonLikeEqual(diskValue, op.baseValue);
      if (externalChanged) {
        conflicts++;
        if (this._conflictPolicy === 'fail') {
          throw new Error(`Cache merge conflict for ${op.namespace}/${op.key}`);
        }
        if (this._conflictPolicy === 'external_wins') {
          skipped++;
          continue;
        }
      }

      if (op.type === 'delete') {
        if (nsObj && Object.prototype.hasOwnProperty.call(nsObj, op.key)) {
          delete nsObj[op.key];
          if (Object.keys(nsObj).length === 0) delete merged[op.namespace];
        }
        applied++;
        continue;
      }

      if (!merged[op.namespace] || typeof merged[op.namespace] !== 'object') {
        merged[op.namespace] = {};
      }
      merged[op.namespace][op.key] = _cloneJsonLike(op.localValue);
      applied++;
    }

    return { entries: merged, conflicts, applied, skipped };
  }

  /**
   * Reload entries from disk and canonicalize keys.
   *
   * @private
   * @param {string} reason
   */
  _reloadFromDiskSync(reason = 'external-change') {
    const nextEntries = this._readDiskEntriesSync();
    this.entries = nextEntries;
    this._dirty = false;
    this._rememberCacheFingerprintSync();
    this._syncBaselineToCurrent();

    log.debug(LOG_TAG_CACHE, 'Reloaded cache from disk.', { reason, file: this.pathFile });
  }

  /**
   * Ensure in-memory cache reflects current cache file when external edits are detected.
   *
   * @private
   * @param {string} reason
   * @param {boolean} [force=false]
   */
  _ensureCacheFreshSync(reason = 'operation', force = false) {
    const now = Date.now();
    if (!force && now - this._lastFreshCheckAt < this._freshCheckIntervalMs) return;
    this._lastFreshCheckAt = now;

    const current = _fileStatFingerprintSync(this.pathFile);
    const prior = this._cacheFingerprint || { exists: false, size: null, mtimeMs: null, checksum: null };

    const statChanged =
      current.exists !== prior.exists ||
      current.size !== prior.size ||
      current.mtimeMs !== prior.mtimeMs;
    if (!statChanged) return;

    const currentChecksum = current.exists ? this._computeCacheFileChecksumSync() : null;
    const checksumChanged = currentChecksum !== prior.checksum;
    if (!checksumChanged) {
      this._cacheFingerprint = { ...current, checksum: currentChecksum };
      return;
    }

    if (this._dirty) {
      log.warn(LOG_TAG_CACHE, 'Cache file changed on disk while in-memory cache has unsaved changes; deferring auto-reload.', {
        file: this.pathFile,
        reason
      });
      return;
    }

    this._reloadFromDiskSync(reason);
  }

  /**
   * Get (and optionally create) the namespace bucket.
   *
   * @private
   * @param {string|null} namespace - Namespace name; when falsy, returns the root entries map.
   * @param {boolean} [create=false] - If true, creates the namespace if missing.
   * @returns {Record<string, any>|null} The namespace map or null when not found.
   * @since 1.0.0
   */
  _ns(namespace, create = false) {
    if (!namespace) return this.entries;
    if (!this.entries[namespace] && create) this.entries[namespace] = {};
    return this.entries[namespace] || null;
  }

  // ---------- Persistence ----------

  /**
   * Persist the entire cache to disk.
   *
   * Behavior:
   * - Sorting of object keys is decided at call time via `gates.sort()`.
   * - JSON indentation uses {@link CacheOptions.fileIndent}.
   *
   * @since 1.0.0
   * @example
   * cache.save(); // writes current state to opts.file
   */
  save() {
    try {
      const diskFingerprint = _fileStatFingerprintSync(this.pathFile);
      const diskChecksum = diskFingerprint.exists ? this._computeCacheFileChecksumSync() : null;

      let entriesToPersist = this.entries;
      if (this._dirty && diskChecksum !== this._baseChecksum) {
        const diskEntries = this._readDiskEntriesSync();
        const merged = this._mergeLocalOpsOntoDisk(diskEntries);
        entriesToPersist = merged.entries;
        this.entries = entriesToPersist;
        log.warn(LOG_TAG_CACHE, 'External cache-file changes detected; reconciled before save.', {
          file: this.pathFile,
          conflictPolicy: this._conflictPolicy,
          conflicts: merged.conflicts,
          applied: merged.applied,
          skipped: merged.skipped
        });
      }

      NODE_CURE_JSON.save(this.pathFile, entriesToPersist, {
        sort: !!this.gates.sort(),
        spacing: this.fileIndent
      });
      this._dirty = false;
      this._rememberCacheFingerprintSync();
      this._syncBaselineToCurrent();
      log.debug(LOG_TAG_CACHE, 'Cache saved.');
    } catch (err) {
      log.error(LOG_TAG_CACHE, 'Failed to save cache:', err);
    }
  }

  /**
   * Clear cached entries.
   *
   * Modes:
   * - `namespace === 'all'`: wipe the entire cache map.
   * - `namespace` provided, `key == null`: remove the whole namespace bucket.
   * - `namespace` and `key` provided: delete only that key; remove empty namespace.
   *
   * @param {'all'|string} [namespace='all'] - What to clear.
   * @param {string|null} [key=null] - Optional key within namespace.
   * @param {boolean} [doSave=true] - If true, persist after clearing.
   * @since 1.0.0
   * @example
   * cache.clear('all');                // wipe everything
   * cache.clear('images');             // remove "images" namespace
   * cache.clear('images', 'a/b.png');  // remove one entry
   */
  clear(namespace = 'all', key = null, doSave = true) {
    this._ensureCacheFreshSync('clear');
    const LOG_TAG_CACHE_CLEAR = LOG_TAG_CACHE + ' [🗑️ Clear]';
    log.begin(LOG_TAG_CACHE_CLEAR, 'Clearing:', { namespace, key });

    if (namespace === 'all') {
      this.entries = {};
      this._dirty = true;
      log.info(LOG_TAG_CACHE_CLEAR, 'Entire cache cleared.');
    } else {
      const ns = this._ns(namespace, false);
      if (!ns) {
        log.info(LOG_TAG_CACHE_CLEAR, `Namespace '${namespace}' not found.`);
        log.end(LOG_TAG_CACHE_CLEAR, 'Done.');
        return;
      }

      if (key == null) {
        delete this.entries[namespace];
        this._dirty = true;
        log.info(LOG_TAG_CACHE_CLEAR, `Namespace '${namespace}' removed.`);
      } else {
        if (ns[key]) {
          delete ns[key];
          this._dirty = true;
          log.info(LOG_TAG_CACHE_CLEAR, `Key '${key}' removed from '${namespace}'.`);
          if (Object.keys(ns).length === 0) {
            delete this.entries[namespace];
            this._dirty = true;
            log.detail(LOG_TAG_CACHE_CLEAR, `Namespace '${namespace}' was empty and removed.`);
          }
        } else {
          log.info(LOG_TAG_CACHE_CLEAR, `Key '${key}' not found in '${namespace}'.`);
        }
      }
    }

    if (doSave) this.save();
    log.end(LOG_TAG_CACHE_CLEAR, 'Done.');
  }

  // ---------- Decision (sync + async) ----------

  /**
   * Synchronous change decision.
   *
   * Use when you need a quick decision without `await` (e.g., inside a Transform).
   * Computes sha256 from a synchronous file read for parity with legacy behavior.
   *
   * @param {string} namespace - Namespace bucket.
   * @param {string} filePath  - Absolute path to the file.
   * @param {{processed?:number, filtered?:number}} [counters] - Optional counters to mutate.
   * @returns {boolean} True if work should proceed (new/changed), false to skip.
   * @since 1.0.0
   * @example
   * if (!cache.shouldProcessSync('project', absPath, counters)) return; // skip unchanged
   */
  shouldProcessSync(namespace, filePath, counters = null) {
    this._ensureCacheFreshSync('shouldProcessSync');
    if (!this.isFilterEnabled()) {
      log.debug(LOG_TAG_CACHE, 'Processing (filter disabled):', { namespace, filePath });
      return true;
    }
    if (!NODE_FS.existsSync(filePath)) {
      log.debug(LOG_TAG_CACHE, 'Skip (not found):', { namespace, filePath });
      return false;
    }
    const key = this._relativeKey(filePath);
    const checksum = NODE_CURE_FS.computeFileChecksumSync(filePath, {
      algorithm: 'sha256',
      normalizeEol: true,
      textMode: 'auto'
    });

    const prior = this._ns(namespace, false)?.[key]?.signature ?? null;
    const should = !prior || prior !== checksum;

    if (counters && typeof counters.processed === 'number' && should) counters.processed++;
    if (counters && typeof counters.filtered  === 'number' && !should) counters.filtered++;

    log.debug(LOG_TAG_CACHE, 'Signature compare (sync):', { namespace, filePath, key, checksum, cached: prior, shouldProcess: should });
    return should;
  }

  /**
   * Asynchronous change decision.
   *
   * Uses the configured `providers.computeSignature` (streaming sha256 by default)
   * to compare current vs cached signature.
   *
   * @param {string} namespace - Namespace bucket.
   * @param {string} filePath  - Absolute path to the file.
   * @param {{processed?:number, filtered?:number}} [counters] - Optional counters to mutate.
   * @returns {Promise<boolean>} Resolves true to process, false to skip.
   * @since 1.0.0
   * @example
   * if (await cache.shouldProcessAsync('project', absPath)) {
   *   await doWork(absPath);
   * }
   */
  async shouldProcessAsync(namespace, filePath, counters = null) {
    this._ensureCacheFreshSync('shouldProcessAsync');
    if (!this.isFilterEnabled()) {
      log.debug(LOG_TAG_CACHE, 'Processing (filter disabled):', { namespace, filePath });
      return true;
    }
    if (!NODE_FS.existsSync(filePath)) {
      log.debug(LOG_TAG_CACHE, 'Skip (not found):', { namespace, filePath });
      return false;
    }
    const key = this._relativeKey(filePath);
    const curSig = await Promise.resolve(this.providers.computeSignature(filePath));
    const prior = this._ns(namespace, false)?.[key]?.signature ?? null;

    const should = !prior || prior !== curSig;

    if (counters && typeof counters.processed === 'number' && should) counters.processed++;
    if (counters && typeof counters.filtered  === 'number' && !should) counters.filtered++;

    log.debug(LOG_TAG_CACHE, 'Signature compare:', { namespace, filePath, key, signature: curSig, cached: prior, shouldProcess: should });
    return should;
  }

  // ---------- Store / Retrieve ----------

  /**
   * Compute and persist the signature (+ optional metadata) for a file.
   *
   * Behavior:
   * - If `providers.computeData` is defined, its result is stored in `entry.data`.
   * - If nothing changed vs prior entry, write is skipped.
   * - When `doSave` is true, the cache file is written immediately.
   *
   * @param {string} namespace - Namespace bucket.
   * @param {string} filePath  - Absolute path to the file.
   * @param {boolean} [doSave=true] - Persist after update.
   * @returns {Promise<{signature:string, data?:*}|null>} The up-to-date entry or null when storing is disabled.
   * @since 1.0.0
   * @example
   * const entry = await cache.store('images', absPath, true);
   * console.log(entry.signature, entry.data);
   */
  async store(namespace, filePath, doSave = true) {
    this._ensureCacheFreshSync('store');
    if (!this.isStoreEnabled()) return null;

    const key = this._relativeKey(filePath);
    const signature = await Promise.resolve(this.providers.computeSignature(filePath));
    const data = await _computeDataSafe(this.providers.computeData, filePath);

    const ns = this._ns(namespace, true);
    const prior = ns[key];

    const unchanged = prior && prior.signature === signature && NODE_CURE_JSON.equal(prior.data, data);
    if (unchanged) {
      log.debug(LOG_TAG_CACHE, 'Unchanged — skip write:', { namespace, key });
      return prior;
    }

    const entry = { signature };
    if (typeof data !== 'undefined') entry.data = data;

    ns[key] = entry;
    this._dirty = true;
    if (doSave) this.save();

    log.debug(LOG_TAG_CACHE, 'Stored entry:', { namespace, key, hasData: typeof data !== 'undefined' });
    return entry;
  }

  /**
   * Retrieve a valid entry when the on-disk file still matches the cached signature.
   *
   * If the file is missing or its signature has changed, resolves to `null`.
   *
   * @param {string} namespace - Namespace bucket.
   * @param {string} filePath  - Absolute path to the file.
   * @returns {Promise<null|{signature:string, data?:*}>}
   * @since 1.0.0
   * @example
   * const hit = await cache.getValidEntry('images', absPath);
   * if (hit) { use(hit.data); }
   */
  async getValidEntry(namespace, filePath) {
    this._ensureCacheFreshSync('getValidEntry');
    const key = this._relativeKey(filePath);
    const entry = this._ns(namespace, false)?.[key];
    if (!entry) return null;
    if (!NODE_FS.existsSync(filePath)) return null;

    const curSig = await Promise.resolve(this.providers.computeSignature(filePath));
    return (curSig === entry.signature) ? entry : null;
  }

  /**
   * Create a Gulp/Vinyl Transform that filters out unchanged files.
   *
   * Notes:
   * - Uses {@link Cache.shouldProcessSync} internally to avoid `await` in streams.
   * - Streams are not supported; buffered files only.
   *
   * @param {string} namespace - Namespace bucket used for decisions.
   * @param {boolean} [filter=true] - If false, pass-through without filtering.
   * @returns {import('stream').Transform} Object-mode Transform.
   * @since 1.0.0
   * @example
   * return gulp.src(GLOB)
   *   .pipe(cache.pipe('project', true))
   *   .pipe(expensive())
   *   .on('end', () => cache.save());
   */
  pipe(namespace, filter = true) {
    const NODE_STREAM = require('stream');
    const t = new NODE_STREAM.Transform({ objectMode: true });

    t._transform = async (file, _enc, next) => {
      try {
        if (file.isStream && file.isStream()) {
          return next(new Error('[cure-cache] Streams not supported.'));
        }
        if (file.isBuffer && file.isBuffer() && filter) {
          const should = this.shouldProcessSync(namespace, file.path); // use sync for zero-await pipelines
          if (!should) return next(null); // filtered out
        }
        next(null, file);
      } catch (e) {
        next(e);
      }
    };
    return t;
  }

  /**
   * Retrieve a cached entry without re-validating the on-disk file.
   *
   * Behavior:
   * - If {@link keyOrFilePath} is an absolute path, it is converted to a cache key
   *   via {@link Cache._relativeKey}.
   * - If {@link keyOrFilePath} looks like a cache key (relative path), it is used as-is.
   * - Does NOT touch the filesystem or recompute signatures. For a validated read,
   *   use {@link Cache.getValidEntry} instead.
   *
   * @param {string} namespace
   *   Namespace bucket (e.g., "images").
   * @param {string} keyOrFilePath
   *   Either a cache key like `"img/logo.png"` or an absolute file path under
   *   {@link CacheOptions.root}.
   * @returns {null|{signature:string, data?:*}}
   *   The cached entry if present, otherwise `null`.
   * @since 1.1.0
   * @example
   * const absPath = NODE_PATH.join(projectRoot, 'input/img/logo.png');
   *
   * // By relative key:
   * const byKey = cache.getEntry('images', 'img/logo.png');
   *
   * // By absolute path:
   * const byPath = cache.getEntry('images', absPath);
   */
  getEntry(namespace, keyOrFilePath) {
    this._ensureCacheFreshSync('getEntry');
    if (!namespace || !keyOrFilePath) return null;

    const ns = this._ns(namespace, false);
    if (!ns) return null;

    const key = NODE_PATH.isAbsolute(keyOrFilePath)
      ? this._relativeKey(keyOrFilePath)
      : _canonicalizeCacheKey(keyOrFilePath);

    return ns[key] || null;
  }

}

