/**
 * Guard text writes against silent UTF-8 decode corruption.
 *
 * @private
 * @function _assertTextSafeForWrite
 * @param {string} path
 * @param {string} text
 * @returns {void}
 */
function _assertTextSafeForWrite(path, text) {
  const s = String(text || '');
  if (s.includes('\uFFFD')) {
    throw new Error(`Refusing to write text with replacement characters (invalid UTF-8 decode): ${path}`);
  }
  if (_containsLikelyMojibake(s)) {
    throw new Error(`Refusing to write text with likely mojibake sequences: ${path}`);
  }
}

/**
 * Lazily resolve log facade from cure-log-hub.
 *
 * @private
 * @function _getLogFacadeSafe
 * @returns {Record<string, Function>|null}
 */
function _getLogFacadeSafe() {
  try {
    const NODE_CURE_LOG_HUB = require('../cure-log-hub');
    if (!NODE_CURE_LOG_HUB || typeof NODE_CURE_LOG_HUB.getRoot !== 'function') return null;
    if (!NODE_CURE_LOG_HUB.getRoot()) return null;
    if (typeof NODE_CURE_LOG_HUB.use !== 'function') return null;
    return NODE_CURE_LOG_HUB.use();
  } catch {
    return null;
  }
}

/**
 * Convert codepoint number to U+XXXX label.
 *
 * @private
 * @function _codePointLabel
 * @param {number} cp
 * @returns {string}
 */
function _codePointLabel(cp) {
  return `U+${cp.toString(16).toUpperCase().padStart(4, '0')}`;
}

/**
 * Return known human-readable names for selected high-risk codepoints.
 *
 * @private
 * @function _knownCodePointName
 * @param {number} cp
 * @returns {string}
 */
function _knownCodePointName(cp) {
  const map = {
    0x00A0: 'NO-BREAK SPACE',
    0x180E: 'MONGOLIAN VOWEL SEPARATOR',
    0x2007: 'FIGURE SPACE',
    0x200B: 'ZERO WIDTH SPACE',
    0x200C: 'ZERO WIDTH NON-JOINER',
    0x200D: 'ZERO WIDTH JOINER',
    0x200E: 'LEFT-TO-RIGHT MARK',
    0x200F: 'RIGHT-TO-LEFT MARK',
    0x2011: 'NON-BREAKING HYPHEN',
    0x202A: 'LEFT-TO-RIGHT EMBEDDING',
    0x202B: 'RIGHT-TO-LEFT EMBEDDING',
    0x202C: 'POP DIRECTIONAL FORMATTING',
    0x202D: 'LEFT-TO-RIGHT OVERRIDE',
    0x202E: 'RIGHT-TO-LEFT OVERRIDE',
    0x202F: 'NARROW NO-BREAK SPACE',
    0x2060: 'WORD JOINER',
    0x2066: 'LEFT-TO-RIGHT ISOLATE',
    0x2067: 'RIGHT-TO-LEFT ISOLATE',
    0x2068: 'FIRST STRONG ISOLATE',
    0x2069: 'POP DIRECTIONAL ISOLATE',
    0x061C: 'ARABIC LETTER MARK',
    0x034F: 'COMBINING GRAPHEME JOINER',
    0xFEFF: 'ZERO WIDTH NO-BREAK SPACE'
  };
  return map[cp] || 'UNNAMED';
}

/**
 * Return true when codepoint should always be removed from text outputs.
 *
 * Notes:
 * - Emoji and visible Unicode remain allowed.
 * - This targets invisible/control and bidi-manipulation codepoints only.
 *
 * @private
 * @function _isForbiddenCodePoint
 * @param {number} cp
 * @returns {boolean}
 */
function _isForbiddenCodePoint(cp) {
  if (cp === 0xFEFF) return true; // BOM / zero-width no-break
  if (cp === 0x034F || cp === 0x061C || cp === 0x180E) return true;

  // Zero-width and directional markers.
  if (
    cp === 0x200B || cp === 0x200C || cp === 0x200D ||
    cp === 0x200E || cp === 0x200F || cp === 0x2060
  ) {
    return true;
  }

  // Bidi controls.
  if ((cp >= 0x202A && cp <= 0x202E) || (cp >= 0x2066 && cp <= 0x2069)) {
    return true;
  }

  // C0/C1 controls except tab/newline/carriage return.
  if ((cp <= 0x1F || (cp >= 0x7F && cp <= 0x9F)) && cp !== 0x09 && cp !== 0x0A && cp !== 0x0D) {
    return true;
  }

  return false;
}

/**
 * Return true when codepoint is suspicious spacing/punctuation but still allowed.
 *
 * @private
 * @function _isWarnOnlyCodePoint
 * @param {number} cp
 * @returns {boolean}
 */
function _isWarnOnlyCodePoint(cp) {
  return cp === 0x00A0 || cp === 0x2007 || cp === 0x202F || cp === 0x2011;
}

/**
 * Record count by codepoint.
 *
 * @private
 * @function _bumpCodePointCount
 * @param {Map<number, number>} map
 * @param {number} cp
 * @returns {void}
 */
function _bumpCodePointCount(map, cp) {
  map.set(cp, (map.get(cp) || 0) + 1);
}

/**
 * Format codepoint count map for logs.
 *
 * @private
 * @function _formatCodePointCountMap
 * @param {Map<number, number>} map
 * @returns {Array<{ code_point: string, name: string, count: number }>}
 */
function _formatCodePointCountMap(map) {
  return Array.from(map.entries())
    .sort((a, b) => a[0] - b[0])
    .map(([cp, count]) => ({
      code_point: _codePointLabel(cp),
      name: _knownCodePointName(cp),
      count
    }));
}

/**
 * Sanitize potentially troublesome Unicode while keeping visible Unicode (incl. emoji).
 *
 * @private
 * @function _sanitizeUnicodeText
 * @param {string} text
 * @returns {{
 *   text: string,
 *   changed: boolean,
 *   normalized: boolean,
 *   removedCount: number,
 *   removedByCodePoint: Map<number, number>,
 *   warnedByCodePoint: Map<number, number>
 * }}
 */
function _sanitizeUnicodeText(text) {
  const input = String(text || '');
  const removedByCodePoint = new Map();
  const warnedByCodePoint = new Map();
  const outParts = [];

  for (const ch of input) {
    const cp = ch.codePointAt(0);
    if (_isForbiddenCodePoint(cp)) {
      _bumpCodePointCount(removedByCodePoint, cp);
      continue;
    }
    if (_isWarnOnlyCodePoint(cp)) {
      _bumpCodePointCount(warnedByCodePoint, cp);
    }
    outParts.push(ch);
  }

  const joined = outParts.join('');
  let normalized = joined;
  try {
    normalized = joined.normalize('NFC');
  } catch {
    normalized = joined;
  }

  const removedCount = Array.from(removedByCodePoint.values()).reduce((sum, n) => sum + n, 0);
  const changed = normalized !== input;

  return {
    text: normalized,
    changed,
    normalized: normalized !== joined,
    removedCount,
    removedByCodePoint,
    warnedByCodePoint
  };
}

/**
 * Validate no forbidden codepoints remain after sanitization.
 *
 * @private
 * @function _assertNoForbiddenUnicodeRemaining
 * @param {string} path
 * @param {string} text
 * @returns {void}
 */
function _assertNoForbiddenUnicodeRemaining(path, text) {
  const counts = new Map();
  for (const ch of String(text || '')) {
    const cp = ch.codePointAt(0);
    if (_isForbiddenCodePoint(cp)) {
      _bumpCodePointCount(counts, cp);
    }
  }
  if (counts.size <= 0) return;

  const details = _formatCodePointCountMap(counts)
    .map((x) => `${x.code_point} ${x.name} x${x.count}`)
    .join(', ');
  throw new Error(`Refusing to write text with forbidden invisible/control Unicode: ${path}. Found: ${details}`);
}

/**
 * Emit Unicode sanitize log entries.
 *
 * @private
 * @function _logUnicodeWriteResult
 * @param {string} path
 * @param {{
 *   changed: boolean,
 *   normalized: boolean,
 *   removedCount: number,
 *   removedByCodePoint: Map<number, number>,
 *   warnedByCodePoint: Map<number, number>
 * }} result
 * @returns {void}
 */
function _logUnicodeWriteResult(path, result) {
  const log = _getLogFacadeSafe();
  if (!log) return;

  const detail = {
    file_path: path,
    normalized_nfc: result.normalized,
    removed_total: result.removedCount
  };

  if (result.changed || result.removedCount > 0) {
    const removed = _formatCodePointCountMap(result.removedByCodePoint);
    if (removed.length > 0 && typeof log.debug === 'function') {
      log.debug(LOG_TAG_CURE_FS_UNICODE, 'Sanitized troublesome Unicode from write payload.', {
        ...detail,
        removed
      });
    }
  }

  const warned = _formatCodePointCountMap(result.warnedByCodePoint);
  if (warned.length > 0 && typeof log.warn === 'function') {
    log.warn(LOG_TAG_CURE_FS_UNICODE, 'Write payload contains non-standard spacing characters (allowed).', {
      file_path: path,
      warned
    });
  }
}

/**
 * Prepare output Buffer (binary/text) with EOL preservation for text.
 *
 * @private
 * @function _prepareWriteBuffer
 * @param {string} path
 * @param {string|Buffer|Uint8Array} data
 * @returns {{ outBuf: Buffer, isBinary: boolean }}
 */
function _prepareWriteBuffer(path, data) {
  const binBuf = _coerceBinaryToBuffer(data);
  if (binBuf) return { outBuf: binBuf, isBinary: true };

  let out = _stripLeadingUtf8Bom(String(data));
  const sanitizeResult = _sanitizeUnicodeText(out);
  out = sanitizeResult.text;

  _assertTextSafeForWrite(path, out);
  _assertNoForbiddenUnicodeRemaining(path, out);

  const targetEol = detectFileEolSync(path, '\n');
  out = _normalizeTextToTargetEol(out, targetEol);
  _logUnicodeWriteResult(path, sanitizeResult);

  const outBuf = Buffer.from(out, 'utf8');
  return { outBuf, isBinary: false };
}

/**
 * Decide if we can skip writing (byte-identical).
 *
 * @private
 * @function _shouldSkipWrite
 * @param {Buffer|null} existingBuf
 * @param {Buffer} outBuf
 * @returns {boolean}
 */
function _shouldSkipWrite(existingBuf, outBuf) {
  return !!(existingBuf && existingBuf.equals(outBuf));
}

/**
 * Heuristic binary detector for a Buffer sample.
 *
 * @private
 * @function _isProbablyBinaryBuffer
 * @param {Buffer} buffer
 * @param {number} [sampleBytes=8192]
 * @returns {boolean}
 */
function _isProbablyBinaryBuffer(buffer, sampleBytes = 8192) {
  const len = Math.min(buffer.length, sampleBytes);
  if (len <= 0) return false;

  let suspicious = 0;
  for (let i = 0; i < len; i++) {
    const byte = buffer[i];
    if (byte === 0x00) return true;
    if ((byte < 0x07 || (byte > 0x0D && byte < 0x20)) && byte !== 0x09 && byte !== 0x0A && byte !== 0x0D) {
      suspicious++;
    }
  }

  return (suspicious / len) > 0.3;
}

/**
 * Normalize a text buffer's line endings to LF for deterministic hashing.
 *
 * @private
 * @function _normalizeBufferEolToLf
 * @param {Buffer} buffer
 * @param {BufferEncoding} [encoding='utf8']
 * @returns {Buffer}
 */
function _normalizeBufferEolToLf(buffer, encoding = 'utf8') {
  const normalized = buffer
    .toString(encoding)
    .replace(/\r\n/g, '\n')
    .replace(/\r/g, '\n');
  return Buffer.from(normalized, encoding);
}

/**
 * Compute a checksum from a Buffer/Uint8Array with optional text EOL normalization.
 *
 * @function computeChecksumBufferSync
 * @memberof module:cure-fs
 * @param {Buffer|Uint8Array} data
 * @param {{
 *   algorithm?: string,
 *   normalizeEol?: boolean,
 *   textMode?: 'auto'|'text'|'binary',
 *   encoding?: BufferEncoding
 * }} [opt]
 * @returns {string}
 * @since 1.2.0
 */
function computeChecksumBufferSync(data, opt = {}) {
  const algorithm = String(opt.algorithm || 'sha256');
  const normalizeEol = opt.normalizeEol === true;
  const textMode = String(opt.textMode || 'auto');
  const encoding = /** @type {BufferEncoding} */ (opt.encoding || 'utf8');

  const input = Buffer.isBuffer(data) ? data : Buffer.from(data);

  let hashInput = input;
  if (normalizeEol) {
    const shouldNormalize =
      textMode === 'text' ||
      (textMode === 'auto' && !_isProbablyBinaryBuffer(input));
    if (shouldNormalize) {
      hashInput = _normalizeBufferEolToLf(input, encoding);
    }
  }

  return NODE_CRYPTO.createHash(algorithm).update(hashInput).digest('hex');
}

/**
 * Compute a checksum for a file synchronously with optional EOL normalization.
 *
 * @function computeFileChecksumSync
 * @memberof module:cure-fs
 * @param {string} path
 * @param {{
 *   algorithm?: string,
 *   normalizeEol?: boolean,
 *   textMode?: 'auto'|'text'|'binary',
 *   encoding?: BufferEncoding
 * }} [opt]
 * @returns {string}
 * @since 1.2.0
 */
function computeFileChecksumSync(path, opt = {}) {
  const data = NODE_FS.readFileSync(path);
  return computeChecksumBufferSync(data, opt);
}

/**
 * Compute a checksum for a file asynchronously with optional EOL normalization.
 *
 * @function computeFileChecksumAsync
 * @memberof module:cure-fs
 * @param {string} path
 * @param {{
 *   algorithm?: string,
 *   normalizeEol?: boolean,
 *   textMode?: 'auto'|'text'|'binary',
 *   encoding?: BufferEncoding
 * }} [opt]
 * @returns {Promise<string>}
 * @since 1.2.0
 */
async function computeFileChecksumAsync(path, opt = {}) {
  const data = await NODE_FS.promises.readFile(path);
  return computeChecksumBufferSync(data, opt);
}

/**
 * Write a UTF-8 file synchronously, ensuring the destination directory exists.
 *
 * @function writeFileSync
 * @memberof module:cure-fs
 * @param {string} path
 *   Output file path (created/overwritten).
 * @param {string|Buffer|Uint8Array} data
 *   Data to write; strings are encoded as UTF-8.
 * @returns {void}
 * @throws {Error}
 *   If writing fails.
 * @since 1.0.0
 *
 * @example
 * writeFileSync('dist/manifest.json', JSON.stringify({ version: '1.0.0' }, null, 2));
 */
function writeFileSync(path, data) {
  ensureDirectorySync(NODE_PATH.dirname(path));

  const existingBuf = _readFileIfExistsSync(path);

  const { outBuf, isBinary } = _prepareWriteBuffer(path, data);

  // If byte-identical, skip. (Applies to both binary + text.)
  if (_shouldSkipWrite(existingBuf, outBuf)) return;

  NODE_FS.writeFileSync(path, outBuf);
}

/**
 * Write a UTF-8 file asynchronously, ensuring the destination directory exists.
 *
 * Behavior:
 * - Creates parent directories as needed (mkdir -p).
 * - If the destination file already exists, preserves its line endings (CRLF/LF/CR).
 * - Performs a byte-level equality check and skips writing if output is identical.
 * - Treats Buffers/Uint8Arrays as binary and never EOL-normalizes them.
 *
 * @function writeFileAsync
 * @memberof module:cure-fs
 * @param {string} path
 *   Output file path (created/overwritten).
 * @param {string|Buffer|Uint8Array} data
 *   Data to write; strings are encoded as UTF-8.
 * @returns {Promise<void>}
 * @throws {Error}
 *   If writing fails.
 * @since 1.0.0
 *
 * @example
 * await writeFileAsync('dist/manifest.json', JSON.stringify({ version: '1.0.0' }, null, 2));
 */
async function writeFileAsync(path, data) {
  await ensureDirectoryAsync(NODE_PATH.dirname(path));

  const existingBuf = await _readFileIfExistsAsync(path);

  const { outBuf, isBinary } = _prepareWriteBuffer(path, data);

  // If byte-identical, skip. (Applies to both binary + text.)
  if (_shouldSkipWrite(existingBuf, outBuf)) return;

  await NODE_FS.promises.writeFile(path, outBuf);
}

