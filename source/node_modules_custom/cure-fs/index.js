/**
 * Read a file if it exists; otherwise return null.
 *
 * @private
 * @function _readFileIfExistsSync
 * @param {string} path
 * @returns {Buffer|null}
 */
function _readFileIfExistsSync(path) {
  return NODE_FS.existsSync(path) ? NODE_FS.readFileSync(path) : null;
}

/**
 * Read a file if it exists; otherwise return null.
 *
 * @private
 * @function _readFileIfExistsAsync
 * @param {string} path
 * @returns {Promise<Buffer|null>}
 */
async function _readFileIfExistsAsync(path) {
  try {
    return await NODE_FS.promises.readFile(path);
  } catch (e) {
    if (e && e.code === 'ENOENT') return null;
    throw e;
  }
}

/**
 * Coerce binary inputs to Buffer, or return null if input should be treated as text.
 *
 * @private
 * @function _coerceBinaryToBuffer
 * @param {string|Buffer|Uint8Array} data
 * @returns {Buffer|null}
 */
function _coerceBinaryToBuffer(data) {
  if (Buffer.isBuffer(data)) return data;
  if (data instanceof Uint8Array) return Buffer.from(data);
  return null;
}

/**
 * Normalize text newlines to a target EOL sequence.
 *
 * - Converts CRLF and CR to LF, then replaces LF with targetEol (if needed).
 * - Only does work if text appears to contain newlines.
 *
 * @private
 * @function _normalizeTextToTargetEol
 * @param {string} out
 * @param {string} targetEol
 * @returns {string}
 */
function _normalizeTextToTargetEol(out, targetEol) {
  if (!(out.includes('\n') || out.includes('\r'))) return out;

  // Normalize to LF first, then convert to target.
  let norm = out.replace(/\r\n/g, '\n').replace(/\r/g, '\n');
  if (targetEol !== '\n') norm = norm.replace(/\n/g, targetEol);
  return norm;
}

/**
 * Strip leading UTF-8 BOM marker (U+FEFF) from a text string.
 *
 * @private
 * @function _stripLeadingUtf8Bom
 * @param {string} text
 * @returns {string}
 */
function _stripLeadingUtf8Bom(text) {
  return String(text || '').replace(/^\uFEFF/, '');
}

