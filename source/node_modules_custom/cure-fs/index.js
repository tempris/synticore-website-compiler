/**
 * Return true when a pattern likely contains glob syntax.
 *
 * @private
 * @param {string} value
 * @returns {boolean}
 */
function _looksLikeGlob(value) {
  const s = String(value || '');
  return /[*?[\]{}()!+@]/.test(s);
}

/**
 * Normalize a delete input to a flat string array.
 *
 * @private
 * @param {string|string[]} pattern
 * @returns {string[]}
 */
function _toPatternArray(pattern) {
  if (Array.isArray(pattern)) return pattern.map((v) => String(v || '')).filter(Boolean);
  return [String(pattern || '')].filter(Boolean);
}

/**
 * Expand a single pattern into absolute candidate paths (sync).
 *
 * @private
 * @param {string} pattern
 * @returns {string[]}
 */
function _expandPatternSync(pattern) {
  const p = String(pattern || '');
  if (!p) return [];
  if (!_looksLikeGlob(p)) return [NODE_PATH.resolve(p)];

  /** @type {any} */
  const GLOB = require('glob');
  const globSync = (GLOB && typeof GLOB.globSync === 'function')
    ? GLOB.globSync
    : (typeof GLOB.sync === 'function' ? GLOB.sync : null);

  if (!globSync) return [];

  const matches = globSync(p, {
    absolute: true,
    dot: true,
    nodir: false,
    windowsPathsNoEscape: true
  });

  return Array.isArray(matches) ? matches.map((m) => NODE_PATH.resolve(String(m))) : [];
}

/**
 * Delete a single absolute literal path (sync).
 *
 * @private
 * @param {string} absPath
 * @param {{ force?: boolean }} [opt]
 * @returns {boolean}
 */
function _deleteLiteralSync(absPath, opt = {}) {
  const force = opt.force !== false;
  try {
    const st = NODE_FS.statSync(absPath);
    if (st.isDirectory()) {
      NODE_FS.rmSync(absPath, { recursive: true, force: true });
    } else {
      NODE_FS.unlinkSync(absPath);
    }
    return true;
  } catch (e) {
    if (force) {
      const code = e && e.code ? String(e.code) : '';
      if (code === 'ENOENT' || code === 'ENOTDIR') return false;
    }
    throw e;
  }
}

/**
 * Delete files/directories synchronously. Supports literal paths and glob patterns.
 *
 * Notes:
 * - For glob patterns, paths are expanded using `glob` first.
 * - Returned paths are absolute.
 *
 * @function deleteSync
 * @memberof module:cure-fs
 * @param {string|string[]} pattern
 *   Path or patterns to delete.
 * @param {{ force?: boolean }} [opt]
 * @returns {string[]}
 *   Absolute paths successfully deleted.
 * @since 1.1.0
 */
function deleteSync(pattern, opt = {}) {
  const force = opt.force !== false;
  const patterns = _toPatternArray(pattern);
  /** @type {string[]} */
  const candidates = [];

  for (const p of patterns) {
    const expanded = _expandPatternSync(p);
    if (expanded.length > 0) {
      candidates.push(...expanded);
      continue;
    }
    if (!_looksLikeGlob(p)) {
      candidates.push(NODE_PATH.resolve(p));
    }
  }

  const unique = Array.from(new Set(candidates));
  /** @type {string[]} */
  const deleted = [];

  for (const abs of unique) {
    if (_deleteLiteralSync(abs, { force })) deleted.push(abs);
  }

  return deleted;
}

/**
 * Delete files/directories asynchronously. Supports literal paths and glob patterns.
 *
 * Notes:
 * - Prefers `del.deleteAsync` (dynamic import, ESM-safe).
 * - Falls back to sync expansion + async fs delete when `del` is unavailable.
 *
 * @function deleteAsync
 * @memberof module:cure-fs
 * @param {string|string[]} pattern
 *   Path or patterns to delete.
 * @param {{ force?: boolean }} [opt]
 * @returns {Promise<string[]>}
 *   Absolute paths successfully deleted.
 * @since 1.1.0
 */
async function deleteAsync(pattern, opt = {}) {
  const force = opt.force !== false;
  const patterns = _toPatternArray(pattern);

  try {
    const DEL = await import('del');
    const deleteAsyncFn = DEL && typeof DEL.deleteAsync === 'function' ? DEL.deleteAsync : null;
    if (deleteAsyncFn) {
      const deleted = await deleteAsyncFn(patterns, { force: true });
      return Array.isArray(deleted) ? deleted.map((p) => NODE_PATH.resolve(String(p))) : [];
    }
  } catch (_) {
    // Ignore and use fallback.
  }

  const deletedSync = deleteSync(patterns, { force });
  return deletedSync;
}

/**
 * Test whether a file or directory exists synchronously (no stat race side-effects).
 *
 * Implementation notes:
 * - Uses `fs.accessSync` and returns a boolean.
 * - Does not throw for non-existence; exceptions are caught and mapped to `false`.
 *
 * @function doesExistFileSync
 * @memberof module:cure-fs
 * @param {import('fs').PathLike} path
 *   File or directory path to probe.
 * @returns {boolean}
 *   `true` if accessible; `false` otherwise.
 * @since 1.0.0
 *
 * @example
 * const ok = doesExistFileSync('dist/app.js'); // → true/false
 */
function doesExistFileSync(path) {
  try {
    NODE_FS.accessSync(path);
    return true;
  } catch {
    return false;
  }
}

/**
 * Test whether a file or directory exists asynchronously (no stat race side-effects).
 *
 * Implementation notes:
 * - Uses `fs.promises.access` and resolves to `true`/`false` only.
 * - No exception is thrown for non-existence.
 *
 * @function doesExistFileAsync
 * @memberof module:cure-fs
 * @param {import('fs').PathLike} path
 *   File or directory path to probe.
 * @returns {Promise<boolean>}
 *   `true` if accessible; `false` otherwise.
 *
 * @example
 * const ok = await doesExistFileAsync('dist/app.js'); // → true/false
 */
async function doesExistFileAsync(path) {
  try {
    await NODE_FS.promises.access(path);
    return true;
  } catch {
    return false;
  }
}

/**
 * Ensure a directory exists synchronously (mkdir -p semantics).
 *
 * Behavior:
 * - Recursively creates the directory and all missing parents.
 * - No-op if the directory already exists.
 *
 * @function ensureDirectorySync
 * @memberof module:cure-fs
 * @param {string} path
 *   Directory path to create.
 * @returns {void}
 * @since 1.0.0
 *
 * @example
 * ensureDirectorySync('out/assets/images');
 */
function ensureDirectorySync(path) {
  NODE_FS.mkdirSync(path, { recursive: true });
}

/**
 * Ensure a directory exists asynchronously (mkdir -p semantics).
 *
 * Behavior:
 * - Recursively creates the directory and all missing parents.
 * - No-op if the directory already exists.
 *
 * @function ensureDirectoryAsync
 * @memberof module:cure-fs
 * @param {string} path
 *   Directory path to create.
 * @returns {Promise<void>}
 *
 * @example
 * await ensureDirectoryAsync('out/assets/images');
 */
async function ensureDirectoryAsync(path) {
  await NODE_FS.promises.mkdir(path, { recursive: true });
}

/**
 * Copy a file synchronously, ensuring the destination directory exists.
 *
 * @function copySync
 * @memberof module:cure-fs
 * @param {string} pathFrom
 *   Source file path.
 * @param {string} pathTo
 *   Destination file path (parent folders will be created as needed).
 * @returns {void}
 * @throws {Error}
 *   If the source cannot be read or destination cannot be written.
 * @since 1.0.0
 *
 * @example
 * copySync('src/logo.png', 'dist/img/logo.png');
 */
function copySync(pathFrom, pathTo) {
  ensureDirectorySync(NODE_PATH.dirname(pathTo));
  NODE_FS.copyFileSync(pathFrom, pathTo);
}

/**
 * Copy a file asynchronously, ensuring the destination directory exists.
 *
 * @function copyAsync
 * @memberof module:cure-fs
 * @param {string} pathFrom
 *   Source file path.
 * @param {string} pathTo
 *   Destination file path (parent folders will be created).
 * @returns {Promise<void>}
 * @throws {Error}
 *   If the source cannot be read or destination cannot be written.
 *
 * @example
 * await copyAsync('src/logo.png', 'dist/img/logo.png');
 */
async function copyAsync(pathFrom, pathTo) {
  await ensureDirectoryAsync(NODE_PATH.dirname(pathTo));
  await NODE_FS.promises.copyFile(pathFrom, pathTo);
}

/**
 * Move (rename) a file synchronously, creating the destination directory and
 * handling cross-device (`EXDEV`) boundaries via copy+unlink fallback.
 *
 * Strategy:
 * - Attempt `renameSync`.
 * - If it fails with `EXDEV`, fallback to `copyFileSync` then `unlinkSync`.
 *
 * @function moveSync
 * @memberof module:cure-fs
 * @param {string} pathFrom
 *   Source path.
 * @param {string} pathTo
 *   Destination path (parent folders will be created as needed).
 * @returns {void}
 * @throws {Error}
 *   If both `renameSync` and the fallback sequence fail.
 * @since 1.0.0
 *
 * @example
 * moveSync('build/app.tmp.js', 'build/app.js');
 */
function moveSync(pathFrom, pathTo) {
  if (doesExistFileSync(pathFrom)) {
    return;
  }
  ensureDirectorySync(NODE_PATH.dirname(pathTo));
  try {
    NODE_FS.renameSync(pathFrom, pathTo);
  } catch (err) {
    if (err && err.code === 'EXDEV') {
      NODE_FS.copyFileSync(pathFrom, pathTo);
      NODE_FS.unlinkSync(pathFrom);
    } else {
      throw err;
    }
  }
}

/**
 * Move (rename) a file asynchronously, creating the destination directory and handling cross-device boundaries.
 *
 * Strategy:
 * - Attempt `rename`.
 * - If it fails with `EXDEV`, fallback to `copyFile` then `unlink` (copyAsync+delete).
 *
 * @function moveAsync
 * @memberof module:cure-fs
 * @param {string} pathFrom
 *   Source path.
 * @param {string} pathTo
 *   Destination path (parent folders will be created).
 * @returns {Promise<void>}
 * @throws {Error}
 *   If both `rename` and the fallback sequence fail.
 *
 * @example
 * await moveAsync('build/app.tmp.js', 'build/app.js');
 */
async function moveAsync(pathFrom, pathTo) {
  if (await doesExistFileAsync(pathFrom)) {
    return;
  }
  await ensureDirectoryAsync(NODE_PATH.dirname(pathTo));
  await NODE_FS.promises.rename(pathFrom, pathTo).catch(async (err) => {
    if (err && err.code === 'EXDEV') {
      // Cross-volume moveAsync: copyAsync + unlink
      await NODE_FS.promises.copyFile(pathFrom, pathTo);
      await NODE_FS.promises.unlink(pathFrom);
    } else {
      throw err;
    }
  });
}

/**
 * Detect the line-ending sequence used by an existing file, cheaply.
 *
 * Reads only the first `maxBytes` bytes looking for the first newline.
 *
 * @param {string} path
 * @param {string} [fallback='\n']
 * @param {number} [maxBytes=65536] 64 KiB
 * @returns {'\n' | '\r\n' | '\r'}
 */
function detectFileEolSync(path, fallback = '\n', maxBytes = 65536) {
  if (!NODE_FS.existsSync(path)) return /** @type any */ (fallback);

  const fd = NODE_FS.openSync(path, 'r');
  try {
    const buf = Buffer.allocUnsafe(maxBytes);
    const bytesRead = NODE_FS.readSync(fd, buf, 0, maxBytes, 0);
    if (bytesRead <= 0) return /** @type any */ (fallback);

    const slice = buf.subarray(0, bytesRead);

    const lf = slice.indexOf(0x0A); // '\n'
    if (lf !== -1) {
      return (lf > 0 && slice[lf - 1] === 0x0D) ? '\r\n' : '\n';
    }

    const cr = slice.indexOf(0x0D); // '\r'
    if (cr !== -1) return '\r';

    return /** @type any */ (fallback);
  } finally {
    NODE_FS.closeSync(fd);
  }
}

