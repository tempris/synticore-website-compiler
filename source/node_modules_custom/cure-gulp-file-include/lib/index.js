function decodeUtf8Strict(bufferOrText, contextPath) {
  var text = Buffer.isBuffer(bufferOrText)
    ? bufferOrText.toString('utf8')
    : String(bufferOrText || '')

  if (text.indexOf('\uFFFD') !== -1) {
    throw new Error('Invalid UTF-8 while processing include source: ' + contextPath)
  }

  return text.replace(/^\uFEFF/, '')
}

function readUtf8Strict(filePath) {
  var text = fs.readFileSync(filePath, 'utf-8')
  return decodeUtf8Strict(text, filePath)
}

function toPluginError(error) {
  var pluginError = new PluginError('gulp-file-include', error && error.message ? error.message : String(error))

  if (error && typeof error === 'object') {
    if (error.fileName || error.file) pluginError.fileName = error.fileName || error.file
    if (typeof error.lineNumber !== 'undefined') pluginError.lineNumber = error.lineNumber
    if (typeof error.columnNumber !== 'undefined') pluginError.columnNumber = error.columnNumber
    if (typeof error.codeFrame !== 'undefined') pluginError.codeFrame = error.codeFrame
    if (typeof error.includeStack !== 'undefined') pluginError.includeStack = error.includeStack
  }

  return pluginError
}

function getLineColumnDetails(sourceText, index) {
  var text = String(sourceText || '')
  var target = Number(index)

  if (!isFinite(target) || target < 0) {
    target = 0
  }
  if (target > text.length) {
    target = text.length
  }

  var line = 1
  var column = 1
  var lastLineStart = 0

  for (var i = 0; i < target; i++) {
    if (text.charCodeAt(i) === 10) {
      line += 1
      column = 1
      lastLineStart = i + 1
    } else {
      column += 1
    }
  }

  var lineEnd = text.indexOf('\n', lastLineStart)
  if (lineEnd === -1) {
    lineEnd = text.length
  }

  var lineText = text.slice(lastLineStart, lineEnd).replace(/\r$/, '')

  return {
    line: line,
    column: column,
    lineText: lineText,
    lineStartIndex: lastLineStart
  }
}

function buildCodeFrame(sourceText, index) {
  var text = String(sourceText || '')
  var details = getLineColumnDetails(text, index)
  var lines = text.split(/\r?\n/)
  var frameRadius = 1
  var startLine = Math.max(1, details.line - frameRadius)
  var endLine = Math.min(lines.length || 1, details.line + frameRadius)
  var width = String(endLine).length
  var out = []

  for (var lineNo = startLine; lineNo <= endLine; lineNo++) {
    var prefix = lineNo === details.line ? '>' : ' '
    var lineLabel = String(lineNo)
    var lineText = String(lines[lineNo - 1] || '')
    out.push(prefix + ' ' + Array(width - lineLabel.length + 1).join(' ') + lineLabel + ' | ' + lineText)

    if (lineNo === details.line) {
      out.push('  ' + Array(width + 1).join(' ') + ' | ' + Array(Math.max(details.column - 1, 0) + 1).join(' ') + '^')
    }
  }

  return out.join('\n')
}

function createDiagnosticError(message, meta) {
  var filePath = meta && meta.filePath ? String(meta.filePath) : ''
  var line = meta && Number.isFinite(meta.line) ? Number(meta.line) : null
  var column = meta && Number.isFinite(meta.column) ? Number(meta.column) : null
  var codeFrame = meta && meta.codeFrame ? String(meta.codeFrame) : ''
  var includeStack = Array.isArray(meta && meta.includeStack) ? meta.includeStack.filter(Boolean).map(String) : []
  var parts = [String(message || 'Unknown file include error')]

  if (filePath && line !== null && column !== null) {
    parts.push('File: ' + filePath + ':' + line + ':' + column)
  } else if (filePath) {
    parts.push('File: ' + filePath)
  }

  if (includeStack.length) {
    parts.push('Include stack:\n' + includeStack.map(function(entry, idx) {
      return (idx === includeStack.length - 1 ? '  -> ' : '   - ') + entry
    }).join('\n'))
  }

  if (codeFrame) {
    parts.push(codeFrame)
  }

  var err = new Error(parts.join('\n'))
  err.fileName = filePath || undefined
  err.file = filePath || undefined
  err.lineNumber = line !== null ? line : undefined
  err.columnNumber = column !== null ? column : undefined
  err.codeFrame = codeFrame || undefined
  err.includeStack = includeStack
  return err
}

function escapeRegExp(value) {
  return String(value || '').replace(/[|\\{}()[\]^$+?.]/g, '\\$&')
}

function globToRegExp(pattern) {
  var normalized = String(pattern || '').replace(/\\/g, '/')
  var out = ''
  var i = 0

  while (i < normalized.length) {
    var ch = normalized[i]

    if (ch === '*') {
      if (normalized[i + 1] === '*') {
        out += '.*'
        i += 2
        continue
      }
      out += '[^/]*'
      i += 1
      continue
    }

    if (ch === '?') {
      out += '[^/]'
      i += 1
      continue
    }

    out += escapeRegExp(ch)
    i += 1
  }

  return new RegExp('^' + out + '$', 'i')
}

function normalizeSlashPath(value) {
  return String(value || '').replace(/\\/g, '/')
}

function normalizePathLikeLiteral(value) {
  var text = String(value || '')

  if (text.indexOf('\\') === -1) {
    return text
  }

  // Normalize Windows-like paths so escape sequences like "\t" do not get
  // interpreted during downstream parsing/evaluation.
  if (/^[A-Za-z]:\\/.test(text) || /^\\\\/.test(text) || text.indexOf('/') !== -1) {
    return normalizeSlashPath(text)
  }

  return text
}

function normalizePathLiteralsInQuotedText(value) {
  var text = String(value || '')
  if (!text) return text

  return text.replace(/(["'])([^"']*)\1/g, function(match, quote, raw) {
    var normalized = normalizePathLikeLiteral(raw)
    return normalized === raw ? match : (quote + normalized + quote)
  })
}

function isPlainObjectValue(value) {
  return Object.prototype.toString.call(value) === '[object Object]'
}

function buildLoopEntryFromFs(entryType, absPath, relPath, sourceDirRel) {
  var parsed = path.parse(absPath)
  var rel = normalizeSlashPath(relPath)
  var sourceRel = normalizeSlashPath(sourceDirRel)
  var webPath = '/' + normalizeSlashPath(path.join(sourceRel, rel)).replace(/^\/+/, '')

  return {
    type: entryType,
    name: parsed.base,
    basename: parsed.base,
    stem: parsed.name,
    ext: parsed.ext,
    path: normalizeSlashPath(absPath),
    path_from_source: rel,
    path_from_project: normalizeSlashPath(path.join(sourceRel, rel)),
    path_web: webPath
  }
}

function readLoopEntriesFromFs(sourceConfig, filebase) {
  var source = isPlainObjectValue(sourceConfig) ? sourceConfig : {}
  var sourceType = String(source.type || '').toLowerCase()
  var sourceDir = String(source.dir || '').trim()
  var recursive = !!source.recursive
  var matchRegex = source.match ? globToRegExp(source.match) : null
  var absRoot
  var out = []

  if (!sourceDir) {
    throw new Error('Loop option error: `source.dir` must be a non-empty folder path. Received: ' + String(source.dir))
  }

  if (sourceType !== 'files' && sourceType !== 'dirs') {
    throw new Error('Loop option error: `source.type` must be "files" or "dirs". Received: ' + String(source.type))
  }

  absRoot = path.resolve(filebase, sourceDir)

  if (!fs.existsSync(absRoot)) {
    throw new Error('Loop source folder was not found on disk: ' + absRoot)
  }

  function walk(currentAbs, currentRel) {
    var entries = fs.readdirSync(currentAbs, { withFileTypes: true })

    entries.forEach(function(dirent) {
      var entryRel = currentRel
        ? path.join(currentRel, dirent.name)
        : dirent.name
      var relNormalized = normalizeSlashPath(entryRel)
      var absPath = path.join(currentAbs, dirent.name)
      var includeEntry = false

      if (sourceType === 'files' && dirent.isFile()) {
        includeEntry = true
      } else if (sourceType === 'dirs' && dirent.isDirectory()) {
        includeEntry = true
      }

      if (includeEntry && (!matchRegex || matchRegex.test(relNormalized))) {
        out.push(buildLoopEntryFromFs(sourceType.slice(0, -1), absPath, relNormalized, sourceDir))
      }

      if (recursive && dirent.isDirectory()) {
        walk(absPath, entryRel)
      }
    })
  }

  walk(absRoot, '')

  out.sort(function(a, b) {
    return String(a.path_from_source || '').localeCompare(String(b.path_from_source || ''))
  })

  return out
}

module.exports = function(opts) {
  if (typeof opts === 'string') {
    opts = {prefix: opts}
  }

  opts = extend({}, {
    basepath: '@file',
    prefix: '@@',
    suffix: '',
    context: {},
    filters: false,
    indent: false
  }, opts)

  if (opts.basepath !== '@file') {
    opts.basepath = opts.basepath === '@root' ? process.cwd() : path.resolve(opts.basepath)
  }

  var customWebRoot = !!opts.context.webRoot
  var includeOnceFiles = {};

  function fileInclude(file, enc, cb) {
    if (!customWebRoot) {
      // built-in webRoot variable, example usage: <link rel=stylesheet href=@@webRoot/style.css>
      opts.context.webRoot =
        path.relative(path.dirname(file.path), file.base).replace(/\\/g, '/') || '.'
    }

    if (file.isNull()) {
      cb(null, file)
    } else if (file.isStream()) {
      file.contents.pipe(concat(function(data) {
        try {
          data = include(file, decodeUtf8Strict(data, file.path))
          cb(null, data)
        } catch (e) {
          log.error(LOG_TAG_FILE_INCLUDE, 'Include processing failed.', {
            file: file && file.path,
            error: String(e && e.message ? e.message : e),
            line: e && e.lineNumber,
            column: e && e.columnNumber,
            includeStack: e && e.includeStack,
            codeFrame: e && e.codeFrame
          })
          cb(toPluginError(e))
        }
      }))
    } else if (file.isBuffer()) {
      try {
        file = include(file, decodeUtf8Strict(file.contents, file.path))
        cb(null, file)
      } catch (e) {
        log.error(LOG_TAG_FILE_INCLUDE, 'Include processing failed.', {
          file: file && file.path,
          error: String(e && e.message ? e.message : e),
          line: e && e.lineNumber,
          column: e && e.columnNumber,
          includeStack: e && e.includeStack,
          codeFrame: e && e.codeFrame
        })
        cb(toPluginError(e))
      }
    }
  }

  return through.obj(fileInclude)

  /**
   * utils
   */
  function stripCommentedIncludes(content, opts) {
    // remove single line html comments that use the format: <!-- @@include() -->
    var regex = new RegExp('<!--(.*)' + opts.prefix + '[ ]*include([\\s\\S]*?)[ ]*' + opts.suffix + '-->', 'g')
    return content.replace(regex, '')
  }

  function splitTopLevelArgs(raw) {
    var str = String(raw || '')
    var args = []
    var chunk = ''
    var depthParen = 0
    var depthBracket = 0
    var depthBrace = 0
    var quote = null
    var escaped = false

    for (var i = 0; i < str.length; i++) {
      var ch = str[i]

      if (quote) {
        chunk += ch
        if (escaped) {
          escaped = false
          continue
        }
        if (ch === '\\') {
          escaped = true
          continue
        }
        if (ch === quote) {
          quote = null
        }
        continue
      }

      if (ch === '\'' || ch === '"') {
        quote = ch
        chunk += ch
        continue
      }

      if (ch === '(') depthParen++
      else if (ch === ')') depthParen--
      else if (ch === '[') depthBracket++
      else if (ch === ']') depthBracket--
      else if (ch === '{') depthBrace++
      else if (ch === '}') depthBrace--

      if (ch === ',' && depthParen === 0 && depthBracket === 0 && depthBrace === 0) {
        args.push(chunk.trim())
        chunk = ''
        continue
      }

      chunk += ch
    }

    if (chunk.trim() !== '') {
      args.push(chunk.trim())
    }

    return args
  }

  function isPlainObject(value) {
    return Object.prototype.toString.call(value) === '[object Object]'
  }

  function getPathValue(source, pathKey) {
    if (!source || typeof pathKey !== 'string' || !pathKey.length) {
      return undefined
    }

    var parts = pathKey.split('.')
    var node = source

    for (var i = 0; i < parts.length; i++) {
      if (node == null) {
        return undefined
      }
      node = node[parts[i]]
    }

    return node
  }

  function matchesFilterValue(actual, expected) {
    if (Array.isArray(expected)) {
      return expected.indexOf(actual) !== -1
    }

    if (isPlainObject(expected)) {
      if (!isPlainObject(actual)) {
        return false
      }

      for (var key in expected) {
        if (Object.prototype.hasOwnProperty.call(expected, key)) {
          if (!matchesFilterValue(actual[key], expected[key])) {
            return false
          }
        }
      }

      return true
    }

    return actual === expected
  }

  function matchesFilterObject(item, filterObject) {
    for (var key in filterObject) {
      if (Object.prototype.hasOwnProperty.call(filterObject, key)) {
        var expected = filterObject[key]
        var actual = key.indexOf('.') !== -1
          ? getPathValue(item, key)
          : (item ? item[key] : undefined)
        if (!matchesFilterValue(actual, expected)) {
          return false
        }
      }
    }

    return true
  }

  function createLoopFilterPredicate(filterSpec, data, options, inst) {
    if (typeof filterSpec === 'undefined') {
      return null
    }

    if (typeof filterSpec === 'string') {
      var source = filterSpec.trim()
      if (!source.length) {
        return null
      }

      var expression
      try {
        expression = new Function('item', 'context', 'options', 'index', 'array', 'return (' + source + ');') // eslint-disable-line
      } catch (err) {
        throw new Error('Loop filter expression is invalid. Check the expression syntax: ' + source + ' :: ' + err.message)
      }

      return function(item, index, array) {
        try {
          return !!expression(item, data, options || {}, index, array)
        } catch (err) {
          throw new Error('Loop filter expression failed while running. Check the expression and data: ' + source + ' :: ' + err.message)
        }
      }
    }

    if (isPlainObject(filterSpec)) {
      return function(item) {
        return matchesFilterObject(item, filterSpec)
      }
    }

    if (Array.isArray(filterSpec)) {
      var predicates = []
      for (var i = 0; i < filterSpec.length; i++) {
        var candidate = createLoopFilterPredicate(filterSpec[i], data, options, inst)
        if (typeof candidate === 'function') {
          predicates.push(candidate)
        }
      }

      if (!predicates.length) {
        return null
      }

      return function(item, index, array) {
        for (var j = 0; j < predicates.length; j++) {
          if (predicates[j](item, index, array)) {
            return true
          }
        }
        return false
      }
    }

    throw new Error('Loop filter option is invalid. Expected a string, object, or array: ' + String(filterSpec))
  }

  function applyLoopFilter(arr, filterSpec, data, options, inst) {
    var predicate = createLoopFilterPredicate(filterSpec, data, options, inst)
    if (typeof predicate !== 'function') {
      return arr
    }

    var filtered = []
    for (var i = 0; i < arr.length; i++) {
      if (predicate(arr[i], i, arr)) {
        filtered.push(arr[i])
      }
    }
    return filtered
  }

  function resolveCollectionData(dataArg, filebase, collectionBase, inst) {
    var arr = []
    var jsonMatch = /^\s*["']([^"']+)["']\s*$/.exec(String(dataArg || ''))
    var rawStringPath = typeof dataArg === 'string' ? String(dataArg).trim() : ''

    if (typeof dataArg === 'string' && (jsonMatch || rawStringPath)) {
      var jsonPath = jsonMatch ? jsonMatch[1] : rawStringPath
      var jsonfile = path.join(collectionBase, jsonPath)

      if (fs.existsSync(jsonfile)) {
        try {
          delete require.cache[jsonfile]
          arr = require(jsonfile)
        } catch (error) {
          throw new Error('Collection data JSON could not be loaded: ' + jsonfile + ' :: ' + error.message)
        }
      } else if (jsonMatch) {
        throw new Error('Collection data JSON file was not found: ' + jsonfile)
      } else {
        try {
          arr = JSON5.parse(normalizePathLiteralsInQuotedText(dataArg))
        } catch (err) {
          throw new Error('Collection data is invalid JSON/JSON5: ' + dataArg + ' :: ' + err.message)
        }
      }
    } else {
      arr = dataArg
    }

    if (isPlainObject(arr) && isPlainObject(arr.source)) {
      arr = readLoopEntriesFromFs(arr.source, filebase)
    }

    if (!Array.isArray(arr)) {
      throw new Error('Collection data must resolve to an array or a supported `source` object.')
    }

    return arr
  }

  function resolveCollectionSelection(collectionInput, selectionConfig, sharedData, filebase, collectionBase, inst) {
    var collection = resolveCollectionData(collectionInput, filebase, collectionBase, inst)
    var filteredCollection = collection
    var filterSpec = selectionConfig.filter
    var neighborsEnabled = !!selectionConfig.neighbors
    var selectionPredicate
    var selectedIndex = -1
    var current = null
    var previous = null
    var next = null

    if (typeof filterSpec !== 'undefined') {
      filteredCollection = applyLoopFilter(filteredCollection, filterSpec, sharedData, selectionConfig.options || {}, inst)
    }

    if (typeof selectionConfig.select !== 'undefined') {
      selectionPredicate = createLoopFilterPredicate(selectionConfig.select, sharedData, selectionConfig.options || {}, inst)

      if (typeof selectionPredicate !== 'function') {
        throw new Error('Include selection is invalid. `select` must resolve to a non-empty string, object, or array.')
      }

      for (var i = 0; i < filteredCollection.length; i++) {
        if (selectionPredicate(filteredCollection[i], i, filteredCollection)) {
          selectedIndex = i
          current = filteredCollection[i]
          break
        }
      }
    }

    if (neighborsEnabled && selectedIndex !== -1) {
      previous = selectedIndex > 0 ? filteredCollection[selectedIndex - 1] : null
      next = selectedIndex < filteredCollection.length - 1 ? filteredCollection[selectedIndex + 1] : null
    }

    return {
      current: current,
      previous: previous,
      next: next,
      _index: selectedIndex,
      _length: filteredCollection.length,
      _collection: filteredCollection
    }
  }

  function include(file, text, data, sourceFile, includeStack) {
    var filebase = opts.basepath === '@file' ? path.dirname(file.path) : opts.basepath
    var currentFilename = path.resolve(file.base, file.path)
    var currentSourceFile = sourceFile ? path.resolve(String(sourceFile)) : currentFilename
    var currentIncludeStack = Array.isArray(includeStack) ? includeStack.slice() : []
    var unresolvedVariableNotices = Object.create(null)

    if (!currentIncludeStack.length) {
      currentIncludeStack.push(currentFilename)
    }

    data = extend(true, {}, opts.context, data || {})
    data.content = text

    function throwLocatedError(inst, rawError, fallbackMessage) {
      var message = rawError && rawError.message ? rawError.message : (fallbackMessage || String(rawError))
      var location = getLineColumnDetails(text, inst && typeof inst.startIndex === 'number' ? inst.startIndex : 0)
      var stack = currentIncludeStack.slice()

      if (!stack.length || stack[stack.length - 1] !== currentFilename) {
        stack.push(currentFilename)
      }

      throw createDiagnosticError(message, {
        filePath: currentSourceFile,
        line: location.line,
        column: location.column,
        codeFrame: buildCodeFrame(text, inst && typeof inst.startIndex === 'number' ? inst.startIndex : 0),
        includeStack: stack
      })
    }

    function processTemplateText(rawText, localData) {
      var nextText = String(rawText || '')
      var nextData = extend(true, {}, data, localData || {})

      nextText = stripCommentedIncludes(nextText, opts)
      nextText = replaceConditionalOperator(nextText, {
        prefix: opts.prefix,
        suffix: opts.suffix,
        name: 'if',
        handler: conditionalHandler,
        sourceFile: currentSourceFile
      })
      nextText = replaceOperator(nextText, {
        prefix: opts.prefix,
        suffix: opts.suffix,
        name: 'for',
        handler: forHandler,
        sourceFile: currentSourceFile
      })
      nextText = replaceVariable(nextText, nextData, opts)
      nextText = replaceFunction(nextText, {
        prefix: opts.prefix,
        suffix: opts.suffix,
        name: 'include_once',
        handler: includeOnceHandler,
        sourceFile: currentSourceFile
      })
      nextText = replaceFunction(nextText, {
        prefix: opts.prefix,
        suffix: opts.suffix,
        name: 'include',
        handler: includeHandler,
        sourceFile: currentSourceFile
      })
      nextText = replaceFunction(nextText, {
        prefix: opts.prefix,
        suffix: opts.suffix,
        name: 'loop',
        handler: loopHandler,
        sourceFile: currentSourceFile
      })

      return replaceVariable(nextText, nextData, extend({}, opts, {
        onUnresolvedVariable: function(info) {
          var dedupeKey = currentSourceFile + '::' + String(info && info.index) + '::' + String(info && info.key)
          var location

          if (unresolvedVariableNotices[dedupeKey]) {
            return
          }
          unresolvedVariableNotices[dedupeKey] = true

          location = getLineColumnDetails(nextText, info && typeof info.index === 'number' ? info.index : 0)

          log.notice(LOG_TAG_FILE_INCLUDE, 'Unresolved plain variable token left as-is.', {
            file: currentSourceFile,
            line: location.line,
            column: location.column,
            token: info && info.token,
            key: info && info.key,
            includeStack: currentIncludeStack,
            codeFrame: buildCodeFrame(nextText, info && typeof info.index === 'number' ? info.index : 0)
          })
        }
      }))
    }

    text = processTemplateText(text)

    function conditionalHandler(inst) {
      var branches = inst.branches || [{
        type: 'if',
        args: inst.args,
        body: inst.body
      }]

      for (var i = 0; i < branches.length; i++) {
        var branch = branches[i]

        if (branch.type === 'else') {
          return branch.body
        }

        try {
          var condition = new Function('var context = this; with (context) { return ' + branch.args + '; }').call(data) // eslint-disable-line
        } catch (error) {
          throwLocatedError(inst, error, error.message + ': ' + branch.args)
        }

        if (condition) {
          return branch.body
        }
      }

      return ''
    }

    function forHandler(inst) {
      var forLoop = 'for' + inst.args + ' { result+=`' + inst.body + '`; }'
      var condition = 'var context = this; with (context) { var result=""; ' + forLoop + ' return result; }'
      try {
        var result = new Function(condition).call(data) // eslint-disable-line
      } catch (error) {
        throwLocatedError(inst, error, error.message + ': ' + forLoop)
      }

      return result
    }

    function includeOnceHandler(inst) {
      var args = /[^)"']*["']([^"']*)["'](,\s*({[\s\S]*})){0,1}\s*/.exec(inst.args)
      if (args) {
        var includeOnceKey = normalizeSlashPath(args[1])
        if (typeof includeOnceFiles[inst.sourceFile] === 'undefined') {
          includeOnceFiles[inst.sourceFile] = [];
        }
        if (includeOnceFiles[inst.sourceFile].indexOf(includeOnceKey) === -1) {
          includeOnceFiles[inst.sourceFile].push(includeOnceKey);
          return includeHandler(inst)
        } else {
          return '';
        }
      }

      throwLocatedError(inst, null, 'Include-once directive is invalid. Expected @@include_once(path[, dataObject]).')
    }

    function includeHandler(inst) {
      var args = /[^)"']*["']([^"']*)["'](,\s*({[\s\S]*})){0,1}\s*/.exec(inst.args)
      var includeData = {}
      var includeRenderData
      var includeMetaKeys = {
        from: true,
        select: true,
        filter: true,
        where: true,
        neighbors: true,
        context: true
      }

      if (!args) {
        throwLocatedError(inst, null, 'Include directive is invalid. Expected @@include(path[, dataObject]).')
      }

      var includePath = path.resolve(filebase, normalizeSlashPath(args[1]))
      // for checking if we are not including the current file again
      if (currentFilename.toLowerCase() === includePath.toLowerCase()) {
        throwLocatedError(inst, null, 'recursion detected in file: ' + currentFilename)
      }

      var includeContent
      try {
        includeContent = readUtf8Strict(includePath)
      } catch (error) {
        throwLocatedError(inst, error)
      }

      if (opts.indent) {
        includeContent = setIndent(inst.before, inst.before.length, includeContent)
      }

      // need to double each `$` to escape it in the `replace` function
      // includeContent = includeContent.replace(/\$/gi, '$$$$');

      // apply filters on include content
      if (typeof opts.filters === 'object') {
        try {
          includeContent = applyFilters(includeContent, args.input)
        } catch (error) {
          throwLocatedError(inst, error, 'Failed to apply include filters.')
        }
      }

      var recFile = new Vinyl({
        cwd: process.cwd(),
        base: file.base,
        path: includePath,
        contents: Buffer.from(includeContent)
      })

      if (args[3]) {
        try {
          includeData = JSON5.parse(
            normalizePathLiteralsInQuotedText(processTemplateText(args[3]))
          )
        } catch (error) {
          throwLocatedError(inst, error, 'Failed to parse include data: ' + args[3])
        }
      }

      includeRenderData = extend(true, {}, includeData)

      if (includeData && Object.prototype.hasOwnProperty.call(includeData, 'from')) {
        var includeContextData = {}
        var includeSelectionData = {}
        var selectionResolverData
        var includeKey

        if (Object.prototype.hasOwnProperty.call(includeData, 'context')) {
          if (!isPlainObject(includeData.context)) {
            throwLocatedError(inst, null, 'Include data error: `context` must be an object when using `from`.')
          }
          includeContextData = extend(true, {}, includeData.context)
        }

        for (includeKey in includeData) {
          if (Object.prototype.hasOwnProperty.call(includeData, includeKey) && !includeMetaKeys[includeKey]) {
            includeContextData[includeKey] = includeData[includeKey]
          }
        }

        selectionResolverData = extend(true, {}, data, includeContextData)
        includeSelectionData = resolveCollectionSelection(includeData.from, {
          filter: Object.prototype.hasOwnProperty.call(includeData, 'filter')
            ? includeData.filter
            : includeData.where,
          select: includeData.select,
          neighbors: includeData.neighbors
        }, selectionResolverData, filebase, file.base, inst)

        includeRenderData = extend(true, {}, includeContextData, includeSelectionData)
      }

      recFile = include(recFile, includeContent, includeRenderData, includePath, currentIncludeStack.concat(includePath))

      return decodeUtf8Strict(recFile.contents, includePath)
    }

    function loopHandler(inst) {
      var args = splitTopLevelArgs(inst.args)
      var rawOptions = {}
      var loopOptions = {}
      var loopSharedContext = {}
      var filterSpec
      var includeArg
      var dataArg
      var includePath
      var includeContent
      var recFile
      var contents = ''

      if (!args || args.length < 2 || args.length > 3) {
        throwLocatedError(inst, null, 'Loop directive is invalid. Expected @@loop(templatePath, arrayOrJsonPathOrSource[, options]).')
      }

      includeArg = /^\s*["']([^"']+)["']\s*$/.exec(args[0])
      if (!includeArg) {
        throwLocatedError(inst, null, 'Loop directive is invalid. First argument must be a quoted template path.')
      }

      dataArg = args[1]

      if (args.length === 3) {
        try {
          rawOptions = JSON5.parse(normalizePathLiteralsInQuotedText(args[2])) || {}
        } catch (err) {
          throwLocatedError(inst, err, 'Loop options JSON is invalid: ' + args[2])
        }
      }

      if (rawOptions && typeof rawOptions !== 'object') {
        throwLocatedError(inst, null, 'Loop options are invalid. Expected an object.')
      }

      if (rawOptions && typeof rawOptions === 'object') {
        var optionKey
        for (optionKey in rawOptions) {
          if (Object.prototype.hasOwnProperty.call(rawOptions, optionKey) && optionKey !== 'loop' && optionKey !== 'context') {
            throwLocatedError(inst, null, 'Loop options key "' + optionKey + '" is not supported. Use only "loop" and "context".')
          }
        }

        if (Object.prototype.hasOwnProperty.call(rawOptions, 'loop')) {
          if (!isPlainObject(rawOptions.loop)) {
            throwLocatedError(inst, null, 'Loop options error: `loop` must be an object.')
          }
          loopOptions = extend(true, {}, rawOptions.loop)
        }

        if (Object.prototype.hasOwnProperty.call(rawOptions, 'context')) {
          if (!isPlainObject(rawOptions.context)) {
            throwLocatedError(inst, null, 'Loop options error: `context` must be an object.')
          }
          loopSharedContext = extend(true, {}, rawOptions.context)
        }
      }

      var arr = resolveCollectionData(dataArg, filebase, file.base, inst)

      if (loopOptions && typeof loopOptions === 'object') {
        if (Object.prototype.hasOwnProperty.call(loopOptions, 'filter')) {
          filterSpec = loopOptions.filter
        } else if (Object.prototype.hasOwnProperty.call(loopOptions, 'where')) {
          // Alias for teams that prefer SQL-style naming.
          filterSpec = loopOptions.where
        }
      }

      if (Array.isArray(arr) && typeof filterSpec !== 'undefined') {
        try {
          arr = applyLoopFilter(arr, filterSpec, data, loopOptions, inst)
        } catch (error) {
          throwLocatedError(inst, error)
        }
      }

      if (arr && loopOptions && typeof loopOptions.item_max !== 'undefined') {
        var rawItemMax = loopOptions.item_max
        var normalizedItemMax = rawItemMax
        var shouldSkipItemMax = false

        if (typeof normalizedItemMax === 'string') {
          normalizedItemMax = normalizedItemMax.trim()
        }

        if (
          normalizedItemMax === null ||
          normalizedItemMax === '' ||
          (typeof normalizedItemMax === 'string' && (
            /^@@/.test(normalizedItemMax) ||
            normalizedItemMax.toLowerCase() === 'null' ||
            normalizedItemMax.toLowerCase() === 'undefined'
          ))
        ) {
          shouldSkipItemMax = true
        }

        if (!shouldSkipItemMax) {
          var itemMax = Number(normalizedItemMax)
          if (isNaN(itemMax) || itemMax < 0) {
            throwLocatedError(inst, null, 'Loop options error: `loop.item_max` must be a number 0 or higher.')
          }
          arr = arr.slice(0, Math.floor(itemMax))
        }
      }

      if (arr) {
        includePath = path.resolve(filebase, normalizeSlashPath(includeArg[1]))
        // for checking if we are not including the current file again
        if (currentFilename.toLowerCase() === includePath.toLowerCase()) {
          throwLocatedError(inst, null, 'recursion detected in file: ' + currentFilename)
        }

        try {
          includeContent = readUtf8Strict(includePath)
        } catch (error) {
          throwLocatedError(inst, error)
        }

        if (opts.indent) {
          includeContent = setIndent(inst.before, inst.before.length, includeContent)
        }

        // apply filters on include content
        if (typeof opts.filters === 'object') {
          try {
            includeContent = applyFilters(includeContent, inst.args)
          } catch (error) {
            throwLocatedError(inst, error, 'Failed to apply loop include filters.')
          }
        }

        recFile = new Vinyl({
          cwd: process.cwd(),
          base: file.base,
          path: includePath,
          contents: Buffer.from(includeContent)
        })

        for (var i in arr) {
          if (arr.hasOwnProperty(i)) {
            var index = Number(i)
            var previousItem = index > 0 ? arr[index - 1] : null
            var nextItem = index < arr.length - 1 ? arr[index + 1] : null
            // Keep parent include context (for shared values like style_column),
            // while allowing per-item keys to override when present.
            var context = extend(true, {}, data, loopSharedContext, arr[i], {
              _key: String(i),
              _index: index,
              _first: index === 0,
              _last: index === arr.length - 1,
              _length: arr.length,
              _previous: previousItem,
              _next: nextItem
            })
            recFile = include(recFile, includeContent, context, includePath, currentIncludeStack.concat(includePath))
            contents += decodeUtf8Strict(recFile.contents, includePath)
            }
          }
      }

      return contents
    }

    file.contents = Buffer.from(text, 'utf8')

    return file
  }

  function applyFilters(includeContent, match) {
    if (!match.match(/\)+$/)) {
      // nothing to filter return unchanged
      return includeContent
    }

    // now get the ordered list of filters
    var filterlist = match.split('(').slice(0, -1)
    filterlist = filterlist.map(function(str) {
      return opts.filters[str.trim()]
    })

    // compose them together into one function
    var filter = filterlist.reduce(compose)

    // check match for filter options object
    var options = match.match('{([^}]*)}')

    // and apply the composed function to the stringified content
    if (options) {
      options = JSON5.parse(options[0])
      return filter(String(includeContent), options)
    } else {
      return filter(String(includeContent))
    }
  }
}

function compose(f, g) {
  return function(x) {
    return f(g(x))
  }
}
