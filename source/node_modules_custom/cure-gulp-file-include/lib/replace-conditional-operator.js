'use strict'

const balanced = require('balanced-match')

module.exports = function parseConditional(content, opts) {
  var regexpIfStart = new RegExp(opts.prefix + '[ ]*if([^{}]*)\\{')
  var regexpElseIfStart = new RegExp('^\\s*' + opts.prefix + '[ ]*elseif([^{}]*)\\{')
  var regexpElseStart = new RegExp('^\\s*' + opts.prefix + '[ ]*else\\s*\\{')
  var regexpEnd = opts.suffix ? new RegExp('^\\s*' + opts.suffix) : false
  var replacement
  var result = ''
  var matchIfStart
  var before

  function parseBlock(source, safeStart) {
    var matchBody = balanced('{', '}', source.slice(safeStart - 1))
    var matchEnd = 0

    if (!matchBody || matchBody.start !== 0) {
      return null
    }

    if (regexpEnd) {
      var suffixMatch = regexpEnd.exec(matchBody.post)
      if (!suffixMatch) {
        return null
      }
      matchEnd = suffixMatch[0].length
    }

    return {
      body: matchBody.body,
      consumed: safeStart + matchBody.end + matchEnd
    }
  }

  while (matchIfStart = regexpIfStart.exec(content)) { // eslint-disable-line
    var safeStart = matchIfStart.index + matchIfStart[0].length
    var parsedIf = parseBlock(content, safeStart)

    if (!parsedIf) {
      result += content.slice(0, safeStart)
      content = content.slice(safeStart)
      continue
    }

    before = content.slice(0, matchIfStart.index)

    var branches = [{
      type: 'if',
      args: matchIfStart[1],
      body: parsedIf.body
    }]

    var totalConsumed = parsedIf.consumed
    var tail = content.slice(totalConsumed)
    var chainMatched = true

    while (chainMatched) {
      chainMatched = false

      var elseifMatch = regexpElseIfStart.exec(tail)
      if (elseifMatch && elseifMatch.index === 0) {
        var elseIfSafeStart = elseifMatch[0].length
        var parsedElseIf = parseBlock(tail, elseIfSafeStart)
        if (parsedElseIf) {
          branches.push({
            type: 'elseif',
            args: elseifMatch[1],
            body: parsedElseIf.body
          })
          totalConsumed += parsedElseIf.consumed
          tail = content.slice(totalConsumed)
          chainMatched = true
          continue
        }
      }

      var elseMatch = regexpElseStart.exec(tail)
      if (elseMatch && elseMatch.index === 0) {
        var elseSafeStart = elseMatch[0].length
        var parsedElse = parseBlock(tail, elseSafeStart)
        if (parsedElse) {
          branches.push({
            type: 'else',
            args: '',
            body: parsedElse.body
          })
          totalConsumed += parsedElse.consumed
          tail = content.slice(totalConsumed)
        }
      }
    }

    replacement = opts.handler({
      before: before,
      args: branches[0].args,
      body: branches[0].body,
      branches: branches,
      sourceFile: opts.sourceFile,
      startIndex: matchIfStart.index
    })

    if (replacement !== undefined) {
      result += before + parseConditional(replacement.toString(), opts)
      content = content.slice(totalConsumed)
      continue
    }

    result += content.slice(0, safeStart)
    content = content.slice(safeStart)
  }

  result += content

  return result
}
