'use strict'

const flatten = require('flatnest').flatten

module.exports = function(content, data, opts) {
  var prefix = opts.prefix + '[ ]*'
  var suffix = opts.suffix ? '[ ]*' + opts.suffix : ''
  var unresolvedRegex = new RegExp(
    prefix + '([A-Za-z0-9_.-]+)' + suffix + '(?!\\s*\\()',
    'g'
  )
  var reservedNames = {
    include: true,
    include_once: true,
    loop: true,
    if: true,
    elseif: true,
    else: true,
    for: true
  }
  data = flatten(data)
  // sort keys by longest keys to iterate in that order
  var keys = Object.keys(data).sort()
  var i = keys.length - 1
  var key

  for (; ~i; i -= 1) {
    key = keys[i]
    content = content.replace(new RegExp(prefix + key + suffix, 'g'), data[key])
  }

  if (typeof opts.onUnresolvedVariable === 'function') {
    var match
    while ((match = unresolvedRegex.exec(content))) { // eslint-disable-line no-cond-assign
      var unresolvedKey = match[1]
      if (reservedNames[unresolvedKey]) {
        continue
      }
      opts.onUnresolvedVariable({
        key: unresolvedKey,
        token: match[0],
        index: match.index
      })
    }
  }

  return content
}
