/**
 * Convert a 0-based character offset into a 1-based `{ line, col }` position.
 *
 * Why:
 * Native `JSON.parse` errors expose a "position N" offset. For human-readable
 * diagnostics we need a conventional line and column. This utility translates
 * that absolute offset into a 1-based line/column pair, normalizing all common
 * newline sequences (`\r\n`, `\n`, `\r`).
 *
 * Characteristics:
 * - Treats `pos` as a 0-based index into `text`.
 * - Returns `{ line, col }` as **1-based** values.
 * - Splits only the substring *before* the offset, avoiding unnecessary work.
 *
 * Complexity:
 * - Time: O(pos) due to slicing and a single split.
 * - Space: O(pos) for the temporary `before` substring and split array.
 *
 * Edge cases:
 * - `pos <= 0` → `{ line: 1, col: 1 }`.
 * - `pos >= text.length` → last line with column = line length + 1.
 *
 * @private
 * @function _posToLineCol
 * @memberof module:cure-json
 * @param {string} text
 *   Full source text being analyzed.
 * @param {number} pos
 *   Absolute, 0-based character offset into `text`.
 * @returns {{line:number, col:number}}
 *   A 1-based line and column pair corresponding to `pos`.
 * @since 1.0.0
 *
 * @example
 * const src = "a\\nBC\\nDEF";
 * // Offsets: 0:'a', 1:'\n', 2:'B', 3:'C', 4:'\n', 5:'D', 6:'E', 7:'F'
 * _posToLineCol(src, 0); // → { line: 1, col: 1 }
 * _posToLineCol(src, 2); // → { line: 2, col: 1 }  ('B')
 * _posToLineCol(src, 7); // → { line: 3, col: 3 }  ('F')
 */
function _posToLineCol(text, pos) {
  // pos is a 0-based index into `text`
  const before = text.slice(0, pos);
  const lines = before.split(/\r\n|\n|\r/);
  const line = lines.length;                 // 1-based
  const col  = (lines[lines.length - 1] || '').length + 1; // 1-based
  return { line, col };
}

/**
 * Build a focused, single-line context slice and a caret marker under a column.
 *
 * Why:
 * When reporting parse/validation errors, a compact one-line excerpt with a
 * caret (`^`) pointing at the problematic column is both readable and stable
 * across terminals/log sinks. This utility returns the line substring around
 * `col` (windowed by `span`) and a matching caret string for alignment.
 *
 * Characteristics:
 * - Accepts 1-based `line` and `col` positions.
 * - Extracts up to `span` characters centered around `col` (left-biased when
 *   near the start of the line).
 * - Produces a caret that aligns under the target column within the slice.
 *
 * Behavior:
 * - If `line` is out of range, returns `{ slice: "", caret: "" }`.
 * - Tabs/spaces in the original line are preserved verbatim in `slice`.
 * - Caret alignment is computed with simple character widths (no tab expansion).
 *
 * @private
 * @function _contextLine
 * @memberof module:cure-json
 * @param {string} text
 *   Full source text to excerpt from.
 * @param {number} line
 *   1-based line number containing the error.
 * @param {number} col
 *   1-based column within `line` to point at.
 * @param {number} [span=120]
 *   Desired width of the returned slice (in characters).
 * @returns {{slice:string, caret:string}}
 *   `slice`: the extracted line substring; `caret`: spaces + `^` aligned to `col`.
 * @since 1.0.0
 *
 * @example
 * const text = "first\\n0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZ\\nlast";
 * // Error at line 2, column 11 (character 'A'):
 * const { slice, caret } = _contextLine(text, 2, 11, 20);
 * // slice: "0123456789ABCDEFGH"  (20 chars starting near col 1)
 * // caret: "          ^"         (10 spaces + '^' under 'A')
 */
function _contextLine(text, line, col, span = 120) {
  const lines = text.split(/\r\n|\n|\r/);
  const raw = lines[line - 1] ?? '';
  // focus window around the error column
  const start = Math.max(0, col - Math.floor(span / 2) - 1);
  const slice = raw.slice(start, start + span);
  const caret = ' '.repeat(Math.max(0, col - start - 1)) + '^';
  return { slice, caret };
}

/**
 * Replace all JSON string literals in `input` with equal-length whitespace,
 * preserving byte offsets and line/column positions for downstream parsing.
 *
 * Why:
 * When validating JSON for comments, we must ignore any `/* ... *\/` or `// ...`
 * sequences that appear **inside** strings. By blanking-out string contents
 * (without changing the overall length), we can safely run comment regex checks
 * against the remaining text while keeping character indices stable for
 * accurate error reporting.
 *
 * Characteristics:
 * - Produces an output string with the **same length** as `input`.
 * - Only the **contents** of string literals are replaced; surrounding quotes
 *   remain intact.
 * - Handles escaped quotes and escape sequences via the default `stringRe`.
 *
 * @private
 * @function _stripStrings
 * @memberof module:cure-json
 * @param {string} input
 *   The original JSON text to process.
 * @param {RegExp} [stringRe=/"(?:\\.|[^"\\])*"/g]
 *   A global regex that matches JSON string literals. Override only if you have
 *   a specialized tokenizer; the default covers escaped quotes and backslashes.
 * @returns {string}
 *   A copy of `input` where all string literal contents are replaced by spaces.
 *   The returned string is the **same length** as `input`.
 * @since 1.1.0
 *
 * @example
 * // Comments inside strings should be ignored by validators:
 * const src = '{"a":"/* not a comment *\/","b":"// also not a comment"}';
 * const masked = _stripStrings(src);
 * // `masked` keeps quotes and overall length; interior characters are spaces.
 *
 * @example
 * // Offsets are preserved for accurate caret positioning:
 * const i = src.indexOf('/*');             // index points inside a string
 * const sameIndexChar = masked[i];         // still a space at the same offset
 */
function _stripStrings(input, stringRe = /"(?:\\.|[^"\\])*"/g) {
  return input.replace(stringRe, m => ' '.repeat(m.length));
}

