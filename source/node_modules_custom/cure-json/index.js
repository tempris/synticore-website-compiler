/**
 * Recursively sort object keys (stable, locale-aware, numeric segments respected).
 *
 * Purpose:
 * Produce a **deeply key-sorted** clone of objects/arrays so that serialized JSON
 * is deterministic across runs and environments (useful for diffs, hashing, and
 * cache busting).
 *
 * Semantics:
 * - **Objects:** Returns a *new* plain object with keys sorted ascending via
 *   `localeCompare(undefined, { numeric: true })`. Nested objects are processed
 *   recursively.
 * - **Arrays:** Array order is preserved; each element is recursively sorted.
 * - **Primitives / null / undefined:** Returned as-is.
 * - **Non-plain objects (Date, Map, class instances):** Treated as generic
 *   objects; only their **enumerable own properties** are considered and the
 *   resulting value becomes a **plain object** (prototype is not preserved).
 *
 * Caveats:
 * - Symbols and non-enumerable properties are ignored.
 * - Cycles are **not** detected; input must be acyclic.
 *
 * Complexity:
 * - Roughly O(N log N) over the number of keys per object (due to sorting),
 *   applied recursively to the structure.
 *
 * @function sort
 * @memberof module:cure-json
 * @param {*} obj
 *   Any JSON-serializable value. Objects/arrays are processed recursively.
 * @returns {*}
 *   A deeply key-sorted clone for objects/arrays; primitives are returned unchanged.
 * @since 1.1.0
 *
 * @example
 * sort({ b: 1, a: { d: 2, c: 3 } });
 * // → { a: { c: 3, d: 2 }, b: 1 }
 *
 * @example
 * // Array order is preserved; elements sorted individually:
 * sort([{ z: 1, a: 2 }, { '10': 'x', '2': 'y' }]);
 * // → [ { a: 2, z: 1 }, { '2': 'y', '10': 'x' } ]
 */
function sort(obj) {
  if (Array.isArray(obj)) {
    return obj.map(sort);
  } else if (obj && typeof obj === 'object') {
    return Object.keys(obj)
      .sort((a, b) => a.localeCompare(b, undefined, { numeric: true }))
      .reduce((acc, key) => {
        acc[key] = sort(obj[key]);
        return acc;
      }, {});
  }
  return obj;
}

/**
 * Deep clone utility using `structuredClone` when available, falling back to JSON.
 *
 * Behavior:
 * - If the platform provides `structuredClone`, it is used (handles many built-ins,
 *   typed arrays, Maps/Sets, etc., but *not* functions or DOM nodes).
 * - Otherwise falls back to `JSON.parse(JSON.stringify(value))`, which supports
 *   only JSON-serializable data (objects, arrays, numbers, strings, booleans, null).
 *
 * Limitations of the JSON fallback:
 * - Drops `undefined`, functions, symbols, and properties with values not
 *   representable in JSON.
 * - Converts `Date` to ISO strings; `NaN`/`Infinity` become `null`.
 * - Loses custom prototypes and non-enumerable properties.
 *
 * Determinism:
 * - No I/O, no randomness; purely functional for the same input value.
 *
 * @function clone
 * @memberof module:cure-json
 * @param {*} value
 *   Any value intended to be cloned. For best results, provide JSON-serializable
 *   data structures or rely on environments with `structuredClone`.
 * @returns {*}
 *   A deep clone of `value`, subject to the limitations above.
 * @throws {Error}
 *   If the JSON fallback encounters circular references or non-serializable
 *   values (e.g., BigInt), `JSON.stringify` will throw.
 * @since 1.1.0
 *
 * @example
 * const a = { when: new Date('2020-01-01T00:00:00Z') };
 * const b = clone(a);
 * // With structuredClone: b.when is a Date
 * // With JSON fallback:   b.when is an ISO string
 */
function clone(value) {
  // eslint-disable-next-line no-undef
  return (typeof structuredClone === 'function')
    ? structuredClone(value)
    : JSON.parse(JSON.stringify(value));
}

/**
 * Deeply merge `source` into `target` (object-only traversal; arrays are overwritten).
 *
 * Semantics:
 * - For each key in `source`:
 *   - If `source[key]` is a non-array object, ensure `target[key]` is an object
 *     (create `{}` if needed) and recurse.
 *   - Otherwise, **assign** `target[key] = source[key]` (arrays and primitives
 *     overwrite the prior value).
 *
 * Mutability:
 * - **Mutates** and returns `target`. `source` is never modified.
 *
 * Scope:
 * - Only **own enumerable properties** of `source` are considered.
 *
 * Expectations & caveats:
 * - Designed for merging **plain objects**. Passing `null`, `undefined`, or
 *   exotic objects (class instances, proxies) may yield surprising results.
 * - Arrays are **not** concatenated—entire arrays are replaced.
 * - No cycle detection; inputs must be acyclic.
 *
 * @function merge
 * @memberof module:cure-json
 * @param {Object} target
 *   Destination object to mutate.
 * @param {Object} source
 *   Source fields that extend/overwrite `target`.
 * @returns {Object}
 *   The same `target` reference, after being merged.
 * @since 1.1.0
 *
 * @example
 * const out = merge({ a: { x: 1 } }, { a: { y: 2 }, b: 3 });
 * // out === { a: { x: 1, y: 2 }, b: 3 }
 *
 * @example
 * // Arrays are replaced (not concatenated):
 * merge({ list: [1,2] }, { list: [9] }); // → { list: [9] }
 */
function merge(target, source) {
  for (const key of Object.keys(source)) {
    if (
      source[key] &&
      typeof source[key] === 'object' &&
      !Array.isArray(source[key])
    ) {
      if (!target[key] || typeof target[key] !== 'object') {
        target[key] = {};
      }
      merge(target[key], source[key]);
    } else {
      target[key] = source[key];
    }
  }
  return target;
}

/**
 * Validate that a file is **strict JSON** (no comments), with rich error context.
 *
 * Pipeline:
 * 1) Reads the file (UTF-8) and strips a leading BOM if present.
 * 2) Masks string literals (preserving length) so comment tokens inside strings
 *    are ignored during detection.
 * 3) Detects comments outside of strings using:
 *    - Block: `/\\*[\\s\\S]*?\\*\/`
 *    - Line:  `/(^|[^:])\\/\\/.*$/m`  (avoids `http://` after a colon)
 * 4) If comments are found, computes 1-based line/column, produces a focused
 *    context slice + caret, logs via Cure Log Hub, and throws.
 * 5) Otherwise attempts `JSON.parse`. On failure, maps native "position N"
 *    to line/column, logs a context block, and rethrows.
 *
 * Logging:
 * - Uses module tag `[🧾 JSON]` and includes an ANSI-off/ANSI-on bracketed
 *   block so the excerpt is readable in plain log files.
 * - Severity is `fatal` when `fatalOnError=true`, else `error`.
 *
 * Returns:
 * - `true` on success, or the **parsed object** when `returnParsed=true`.
 *
 * @function validate
 * @memberof module:cure-json
 * @param {string} filePath
 *   Path to the JSON file (absolute or relative).
 * @param {Object} [options]
 * @param {boolean} [options.returnParsed=false]
 *   If `true`, return the parsed object instead of `true`.
 * @param {boolean} [options.fatalOnError=true]
 *   If `true`, log with `fatal` severity; otherwise `error`.
 * @returns {true|Object}
 *   `true` on success, or the parsed JSON object if `returnParsed=true`.
 * @throws {Error}
 *   When comments are detected or parsing fails; the error message reflects
 *   the first detected problem and a contextual excerpt is logged.
 * @since 1.0.0
 *
 * @example
 * // Validate only:
 * validate('config.json'); // → true (or throws)
 *
 * @example
 * // Validate and parse in one pass:
 * const cfg = validate('config.json', { returnParsed: true });
 */
function validate(filePath, { returnParsed = false, fatalOnError = true } = {}) {
  try {
    let raw = NODE_FS.readFileSync(filePath, 'utf8');

    // Strip BOM if present
    raw = raw.replace(/^\uFEFF/, '');

    let withoutStrings = _stripStrings(raw);

    const blockRe = /\/\*[\s\S]*?\*\//g;
    const lineRe  = /(^|[^:])\/\/.*$/mg;

    let match, badIndex = null, reason = null;

    if ((match = blockRe.exec(withoutStrings))) {
      badIndex = match.index;
      reason = 'File contains block comments (/* */), which are not valid JSON.';
    } else if ((match = lineRe.exec(withoutStrings))) {
      badIndex = match.index + (match[1] ? 1 : 0);
      reason = 'File contains line comments (//), which are not valid JSON.';
    }

    if (badIndex != null) {
      const { line, col } = _posToLineCol(raw, badIndex);
      const { slice, caret } = _contextLine(raw, line, col);

      const block = [
        '',
        '[[ANSI_OFF]]Context[[ANSI_ON]]:',
        `[[ANSI_OFF]]${slice}[[ANSI_ON]]`,    // raw line
        `[[ANSI_OFF]]${caret}[[ANSI_ON]]`,    // ^ pointer
        ''
      ].join('\n');

      log[fatalOnError ? 'fatal' : 'error'](
        LOG_TAG_JSON,
        '[JSON Validation Error]',
        {
          File: filePath,
          Reason: reason,
          Line: line,
          Column: col,
        },
        block,
        'Please remove comments or convert to strict JSON.'
      );
      throw new Error(reason);
    }

    // 2) Parse; if it throws, convert "position N" to line/col
    const parsed = JSON.parse(raw);
    return returnParsed ? parsed : true;
  } catch (err) {
    // TODO: Avoid duplicate error logs when comment-detection already logged an error above.
    // Current flow logs in badIndex branch, throws, then catch logs the same failure again.
    // Consider tagging the thrown error as already-logged and skip the second log call here.
    // Try to pull a character position from the native error
    let pos = null;
    const m = /position\s+(\d+)/i.exec(err.message || '');
    if (m) pos = Number(m[1]);

    let line = null, col = null, slice = '', caret = '';
    try {
      const raw2 = NODE_FS.readFileSync(filePath, 'utf8').replace(/^\uFEFF/, '');
      if (pos != null && Number.isFinite(pos)) {
        ({ line, col } = _posToLineCol(raw2, pos));
        ({ slice, caret } = _contextLine(raw2, line, col));
      }
    } catch {}

    const reason = err?.message || 'Invalid JSON.';
    const meta = { File: filePath, Reason: reason };
    if (line != null && col != null) {
      meta.Line = line;
      meta.Column = col;
    }

    // Match the same “block” structure used earlier
    const block = (slice && caret)
      ? [
          '',
          '[[ANSI_OFF]]Context[[ANSI_ON]]:',
          `[[ANSI_OFF]]${slice}[[ANSI_ON]]`,
          `[[ANSI_OFF]]${caret}[[ANSI_ON]]`,
          ''
        ].join('\n')
      : '';

    log[fatalOnError ? 'fatal' : 'error'](
      LOG_TAG_JSON,
      '[JSON Validation Error]',
      meta,
      block,
      `Please fix this file before ${
        fatalOnError ? 'running' : 'continuing'
      }.`
    );
    throw err;
  }
}

/**
 * Validate and parse a JSON file in one pass, returning the parsed object.
 *
 * Behavior:
 * - Delegates to {@link module:cure-json.validate} with `returnParsed: true`,
 *   mapping `options.fatal` to `fatalOnError`.
 *
 * Reliability:
 * - Ensures comment-free, strict JSON. Throws with contextual logging on failure.
 *
 * @function load
 * @memberof module:cure-json
 * @param {string} filePath
 *   Path to a JSON file.
 * @param {Object} [options]
 * @param {boolean} [options.fatal=true]
 *   If `true`, validation errors are logged as `fatal`; otherwise `error`.
 * @returns {Object}
 *   Parsed JSON object.
 * @throws {Error}
 *   If validation or parsing fails; see {@link module:cure-json.validate}.
 * @since 1.0.0
 *
 * @example
 * const cfg = load('config.json');
 */
function load(filePath, { fatal = true } = {}) {
  // Validate and return the parsed object (no double-parse)
  return validate(filePath, { returnParsed: true, fatalOnError: fatal });
}

/**
 * Write a JSON-serializable value to disk with optional deep key sorting and indentation.
 *
 * Behavior:
 * - If `options.sort` is `true`, the value is first transformed by
 *   {@link module:cure-json.sort} for deterministic key order.
 * - Serializes with `JSON.stringify(value, null, spacing)` and writes synchronously
 *   with UTF-8 encoding. Existing files are overwritten.
 * - Emits a debug log entry on success; emits an error log and rethrows on failure.
 *
 * Inputs:
 * - Value must be JSON-serializable after optional sorting; circular references
 *   or unsupported types will cause `JSON.stringify` to throw.
 *
 * I/O:
 * - Synchronous write via `fs.writeFileSync`.
 *
 * @function save
 * @memberof module:cure-json
 * @param {string} filePath
 *   Output file path (created or overwritten).
 * @param {*} data
 *   JSON-serializable value to persist.
 * @param {Object} [options]
 * @param {boolean} [options.sort=false]
 *   If `true`, apply {@link module:cure-json.sort} before serialization.
 * @param {number}  [options.spacing=2]
 *   Indentation width passed to `JSON.stringify`.
 * @returns {void}
 * @throws {Error}
 *   If serialization or file writing fails.
 * @since 1.0.0
 *
 * @example
 * save('out.json', { z: 1, a: 2 }, { sort: true, spacing: 2 });
 */
function save(filePath, data, { sort: sortOpt = false, spacing = 2 } = {}) {
  try {
    const toWrite = sortOpt ? sort(data) : data;
    const jsonText = JSON.stringify(toWrite, null, spacing);
    const NODE_CURE_FS = require('@custom/cure-fs');
    NODE_CURE_FS.writeFileSync(filePath, jsonText);
    log.debug?.(LOG_TAG_JSON, 'Saved JSON file:', filePath, { sort: sortOpt, spacing });
  } catch (err) {
    log.error?.(LOG_TAG_JSON, 'Failed to save JSON file:', filePath, { err });
    throw err;
  }
}

/**
 * Safely read a nested property from an object using a dot path (supports simple [index] for arrays).
 *
 * Characteristics:
 * - Pure, synchronous, zero I/O.
 * - Returns `undefined` when any segment is missing (no throws).
 *
 * Path grammar:
 * - Dot segments: "a.b.c"
 * - Optional simple array indices: "items[0].name"
 *
 * Caveats:
 * - Does not evaluate quoted keys or complex bracket notation. Use dot segments and simple [number] indices only.
 *
 * @function get
 * @memberof module:cure-json
 * @param {*} obj
 *   The root value (usually an object). Non-objects simply yield `undefined` unless path is empty.
 * @param {string} path
 *   Lookup path like `"build.version"` or `"items[0].name"`.
 * @param {*} [fallback]
 *   Value to return when the resolved lookup is `undefined`.
 * @returns {*}
 *   The resolved value or `fallback` (if provided) when result is `undefined`.
 * @since 1.2.0
 *
 * @example
 * get({ a: { b: 1 } }, 'a.b');            // → 1
 * get({ list: [{x: 9}] }, 'list[0].x');   // → 9
 * get(null, 'anything', 'n/a');           // → 'n/a'
 */
function get(obj, path, fallback) {
  if (!path || typeof path !== 'string') return (obj === undefined ? fallback : obj);
  if (obj == null || (typeof obj !== 'object' && !Array.isArray(obj))) return fallback;

  // Split path into tokens: foo, bar, 0, baz, ...
  const tokens = [];
  const re = /[^.\[\]]+|\[(\d+)\]/g;
  let m;
  while ((m = re.exec(path))) {
    tokens.push(m[1] !== undefined ? Number(m[1]) : m[0]);
  }

  let cur = obj;
  for (const k of tokens) {
    if (cur == null) return fallback;
    if (typeof k === 'number') {
      if (!Array.isArray(cur) || k < 0 || k >= cur.length) return fallback;
      cur = cur[k];
    } else {
      if (typeof cur !== 'object' || !(k in cur)) return fallback;
      cur = cur[k];
    }
  }
  return cur === undefined ? fallback : cur;
}

/**
 * Flatten a nested object into a flat `{ CONFIG_FOO_BAR: value }` map.
 *
 * Purpose:
 * - Turn a nested config object into token-ready placeholders (e.g., for HTML replacement).
 *
 * Behavior (defaults mirror legacy `_getConfigFlat` literally):
 * - Recurse into any non-null object **including arrays**.
 * - Keys are joined by dots while recursing, then converted to UPPER_SNAKE with dots → underscores.
 * - `null` is treated as a terminal value.
 *
 * Options (optional; defaults preserve legacy behavior):
 * - `prefix` (string): leading token prefix (default `'CONFIG_'`).
 * - `shouldRecurse` (fn): `(value) => boolean` to decide recursion (default: `v && typeof v === 'object'`).
 * - `keyTransform` (fn): `(pathString) => string` to override how `a.b.c` → `A_B_C` is derived.
 *
 * @function flatten
 * @memberof module:cure-json
 * @param {Object} obj
 *   Source object to flatten.
 * @param {Object} [options]
 * @param {string} [options.prefix='CONFIG_']
 * @param {(value:*)=>boolean} [options.shouldRecurse]
 * @param {(path:string)=>string} [options.keyTransform]
 * @returns {Object<string,*>}
 *   Flat map like `{ CONFIG_BUILD_VERSION: '1.2.3', CONFIG_BRAND_NAME: 'Synticore' }`.
 * @since 1.2.0
 *
 * @example
 * flatten({ build: { version: '1.2.3' }, brand: { name: 'Synticore' } });
 * // → { CONFIG_BUILD_VERSION: '1.2.3', CONFIG_BRAND_NAME: 'Synticore' }
 *
 * @example
 * // Custom prefix (still recurses into arrays unless you override shouldRecurse):
 * flatten({ a: { b: 1 } }, { prefix: 'MY_' });
 * // → { MY_A_B: 1 }
 */
function flatten(obj, {
  prefix = 'CONFIG_',
  // Legacy-default: recurse into ANY non-null object (arrays included)
  shouldRecurse = (v) => v && typeof v === 'object',
  keyTransform = (dotPath) => dotPath.replace(/\./g, '_').toUpperCase()
} = {}) {
  const out = {};
  if (!obj || typeof obj !== 'object') return out;

  /** @param {Object} node @param {string} path */
  (function walk(node, path) {
    for (const k of Object.keys(node)) {
      const v = node[k];
      const nextPath = path ? `${path}.${k}` : k;
      if (shouldRecurse(v)) {
        walk(v, nextPath);
      } else {
        const flatKey = prefix + keyTransform(nextPath);
        out[flatKey] = v;
      }
    }
  })(obj, '');

  return out;
}

/**
 * Structural equality for JSON-serializable values.
 *
 * Optimization:
 * - Treats both values `undefined` as equal to avoid unnecessary writes.
 *
 * @param {*} a
 * @param {*} b
 * @returns {boolean} True if equal.
 * @since 1.0.0
 * @example
 * if (equal(prev.data, next.data)) { /* skip *\/ }
 */
function equal(a, b) {
  if (a === b) return true;
  if (typeof a === 'undefined' && typeof b === 'undefined') return true;
  return JSON.stringify(a) === JSON.stringify(b);
}

