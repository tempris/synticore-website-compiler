'use strict';

/**
 * @fileoverview
 * cure-log-bridge-py — Python→Node stdin bridge that forwards newline-delimited JSON to {@link module:cure-log.Log}.
 *
 * Purpose:
 * - Read one JSON object per line from **STDIN**, validate shape, and dispatch to the corresponding `log[level](...args)`.
 * - Emit a delivery sentinel (`LOG_BRIDGE_END\n`) to **both STDOUT and STDERR** after each processed line so the sender
 *   can do strict line-granular acknowledgement.
 * - Keep the bridge thin: let {@link module:cure-log.Log} handle formatting, thresholds, file/console routing, and exit banners.
 *
 * Runtime & module system:
 * - Runtime: Node.js (LTS).
 * - Module system: CommonJS (CJS).
 *
 * Side effects on import:
 * - None. Process I/O and listeners are attached when this file is executed (intended as a CLI/child process).
 *
 * Globals & environment:
 * - Uses Node globals: `process`, `__dirname`.
 * - Reads `process.argv[2]` (log file path, absolute/relative) and optional `process.argv[3]` (log config file path).
 *
 * Security & permission assumptions:
 * - Treats STDIN strictly as data; parses JSON and dispatches only to known logger methods (no `eval`, no dynamic code).
 * - Writes to the filesystem **only through** the underlying {@link module:cure-log.Log} instance; caller must ensure permissions.
 * - No network access. No shelling out. Input lines outside the contract are logged as errors and acknowledged.
 *
 * I/O contract:
 * - Input: newline-terminated UTF-8 lines on STDIN; each line a JSON object `{ "level": string, "args": any[] }`.
 * - Output: writes the exact sentinel string `LOG_BRIDGE_END\n` to **STDOUT** and **STDERR** after each handled line (success or error).
 * - Encoding: STDOUT default encoding is set to UTF-8 to ensure the sentinel is consistent cross-platform.
 *
 * Concurrency & async:
 * - Sequential, line-oriented processing via `readline.Interface` (`terminal:false`). No batching or parallelism.
 * - Backpressure is bounded by the line-by-line acknowledgement pattern (producer should wait for the sentinel).
 * - Cleanup is automatic on process exit; no open handles beyond the active `readline` interface.
 *
 * Compatibility & performance:
 * - Works on modern Node LTS. Processing is O(1) per line besides JSON parse and a single logger call.
 * - No hot path file I/O here; file writing is delegated to {@link module:cure-log.Log} and gated by its config.
 *
 * Versioning policy:
 * - SemVer. Breaking CLI/protocol changes bump MAJOR.
 *
 * @module cure-log-bridge-py
 * @version 1.0.0
 * @since 1.0.0
 * @license MIT
 * @author Cure Interactive
 *
 * @example <caption>Basic Python → Node usage</caption>
 * // Python (sender) — pseudocode:
 * // ---------------------------------------
 * // proc = subprocess.Popen(
 * //   ["node", "index.js", "/abs/path/_log/app.log", "/abs/path/config/log.json"],
 * //   stdin=subprocess.PIPE, stdout=subprocess.PIPE, stderr=subprocess.PIPE, text=True
 * // )
 * // msg = json.dumps({"level":"info", "args":["Hello from Python", {"meta":1}]})
 * // proc.stdin.write(msg + "\n"); proc.stdin.flush()
 * // # Now read either STDOUT or STDERR until you see 'LOG_BRIDGE_END\n'
 * // line = proc.stdout.readline()
 * // assert line == "LOG_BRIDGE_END\n"
 *
 * @example <caption>Direct shell test</caption>
 * # echo '{"level":"success","args":["[BridgeTest]","It works!"]}' | node index.js ./_log/app.log
 * # expect the sentinel on stdout and stderr; check the log file for the entry
 */

/**
 * JSON line contract for the bridge.
 * @typedef {Object} BridgeMessage
 * @property {keyof ReturnType<() => any>} level
 *   Name of a method on the {@link module:cure-log.Log} instance, e.g. `"info" | "warn" | "error" | "debug" | "success" | "begin" | "end" | "init" | "shutdown" | "detail" | "trace" | "log" | "fatal"`.
 * @property {any[]} args
 *   Positional arguments spread into the logger call: strings, objects, errors (the logger formats them).
 * @since 1.0.0
 */

/**
 * Effective CLI arguments consumed by this bridge.
 * @typedef {Object} BridgeCli
 * @property {string} logPath Absolute path to the log file to write via {@link module:cure-log.Log}.
 * @property {string|null} [configPath] Optional absolute path to a JSON config consumed by {@link module:cure-log.Log}.
 * @since 1.0.0
 */

// -----------------------------------------------------------------------------
// [🔌 Dependencies]
// -----------------------------------------------------------------------------
const NODE_PATH = require('path');
const NODE_READLINE = require('readline');
const NODE_CURE_LOG = require(NODE_PATH.resolve(__dirname, '../cure-log'));

// -----------------------------------------------------------------------------
// [🏷️ Tags & Process Encoding]
// -----------------------------------------------------------------------------
/** Human-readable tag used in initial log lines for this bridge. @since 1.0.0 */
const LOG_TAG_BRIDGE = '[🌉 Log Bridge Python]'

/**
 * Normalize STDOUT encoding to UTF-8 so the sentinel is consistent across platforms/hosts.
 * @since 1.0.0
 */
process.stdout.setDefaultEncoding('utf8');

// -----------------------------------------------------------------------------
// [📎 CLI → Paths]
// -----------------------------------------------------------------------------

/**
 * Resolve CLI arguments to absolute paths (defensive defaults).
 *
 * Semantics & side effects:
 * - `PATH_FILE_LOG` becomes an absolute path; defaults to `<scriptDir>/_log/app.log` if missing.
 * - `PATH_FILE_CONFIG` is `null` when not provided; it is only included in the logger options when truthy.
 *
 * @type {string}
 * @since 1.0.0
 */
const PATH_FILE_LOG =
  NODE_PATH.resolve(process.argv[2]) ||
  // FIXME: make default to root _log dir file named after caller file, if possible.
  NODE_PATH.resolve(__dirname, '_log', 'app.log');

/** @type {string|null} Optional absolute path to a Cure Log config file. @since 1.0.0 */
const PATH_FILE_CONFIG = process.argv[3] ? NODE_PATH.resolve(process.argv[3]) : null;

// -----------------------------------------------------------------------------
// [🪵 Logger Initialization]
// -----------------------------------------------------------------------------

/**
 * Underlying Cure Log instance. All formatting, thresholds, routing and exit banners
 * are delegated to this logger. The bridge simply invokes methods on it.
 *
 * @type {import('../cure-log').Log}
 * @since 1.0.0
 *
 * @example
 * // Emits an init line showing resolved paths
 * // (already done below via `log.init(...)`).
 */
const log = new NODE_CURE_LOG.Log({
    filePath: {
        log: PATH_FILE_LOG,
        ...(PATH_FILE_CONFIG ? { config: PATH_FILE_CONFIG } : {})
    }
});

// -----------------------------------------------------------------------------
// [📥 Input Reader: Readline]
// -----------------------------------------------------------------------------

/**
 * Line-oriented interface over STDIN (non-interactive).
 *
 * Concurrency model:
 * - One line at a time; next line is not blocked but the producer should
 *   honor the sentinel to avoid overrunning the reader.
 *
 * @type {import('readline').Interface}
 * @since 1.0.0
 */
const rl = NODE_READLINE.createInterface({
    input: process.stdin,
    output: process.stdout,
    terminal: false
});

// -----------------------------------------------------------------------------
// [📤 Sentinel]
// -----------------------------------------------------------------------------

/**
 * Emit the delivery sentinel to both STDOUT and STDERR.
 *
 * Contract:
 * - Always write the exact string `"LOG_BRIDGE_END\n"`.
 * - Always write to **both** streams to support diverse producer setups.
 *
 * @function end
 * @memberof module:cure-log-bridge-py
 * @returns {void}
 * @since 1.0.0
 *
 * @example
 * // After every processed input line, the bridge calls:
 * end(); // writes LOG_BRIDGE_END on stdout and stderr
 */
function end() {
    process.stdout.write('LOG_BRIDGE_END\n');
    process.stderr.write('LOG_BRIDGE_END\n');
}

// -----------------------------------------------------------------------------
// [🧮 Line Handler]
// -----------------------------------------------------------------------------

/**
 * Handle each newline-terminated input `line` from STDIN.
 *
 * Semantics & side effects:
 * - Empty line → `log.error('Received empty log message')` and sentinel.
 * - Malformed JSON → `log.error('Error parsing log message:', error)` and sentinel.
 * - Unknown or non-callable `level` → `log.error('Invalid log level: <level>')` and sentinel.
 * - Valid `{level,args}` → invoke `log[level](...args)` and sentinel.
 *
 * I/O contract:
 * - Exactly **one** sentinel is emitted per input line (success or failure).
 *
 * @callback OnLine
 * @param {string} line Raw UTF-8 line from STDIN (without trailing newline).
 * @returns {void}
 * @since 1.0.0
 */
rl.on('line', (line) => {
    if (!line.trim()) {
        log.error('Received empty log message');
        end();
        return;
    }

    try {
        const { level, args } = JSON.parse(line);
        if (typeof log[level] === 'function') {
            log[level](...args);
            end();
        } else {
            log.error(`Invalid log level: ${level}`);
            end();
        }
    } catch (error) {
        log.error("Error parsing log message:", error);
        end();
    }
});

// -----------------------------------------------------------------------------
// [🚦 Bridge Online — Initial Log]
// -----------------------------------------------------------------------------

/**
 * Announce the bridge is online and where it will write logs.
 * This also acts as a sanity check that file paths resolved cleanly.
 * @since 1.0.0
 */
log.init(LOG_TAG_BRIDGE, 'Logger initialized. Writing logs to:', {PATH_FILE_LOG});

