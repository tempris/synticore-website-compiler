/**
 * Safely get e[key], optionally transform, else return undefined.
 * Returning undefined lets us filter it out of the log args cleanly.
 */
function _includeErrorValueIfExists(e, key, transform) {
  if (!e) return undefined;
  const v = e[key];
  if (v === undefined || v === null || v === '') return undefined;
  return typeof transform === 'function' ? transform(v) : v;
}

