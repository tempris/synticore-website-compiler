/**
 * @fileoverview
 * Gulp-CLI → Cure Log bridge (consumer).
 *
 * Purpose:
 *   Consume events emitted by `.gulp.js` (via `globalThis.__GULPCLI_BUS__`)
 *   and mirror them into Cure-Log with task lineage tracking, accurate
 *   elapsed timing (hrtime with CLI literal fallback), and friendlier error
 *   formatting blocks.
 *
 * Runtime & module system:
 *   - Node.js ≥ 14
 *   - CommonJS (CJS)
 *
 * Side effects on import:
 *   - None. Call {@link attach} to bind to the CLI bus.
 *
 * Concurrency & async:
 *   - Event-driven; supports parallel/overlapping tasks via per-task start stacks.
 *
 * Dependencies:
 *   - `@custom/cure-log-hub` (to obtain the process-wide logger)
 *   - `@custom/cure-ansi` (optional, for pretty error blocks; already required above)
 *   - `@custom/cure-time` (lazy-required inside for duration parsing/formatting)
 *
 * Security & assumptions:
 *   - Reads from `globalThis.__GULPCLI_BUS__` if present.
 *   - Assumes Cure-Log Hub is configured project-wide.
 *
 * @module @custom/cure-gulp-bridge
 * @version 1.0.0
 * @since 1.0.0
 * @license MIT
 */

