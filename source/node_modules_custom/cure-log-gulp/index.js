/**
 * Attach a single consumer to the Gulp-CLI bus and forward normalized events to Cure-Log.
 *
 * Behavior:
 *   - Safely no-ops if the CLI bus is missing or doesn’t expose `.attach`.
 *   - Tracks task lineage (root → … → leaf) across series/parallel execution.
 *   - Computes durations using `process.hrtime.bigint()` with CLI text fallback
 *     via `@custom/cure-time.normalizeDurationToNs`.
 *   - Emits structured begin/end/success/error log lines, and an emphasized,
 *     ANSI-framed error message block for `TASK_ERROR` payloads.
 *   - Exposes `globalThis.setGulpCliSwallow(v:boolean)` and
 *     `globalThis.toggleGulpCliSwallow()` helpers to flip CLI echoing.
 *
 * Idempotency:
 *   - Designed to be called once per process; subsequent calls are harmless in practice,
 *     but you should invoke it once during gulpfile initialization.
 *
 * Inputs:
 *   - None. This function discovers the bus from `globalThis.__GULPCLI_BUS__` and uses
 *     the process-wide Cure-Log instance obtained via Cure-Log Hub.
 *
 * Throws:
 *   - Never throws. All internal errors are caught and logged at debug level.
 *
 * @function attach
 * @returns {void}
 * @since 1.0.0
 *
 * @example
 * // In your gulpfile.js (after Cure-Log Hub is initialized):
 * const bridge = require('@custom/cure-gulp-bridge');
 * bridge.attach(); // attaches to globalThis.__GULPCLI_BUS__ if present
 */
function attach() {
  const LOG_TAG_GULP_TASK = LOG_TAG_GULP + ' [📋 Task]';
  const g = globalThis;

  if (!g.__GULPCLI_BUS__ || typeof g.__GULPCLI_BUS__.attach !== 'function') {
    log.debug?.(LOG_TAG_GULP_CLI, 'bus NOT detected — .gulp.js may not have loaded (cwd?)');
    log.notice?.(LOG_TAG_GULP_CLI, 'CLI bus not detected; .gulp.js may not have loaded.');
    return;
  }

  log.debug?.(LOG_TAG_GULP_CLI, 'bus detected; attaching consumer…');

  // NEW: per-task start-time stacks (supports parallel/overlapping tasks)
  const taskStacks = new Map();

  function pushStart(task) {
    const stack = taskStacks.get(task) || [];
    stack.push(process.hrtime.bigint());
    taskStacks.set(task, stack);
  }

  function popStart(task) {
    const stack = taskStacks.get(task);
    if (!stack || stack.length === 0) return null;
    const start = stack.pop();
    if (stack.length === 0) taskStacks.delete(task);
    return start;
  }

  // --- Task lineage tracking (root → … → leaf) -------------------------------
  // Tracks the current "call stack" of running tasks.
  // Works with series + parallel; STOP removes only the matching task.
  const execStack = [];                        // e.g., ['default', 'build', 'image']
  const startPaths = new Map();                // taskName -> array stack of path arrays

  function pushExec(task) {
    // lineage at START is current stack + this task
    const lineage = [...execStack, task];

    // remember the lineage for this specific start (handles parallel siblings)
    const list = startPaths.get(task) || [];
    list.push(lineage);
    startPaths.set(task, list);

    // make it active on the execution stack
    execStack.push(task);

    return lineage; // return for immediate logging use
  }

  function popExec(task) {
    // remove this task from execStack (it may not be the top in parallel finishes)
    for (let i = execStack.length - 1; i >= 0; i--) {
      if (execStack[i] === task) { execStack.splice(i, 1); break; }
    }

    // fetch the lineage saved at START (LIFO per task name)
    const list = startPaths.get(task);
    const lineage = (list && list.length) ? list.pop() : [task];
    if (list && list.length === 0) startPaths.delete(task);

    return lineage; // return for STOP/ERROR logging
  }

  // Tunables
  const DURATION_DEBUG = false;          // set true to log cross-checks
  const CROSSCHECK_THRESHOLD = 1.5;      // log if ours vs CLI differs by 1.5×+

  /**
   * Pop our start time stack, compute ns via hrtime, parse CLI literal,
   * choose best ns, and return a normalized payload for logging.
   */
  function calcTaskDuration(task, cliLiteral) {
    const NODE_CURE_TIME = require('@custom/cure-time');
    // ours (authoritative): based on hrtime.bigint() and the per-task stack
    const startedAt = popStart(task);
    const nsOurs = startedAt != null ? Number(process.hrtime.bigint() - startedAt) : undefined;

    // cli (literal): “947 μs”, “6.06 ms”, “1.4 s”, “00:00:01.234”, etc.
    const nsCli = NODE_CURE_TIME.normalizeDurationToNs(cliLiteral);

    // prefer ours when available; fall back to CLI
    const ns = nsOurs ?? nsCli;

    // pretty strings
    const prettyShort = NODE_CURE_TIME.formatDuration(ns, { long: false, style: 'compact', decimals: 2 });
    const prettyLong  = NODE_CURE_TIME.formatDuration(ns, { long: true });

    // Optional cross-check debug
    if (DURATION_DEBUG && nsOurs && nsCli) {
      const ratio = nsOurs > nsCli ? nsOurs / nsCli : nsCli / nsOurs;
      if (ratio >= CROSSCHECK_THRESHOLD) {
        log.info?.(LOG_TAG_GULP_TASK, 'Duration cross-check', {
          task,
          ours_ns: nsOurs,
          cli_ns: nsCli,
          ratio: +ratio.toFixed(2),
          cli_literal: String(cliLiteral || '')
        });
      }
    }

    return {
      ns,                 // chosen nanoseconds
      ns_ours: nsOurs,    // optional: our hrtime
      ns_cli: nsCli,      // optional: parsed CLI
      cli_literal: String(cliLiteral || ''),
      pretty: prettyShort,
      prettyLong
    };
  }

  function normalizeError(err) {
    // Null/undefined ⇒ nothing to do
    if (err == null) return null;

    // Helper: safe JSON for any value (handles circular)
    function safeStringify(value) {
      try {
        const seen = new WeakSet();
        return JSON.stringify(value, function (k, v) {
          if (typeof v === 'object' && v !== null) {
            if (seen.has(v)) return '[Circular]';
            seen.add(v);
          }
          return v;
        });
      } catch {
        // Last resort
        try { return String(value); } catch { return '[Unprintable]'; }
      }
    }

    // Helper: best-effort message for non-Error throwables or ugly cases
    function deriveMessageFromNonError(x) {
      if (typeof x === 'string') return x || 'Non-error string thrown';
      if (typeof x === 'number' || typeof x === 'boolean') return String(x);
      // Plain object (or anything else): compact JSON
      const s = safeStringify(x);
      return s && s !== '{}' ? s : 'Non-error object thrown';
    }

    // If it's an actual Error (or quacks like one), extract fields
    const isErrorLike =
      (typeof err === 'object' && err !== null && ('stack' in err || 'message' in err)) ||
      (err instanceof Error);

    const name = (isErrorLike && typeof err.name === 'string' && err.name) ? err.name : 'Error';

    // Prefer .message; but fix "[object Object]" & empties
    let message = (isErrorLike && typeof err.message === 'string') ? err.message : '';
    if (!message || message.trim() === '[object Object]') {
      // Try to derive from stack’s first line
      const stack = err && err.stack ? String(err.stack) : '';
      if (!message && stack) {
        const first = stack.split(/\r?\n/)[0] || '';
        message = first.replace(/^[A-Za-z]*Error:\s*/, '') || first;
      }
      // If still useless, fall back to serializing the entire thing
      if (!message || message.trim() === '[object Object]') {
        message = deriveMessageFromNonError(err);
      }
    }

    const stack = err && err.stack ? String(err.stack) : '';

    // Pull a few common extras when present (won’t add if missing)
    const extra = {};
    for (const k of [
      'code',
      'plugin',
      'showProperties',
      'showStack',
      'fileName',
      'lineNumber',
      'columnNumber'
    ]) {
      if (err && err[k] != null) extra[k] = err[k];
    }

    // For non-Error inputs, keep a snapshot so we can see the raw payload
    if (!isErrorLike) {
      extra.raw = err;
      extra.raw_serialized = deriveMessageFromNonError(err);
    } else if (message.trim() === '[object Object]') {
      // Preserve original object too if the message was the bad sentinel
      extra.raw = err;
      extra.raw_serialized = deriveMessageFromNonError(err);
    }

    return { name, message, stack, ...extra };
  }

  function handleEvent(ev) {
    try {
      const tag  = ev?.tag || '';
      const data = ev?.data || {};
      const task = (data.task || data.name || '').toString(); // normalize
      // const task_string = `\"[[ANSI_OFF]]${task}[[ANSI_ON]]\"`;
      const file = (data.path || data.file || '').toString();
      const dur  = (data.duration || '').toString(); // literal CLI text

      // log.notice(LOG_TAG_GULP, tag);

      switch (tag) {
        case 'GULP_CLI_GULPFILE': {
          // "Using gulpfile …"
          log.notice?.(LOG_TAG_GULP, 'Using gulpfile:', { file });
          break;
        }

        case 'GULP_CLI_TASK_START': {
          // capture authoritative start time
          pushStart(task);

          // lineage (root → … → leaf)
          const lineage = pushExec(task);

          // include both raw task and full path for clarity
          log.begin?.(LOG_TAG_GULP_TASK, 'Running...', {
            // task,
            task: lineage.join(' → '),
            // task_path: lineage.join(' → ')
          });

          // (Optional) also echo the full path as an info line if you like:
          // log.info?.(LOG_TAG_GULP_TASK, 'Lineage:', lineage_str);

          break;
        }

        case 'GULP_CLI_TASK_STOP': {
          const d = calcTaskDuration(task, dur);

          // recover lineage captured at START
          const lineage = popExec(task);

          // swap single task string with the full root→leaf
          log.success?.(LOG_TAG_GULP_TASK, `Complete: "[[ANSI_OFF]]${lineage.join(' → ')}[[ANSI_ON]]".`);
          log.end?.(LOG_TAG_GULP_TASK, 'Complete:', {
            // task,
            task: lineage.join(' → '),
            // task_path: lineage.join(' → '),
            duration: d.pretty
          });

          break;
        }

        case 'GULP_CLI_TASK_FAILURE': {
          const d = calcTaskDuration(task, dur);
          const lineage = popExec(task);

          log.error?.(LOG_TAG_GULP_TASK, 'Failure:', {
            task: lineage.join(' → '),
            duration: d.pretty
          });

          break;
        }

        case 'GULP_CLI_TASK_ERROR': {
          log.error?.(LOG_TAG_GULP_TASK, 'Error:', normalizeError(data?.error || data?.err || data));
          break;
        }

        // gulp-cli also emits plain 'error' sometimes
        case 'GULP_CLI_TASK_MISSING': {
          log.error?.(LOG_TAG_GULP, 'Task missing:', {task});
          break;
        }

        // gulp-cli also emits plain 'error' sometimes
        case 'error': {
          log.error?.(LOG_TAG_GULP, 'Error');
          break;
        }

        // Timestamp unneeded, logger timestamps itself
        case 'timestamp': {
          break;
        }

        default: {
          // Future-proof: log unknown tags at info with small, safe context
          log.info?.(LOG_TAG_GULP, tag, (task || file) ? { task, file } : undefined);
        }
      }

      // NEW (optional): cross-check our timing vs parsed CLI timing
      // if (tag === 'GULP_CLI_TASK_STOP' || tag === 'GULP_CLI_TASK_ERROR') {
      //   // Recompute once here so we have values for both STOP/ERROR paths.
      //   // calcTaskDuration → { ns, ns_ours, ns_cli, ... }
      //   const d = calcTaskDuration(task, dur);
      //   const ours_ns = d.ns_ours;

      //   if (ours_ns != null && d.ns) {
      //     const ratio = (ours_ns / d.ns).toFixed(2);
      //     log.info?.(LOG_TAG_GULP_TASK, 'Duration cross-check', {
      //       task, ratio, ours_ns, cli_ns: d.ns
      //     });
      //   }
      // }

    } catch (e) {
      log.debug?.(LOG_TAG_GULP_CLI, 'handleEvent fatal:', e?.message);
    }
  }

  // Attach & flush
  console.debug?.(LOG_TAG_GULP_CLI, 'about to attach consumer; typeof bus.attach =', typeof g.__GULPCLI_BUS__?.attach);

  g.__GULPCLI_BUS__.attach(handleEvent);
  log.debug?.(LOG_TAG_GULP_CLI, 'consumer attached; queued flushed');

  // LIVE SWALLOW TOGGLE
  const bus = g.__GULPCLI_BUS__;

  g.setGulpCliSwallow = (v) => {
    bus?.setSwallow?.(!!v);
    log.debug?.(LOG_TAG_GULP_CLI, 'setSwallow →', !!v);
  };
  g.toggleGulpCliSwallow = () => {
    bus?.toggle?.();
    log.debug?.(LOG_TAG_GULP_CLI, 'toggleSwallow');
  };

  // Initial swallow decision (safe default: show originals)
  let swallow = !!bus.swallow;
  const argv = process.argv.slice(2);
  if (argv.includes('--swallow-cli')) swallow = true;
  if (argv.includes('--no-swallow-cli')) swallow = false;
  if (process.env.CI === '1' && process.env.GULP_CLI_SWALLOW !== '0') swallow = true;

  swallow = true;
  bus.setSwallow?.(swallow);
  log.debug?.(LOG_TAG_GULP_CLI, 'swallow initial =', swallow);
  log.notice?.(LOG_TAG_GULP, `CLI swallow ${swallow ? 'ENABLED' : 'DISABLED'}`);
};

