'use strict';

/**
 * @fileoverview
 * cure-log-hub — process-wide root logger registry and caller-aware logger factory.
 *
 * Purpose:
 * - Provide a tiny “hub” that coordinates a single **root** Cure Log instance
 *   and returns **facade** loggers that forward method calls to either the root
 *   or a **dedicated** per-file instance. When no root exists, the hub can
 *   derive a caller-based log file path and adopt that instance as the root.
 *
 * Runtime & module system:
 * - Runtime: Node.js (LTS recommended).
 * - Module system: CommonJS (CJS). Safe to `require()` from other CJS modules.
 *
 * Side effects on import:
 * - **None**: importing this module only defines symbols and an in-memory cache.
 *   No filesystem or environment mutation occurs at import-time.
 *
 * Globals & environment:
 * - Uses Node globals: `globalThis`, `process`, `__dirname`.
 * - Stores root logger under a well-known symbol on `globalThis`.
 *
 * Security & permissions:
 * - No direct filesystem, network, or subprocess access.
 * - When constructing loggers, this module **requires** `../cure-log`. If that
 *   dependency is unavailable, a **console proxy** is used (best-effort behavior).
 * - File paths provided to {@link use} are normalized via `path.resolve` only;
 *   path validity and traversal safety (e.g., outside allowed dirs) must be
 *   enforced by the caller or by `cure-log` configuration.
 *
 * I/O contract:
 * - This hub does not open files itself. File I/O (e.g., writing logs) is
 *   performed by `cure-log` instances constructed by the hub.
 *
 * Concurrency & async:
 * - All APIs are synchronous. No timers/streams/backpressure.
 *
 * Compatibility:
 * - Works on any modern Node LTS; no ESM features, no native addons.
 *
 * Versioning policy:
 * - SemVer. New public APIs get `@since` tags; deprecations include `@deprecated`
 *   and suggested replacements in the symbol docs.
 *
 * @module cure-log-hub
 * @version 1.0.0
 * @since 1.0.0
 * @license MIT
 * @author Cure Interactive
 *
 * @example <caption>Public API signature (TypeScript-style)</caption>
 * ```ts
 * function setRoot(log: CureLogLike | null): CureLogLike | null;
 * function getRoot(): CureLogLike | null;
 * function use(opts?: UseOptions): CureLogLike; // facade
 * ```
 *
 * @example <caption>Basic usage with an existing root</caption>
 * const { setRoot, use } = require('./cure-log-hub');
 * const { Log } = require('../cure-log');
 * setRoot(new Log({ filePath: { log: '/abs/app.log' } }));
 * const log = use();
 * log.info('hello root');
 *
 * @example <caption>Dedicated per-file logger (does not track root swaps)</caption>
 * const { use } = require('./cure-log-hub');
 * const debugLog = use({ filePath: '/abs/debug.log' });
 * debugLog.debug('state', { x: 1 });
 *
 * @example <caption>No root yet → caller-derived fallback becomes root</caption>
 * const { use } = require('./cure-log-hub');
 * const first = use(); // derives `<callerDir>/_log/<callerBase>.log` and adopts as root
 * first.success('booted');
 */

const PATH = require('path');

/**
 * Minimal “log-like” shape that the hub can forward to. Real Cure Log exposes
 * more, but the facade can operate with any object that has these optional
 * methods; unknown methods are routed to `info → log → console.log`.
 *
 * @typedef {Object} CureLogLike
 * @property {Function} [trace]
 * @property {Function} [debug]
 * @property {Function} [info]
 * @property {Function} [log]
 * @property {Function} [warn]
 * @property {Function} [error]
 * @property {Function} [fatal]
 * @property {Function} [success]
 * @since 1.0.0
 */

/**
 * Options accepted by {@link use}.
 *
 * @typedef {Object} UseOptions
 * @property {string|null} [filePath=null]
 *   Absolute or relative log file path for a **dedicated** logger. When
 *   provided, the returned facade is bound to that dedicated instance and
 *   does **not** follow future root swaps.
 * @since 1.0.0
 */

/**
 * Symbol used to store the process-wide root logger on the global object.
 * Ownership & lifecycle:
 * - Owned by this module; value may be replaced by {@link setRoot}.
 * - Cleared by calling {@link setRoot} with `null`.
 *
 * @type {symbol}
 * @private
 * @since 1.0.0
 */
const ROOT_KEY = Symbol.for('cure.log.root');

/**
 * Cache of dedicated loggers keyed by **absolute** file path.
 * Invariants:
 * - For any absolute path `p`, `_getOrCreateDedicatedLogger(p)` returns a
 *   stable instance across calls for the lifetime of the process.
 *
 * @type {Map<string, CureLogLike>}
 * @private
 * @since 1.0.0
 */
const DEDICATED_CACHE = new Map(); // filePath(abs) -> Log instance

// =============================================================================
// Root management
// =============================================================================

/**
 * Set the process-wide root logger.
 *
 * Side effects:
 * - Stores the value on `globalThis` under a well-known symbol.
 *
 * @function setRoot
 * @param {CureLogLike|null} log
 *   A Cure Log instance (or console proxy), or `null` to clear the root.
 * @returns {CureLogLike|null}
 *   The value stored (echoed for convenience).
 * @since 1.0.0
 *
 * @example
 * const { Log } = require('../cure-log');
 * setRoot(new Log({ filePath: { log: '/abs/app.log' } }));
 */
function setRoot(log) {
  globalThis[ROOT_KEY] = log || null;
  return globalThis[ROOT_KEY];
}

/**
 * Get the current process-wide root logger, if any.
 *
 * @function getRoot
 * @returns {CureLogLike|null}
 *   The current root logger or `null` if not set.
 * @since 1.0.0
 *
 * @example
 * const root = getRoot();
 * if (root) root.info('root present');
 */
function getRoot() {
  return globalThis[ROOT_KEY] || null;
}

// =============================================================================
// Caller resolution
// =============================================================================

/**
 * @returns {string | null} Absolute path of the first external callsite file,
 * or `null` if none can be determined (never this file).
 * @private
 */
function _getCallerFile() {
  const originalPrepare = Error.prepareStackTrace;
  try {
    const err = new Error();

    // Ask V8 to give us structured CallSite objects instead of a string.
    Error.prepareStackTrace = (_, stack) => stack;

    // In JS with // @ts-check, assert the runtime type so the checker knows
    // stack[i] has getFileName().
    /** @type {NodeJS.CallSite[] | undefined} */
    const stack = /** @type {any} */ (err).stack;

    // Guard in case some runtime returned a string stack.
    if (!Array.isArray(stack)) {
      return null;
    }

    // Find the first frame not pointing at this file.
    for (const frame of stack) {
      const fileName = frame && typeof frame.getFileName === 'function'
        ? frame.getFileName()
        : null;

      if (fileName && fileName !== __filename) {
        return fileName;
      }
    }
  } catch (_) {
    // ignore and fall through to fallback
  } finally {
    // Always restore the original hook.
    Error.prepareStackTrace = originalPrepare;
  }
  return null; // fallback
}

// =============================================================================
// CureLog construction helpers
// =============================================================================

/**
 * Construct a Cure Log instance with the provided options, or a console proxy
 * if `../cure-log` cannot be required.
 *
 * Console proxy behavior:
 * - Methods mirror `console.*` and are bound. Unknown methods will still be
 *   synthesized by the facade to route to `info → log → console.log`.
 *
 * @param {object} [opts={}]
 *   Options forwarded to `new Log(opts)` in `cure-log`.
 *   Typically `{ filePath: { log, config? } }`.
 * @returns {CureLogLike}
 *   A Cure Log instance, or a console proxy with function properties bound.
 * @private
 * @since 1.0.0
 *
 * @example
 * const inst = _newCureLogInstance({ filePath: { log: '/abs/debug.log' } });
 */
function _newCureLogInstance(opts = {}) {
  // Try to construct your Cure Log; if unavailable, return console proxy
  try {
    const { Log } = require('../cure-log');
    return new Log(opts);
  } catch {
    return new Proxy({}, {
      get(_t, prop) {
        const v = console[prop];
        return typeof v === 'function' ? v.bind(console) : v;
      }
    });
  }
}

/**
 * Build a deterministic log file path based on the **external caller**.
 * Shape: `<callerDir>/_log/<callerBase>.log`
 *
 * Fallbacks:
 * - If the caller cannot be determined, use `process.cwd()` and `"app"` as base.
 *
 * @returns {string}
 *   Absolute path to a suggested log file for the caller.
 * @private
 * @since 1.0.0
 *
 * @example
 * const p = _buildCallerDerivedFilePath(); // "/abs/project/_log/my-file.log"
 */
function _buildCallerDerivedFilePath() {
  const callerFile = _getCallerFile();
  // Fallbacks if we can’t determine caller
  const baseDir = callerFile ? PATH.dirname(callerFile) : process.cwd();
  const baseName = callerFile ? PATH.basename(callerFile, PATH.extname(callerFile)) : 'app';
  const dir = PATH.resolve(baseDir, '_log');
  return PATH.join(dir, `${baseName}.log`);
}

/**
 * Get or create a **dedicated** Cure Log instance bound to an absolute file path.
 * Instances are cached so the same path always returns the same logger.
 *
 * @param {string} absFilePath
 *   Absolute path to the dedicated log file.
 * @returns {CureLogLike}
 *   The cached or newly constructed Cure Log instance.
 * @private
 * @since 1.0.0
 *
 * @example
 * const inst = _getOrCreateDedicatedLogger('/abs/debug.log');
 */
function _getOrCreateDedicatedLogger(absFilePath) {
  if (!DEDICATED_CACHE.has(absFilePath)) {
    const logger = _newCureLogInstance({ filePath: { log: absFilePath } });
    DEDICATED_CACHE.set(absFilePath, logger);
  }
  return DEDICATED_CACHE.get(absFilePath);
}

// =============================================================================
// Facades
// =============================================================================

/**
 * Prefix call arguments with `"[method]"` so unknown methods still produce useful
 * output when falling back to `.info()` / `.log()` / `console.log`.
 *
 * @param {PropertyKey} method
 *   The requested method name from the facade (e.g., `"trace"`, `"fatal"`).
 * @param {any[]} args
 *   Original arguments from the user.
 * @returns {any[]}
 *   Arguments with a leading label merged with the first string if applicable.
 * @private
 * @since 1.0.0
 */
function _prefixWithMethod(method, args) {
  const label = `[${String(method)}]`;
  if (args.length === 0) return [label];
  const [first, ...rest] = args;
  if (typeof first === 'string') return [`${label} ${first}`, ...rest];
  return [label, first, ...rest];
}

/**
 * Create a **live-binding facade** that forwards property accesses and calls
 * to the current target returned by `resolveTarget()`.
 *
 * Forwarding rules:
 * - If `base[prop]` is a function, return it **bound** (`fn.bind(base)`).
 * - Otherwise, synthesize a function that prefixes with `[prop]` and falls back
 *   to `info → log → console.log`.
 *
 * @param {() => CureLogLike|null} resolveTarget
 *   Getter that returns the current underlying logger instance.
 * @returns {CureLogLike}
 *   A `Proxy` suitable for use as a logger facade.
 * @private
 * @since 1.0.0
 */
function _makeFacade(resolveTarget) {
  // resolveTarget(): returns the current underlying logger object
  return new Proxy({}, {
    get(_t, prop) {
      const base = resolveTarget();
      if (!base) return undefined;

      const exact = base[prop];
      if (typeof exact === 'function') {
        return exact.bind(base);
      }

      const info = typeof base.info === 'function' ? base.info.bind(base) : null;
      const log  = typeof base.log  === 'function' ? base.log.bind(base)  : null;
      const con  = typeof console.log === 'function' ? console.log.bind(console) : () => {};

      return (...args) => {
        const out = _prefixWithMethod(prop, args);
        if (info) return info(...out);
        if (log)  return log(...out);
        return con(...out);
      };
    }
  });
}

