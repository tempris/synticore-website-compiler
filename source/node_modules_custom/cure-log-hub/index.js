/**
 * Obtain a **logger facade** bound to either:
 * 1) the process-wide **root** (if present and no `filePath` provided),
 * 2) a **dedicated** logger at `opts.filePath` (cached by absolute path), or
 * 3) a **caller-derived** fallback (adopted as new root by default).
 *
 * Defaults & side effects:
 * - `opts` defaults to an empty object.
 * - If no root exists and no `filePath` is provided, a caller-derived instance
 *   is created and **adopted as the root** (so future `use()` calls share it).
 *
 * Security:
 * - File path validation is not performed here beyond normalization; ensure
 *   `opts.filePath` points to an allowed location in your environment.
 *
 * @function use
 * @param {UseOptions} [opts={}]
 *   Optional: absolute or relative path for a dedicated log file. If provided,
 *   the facade binds to a stable instance for that path and does not track the root.
 * @returns {CureLogLike}
 *   A facade exposing logging methods. Unknown methods are prefixed and routed
 *   to `info → log → console.log` in that order.
 * @since 1.0.0
 *
 * @example <caption>Use root (already set elsewhere)</caption>
 * const log = use();
 * log.info('hello');
 *
 * @example <caption>Dedicated per-file logger</caption>
 * const debugLog = use({ filePath: '/abs/debug.log' });
 * debugLog.debug('state', { x: 1 });
 *
 * @example <caption>Caller-derived (no root yet)</caption>
 * const first = use(); // becomes root using caller-derived path
 * first.success('booted');
 */
function use(opts = {}) {
  const { filePath } = opts || {};

  // 1) Prefer the shared global root if it exists
  const root = getRoot();
  if (root) {
    return _makeFacade(() => getRoot());
  }

  // 2) If no root, but a manual filePath was provided, use a dedicated logger
  if (filePath) {
    const abs = PATH.resolve(filePath);
    const inst = _getOrCreateDedicatedLogger(abs);
    return _makeFacade(() => inst);
  }

  // 3) Otherwise, create a caller-derived fallback and (optionally) adopt it as root
  const derived = _buildCallerDerivedFilePath();
  const fallback = _newCureLogInstance({ filePath: { log: derived } });

  // Adopt the caller-derived instance as the process-wide root so that subsequent
  // `use()` calls without `filePath` get a stable, shared logger. Remove this line
  // if you prefer the fallback to remain **local-only** (no global root).
  setRoot(fallback);

  return _makeFacade(() => getRoot());
}

