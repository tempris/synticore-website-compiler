  /**
   * Attach process-level handlers (uncaughtException, unhandledRejection, warning) and
   * patch `process.emitWarning` to route through logger without duplicating Node’s warning banner.
   * Stores a detacher on `this._detachProcessHandlers`.
   *
   * @returns {void}
   * @private
   * @since 1.0.0
   */
  _attachProcessHandlers() {
    const LOG_TAG_LOG_CATCH = LOG_TAG_CURE_LOG + ' [🪝 Catch]';

    // If already attached, cleanly detach before re-attaching (idempotent)
    if (typeof this._detachProcessHandlers === 'function') {
      try { this._detachProcessHandlers(); } catch {}
    }

    const p = process;

    // ---- Stash originals / refs we’ll need to restore later
    const originalEmitWarning = p.emitWarning.bind(p);
    const origConsoleErrSaved = this._originalConsole?.error
      ? this._originalConsole.error
      : console.error.bind(console);

    // ---- Build handlers once, keep refs for .off()
    const onUncaught = (err) => {
      try { this.error('[🌋 Uncaught Exception]', err); }
      catch (e) { origConsoleErrSaved('Failed to log uncaughtException:', err, e); }
    };

    const onUnhandled = (reason, promise) => {
      try { this.error('[🤯 Unhandled Rejection]', { reason, promise }); }
      catch (e) { origConsoleErrSaved('Failed to log unhandledRejection:', reason, e); }
    };

    const onWarning = (warning) => {
      try {
        const msg    = typeof warning?.message === 'string' ? warning.message : String(warning?.message ?? '');
        const type   = warning?.name || warning?.type || 'Warning';
        const code   = warning?.code;
        const detail = warning?.detail;

        // Treat deprecations as debug
        const isDeprecation =
          /\bDeprecationWarning\b/i.test(type) ||
          /\bDeprecationWarning\b/i.test(msg)  ||
          (typeof code === 'string' && /^DEP\d+$/i.test(code));

        const payload = {
          type,
          ...(code   != null && { code }),
          message: msg,
          ...(detail != null && { detail }),
        };

        if (isDeprecation) {
          this.debug(payload);
        } else {
          this.warn(payload);
        }
      } catch (e) {
        try { origConsoleErrSaved('Failed to log process warning:', warning, e); } catch {}
      }
    };

    // ---- Attach listeners
    p.on('uncaughtException', onUncaught);
    p.on('unhandledRejection', onUnhandled);
    p.on('warning', onWarning);

    // ---- Override emitWarning: log first, then call original with banner bypass
    let bypassDepth = 0;

    const patchedEmitWarning = (warning, typeOrOptions, code, ctor) => {
      // 1) Structured log (covers all signatures)
      try {
        // Normalize a synthetic "type" and "code" across signatures
        let normType, normCode, normDetail, normMsg;

        if (warning instanceof Error) {
          normType = warning.name || 'Warning';
          normMsg  = warning.message || String(warning);
        } else if (typeOrOptions && typeof typeOrOptions === 'object') {
          const { type, code: optCode, detail } = typeOrOptions;
          normType   = type || 'Warning';
          normCode   = typeof optCode === 'string' ? optCode : undefined;
          normDetail = detail;
          normMsg    = String(warning);
        } else {
          normType = (typeof typeOrOptions === 'string') ? typeOrOptions : 'Warning';
          normCode = (typeof code === 'string') ? code : undefined;
          normMsg  = String(warning);
        }

        const isDeprecation =
          /\bDeprecationWarning\b/i.test(normType) ||
          /\bDeprecationWarning\b/i.test(normMsg)  ||
          (typeof normCode === 'string' && /^DEP\d+$/i.test(normCode));

        const base = {
          type: normType,
          ...(normCode && { code: normCode }),
          message: normMsg,
          ...(normDetail && { detail: normDetail }),
        };

        const method = isDeprecation ? 'debug' : 'warn';
        const fn = (this && typeof this[method] === 'function') ? this[method] : null;
        if (fn) {
          fn.call(this, base); // preserve logger `this`
        } else if (typeof this?.log === 'function') {
          this.log(base);
        }
      } catch { /* never block emission */ }

      // 2) Temporarily bypass patched console.error so Node’s banner doesn’t echo
      const currentPatchedErr = console.error;
      const wePatchedConsole = currentPatchedErr !== origConsoleErrSaved;

      if (wePatchedConsole) {
        bypassDepth += 1;
        if (bypassDepth === 1) {
          console.error = origConsoleErrSaved;
        }
      }

      try {
        return originalEmitWarning.call(process, warning, typeOrOptions, code, ctor);
      } finally {
        if (wePatchedConsole) {
          bypassDepth -= 1;
          if (bypassDepth === 0) {
            if (console.error === origConsoleErrSaved) {
              console.error = currentPatchedErr;
            }
          }
        }
      }
    };

    p.emitWarning = patchedEmitWarning;

    // ---- Single detacher (restores everything we touched)
    this._detachProcessHandlers = () => {
      try { p.off('uncaughtException', onUncaught); } catch {}
      try { p.off('unhandledRejection', onUnhandled); } catch {}
      try { p.off('warning', onWarning); } catch {}
      try { p.emitWarning = originalEmitWarning; } catch {}
      this._detachProcessHandlers = null;
    };
  }

