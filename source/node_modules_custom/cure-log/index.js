/**
 * Load a JSON file safely (BOM-tolerant). Returns {@link fallback} on error.
 *
 * @param {string} file
 * @param {any} [fallback=null]
 * @returns {any}
 * @private
 * @since 1.0.0
 */
function _readJSONSafe(file, fallback = null) {
  try {
    const raw = NODE_FS.readFileSync(file, 'utf8').replace(/^\uFEFF/, '');
    return JSON.parse(raw);
  } catch (_) {
    return fallback;
  }
}

/**
 * Find nearest existing file by walking upward from one or more start dirs.
 *
 * @param {string|string[]|null|undefined} startDirs
 * @param {string} relativePath
 * @returns {string|null}
 * @private
 * @since 1.1.0
 */
function _findNearestUpwardFile(startDirs, relativePath) {
  const starts = Array.isArray(startDirs) ? startDirs : [startDirs];
  const seen = new Set();

  for (const start of starts) {
    if (!start) continue;
    let dir = NODE_PATH.resolve(String(start));
    while (true) {
      const key = dir.toLowerCase();
      if (seen.has(key)) break;
      seen.add(key);

      const candidate = NODE_PATH.resolve(dir, relativePath);
      if (NODE_FS.existsSync(candidate)) return candidate;

      const parent = NODE_PATH.dirname(dir);
      if (!parent || parent === dir) break;
      dir = parent;
    }
  }

  return null;
}

