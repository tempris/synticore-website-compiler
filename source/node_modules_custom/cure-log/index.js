  /**
   * Patch `console.*` to forward through this logger (with reentrancy safeguard).
   * Stores a detacher on `this._detachConsoleHandlers`.
   * @returns {void}
   * @private
   * @since 1.0.0
   */
  _attachConsoleHandlers() {
    // If already attached, cleanly detach before re-attaching (idempotent)
    if (typeof this._detachConsoleHandlers === 'function') {
      try { this._detachConsoleHandlers(); } catch {}
    }

    const self = this;
    const NODE_UTIL = require('util');

    // ---- Stash originals (we restore exactly these in the detacher)
    const originals = {
      log:   console.log.bind(console),
      info:  console.info.bind(console),
      warn:  console.warn.bind(console),
      error: console.error.bind(console),
      debug: console.debug ? console.debug.bind(console) : console.log.bind(console),
      trace: console.trace ? console.trace.bind(console) : console.log.bind(console),
    };

    const levelMap = {
      log:   'log',
      info:  'info',
      debug: 'debug',
      warn:  'warn',
      error: 'error',
      trace: 'trace',
    };

    // Reentrancy / recursion guard
    let inPatchDepth = 0;

    function normalizeArgs(method, args) {
      const hardLevels = method === 'warn' || method === 'error' || method === 'trace';
      const out = [];

      for (const a of args) {
        if (hardLevels && a instanceof Error) {
          out.push({
            name: a.name || 'Error',
            message: a.message || String(a),
            stack: (a.stack && String(a.stack).split('\n')) || [],
          });
        } else {
          out.push(a);
        }
      }

      // If no Error provided on hard level, add a shallow callsite
      if (hardLevels && !args.some(a => a instanceof Error)) {
        const e = new Error(`Console.${method} callsite`);
        const stackLines = (e.stack && String(e.stack).split('\n')) || [];
        out.push({ callsite: stackLines.slice(1) });
      }

      return out;
    }

    const makePatched = (method) => {
      const defaultLevel = levelMap[method];
      const tag = `[🪝 Hook: console.${method}]`;

      const hasPrintfTokens = (arr) => (
        arr.length > 1 &&
        typeof arr[0] === 'string' &&
        /%[sdifjoOc%]/.test(arr[0])
      );

      const formatConsoleArgs = (arr) => {
        try {
          return NODE_UTIL.formatWithOptions(
            {
              colors: false,
              compact: true,
              breakLength: Infinity
            },
            ...arr
          );
        } catch (_) {
          return null;
        }
      };

      // helper: does any arg look like a deprecation banner or Node's advisory line?
      const isDeprecationArgs = (arr) => {
        try {
          for (const a of arr) {
            if (a instanceof Error) {
              if (/\bDeprecationWarning\b/i.test(a.name) || /\bDeprecationWarning\b/i.test(String(a.message))) return true;
              continue;
            }
            const s = typeof a === 'string' ? a : JSON.stringify(a);

            // Core deprecation signals
            if (/\bDeprecationWarning\b/i.test(s) || /\[DEP\d+\]/i.test(s)) return true;

            // Node’s advisory line printed after deprecations (stderr)
            // e.g. "(Use `node --trace-deprecation ...` to show where the warning was created)"
            if (/^\(Use `node --trace-deprecation\b/i.test(s)) return true;
            if (/\btrace-deprecation\b/i.test(s) && /\bwarning\b/i.test(s)) return true;
            if (/\bwhere the warning was created\b/i.test(s)) return true;
          }
        } catch (_) {}
        return false;
      };

      return function patchedConsoleMethod(...args) {
        // Prevent infinite loops if the logger internally uses console.*
        if (inPatchDepth > 8) {
          return originals[method](...args);
        }

        inPatchDepth++;

        try {
          const useDebug = isDeprecationArgs(args);

          // 1) Preferred: call the matching CureLog method with raw args; downgrade deprecations to debug
          if (typeof self[useDebug ? 'debug' : defaultLevel] === 'function') {
            if (hasPrintfTokens(args)) {
              const formatted = formatConsoleArgs(args);
              if (formatted != null) {
                self[useDebug ? 'debug' : defaultLevel](tag, formatted);
                return;
              }
            }

            self[useDebug ? 'debug' : defaultLevel](tag, ...args);
            return;
          }

          // 2) Fallback to a generic CureLog log() if the specific level isn't available
          if (typeof self.log === 'function') {
            self.log(tag, ...args);
            return;
          }

          // 3) Last resort: use the original console
          originals[method](tag, ...args);
        } catch (e) {
          // Never lose the user's message: report the patch error, then echo the original call
          try { originals.error('[❌ PatchError]', `console.${method} hook failed:`, e); } catch {}
          try { originals[method](...args); } catch {}
        } finally {
          inPatchDepth--;
        }
      };
    };

    // ---- Install patches
    const patched = {
      log:   makePatched('log'),
      info:  makePatched('info'),
      warn:  makePatched('warn'),
      error: makePatched('error'),
      debug: makePatched('debug'),
      trace: makePatched('trace'),
    };

    console.log   = patched.log.bind(this);
    console.info  = patched.info.bind(this);
    console.warn  = patched.warn.bind(this);
    console.error = patched.error.bind(this);
    console.debug = patched.debug.bind(this);
    console.trace = patched.trace;

    // ---- Single detacher (mirrors process hook style)
    this._detachConsoleHandlers = () => {
      try { console.log   = originals.log;   } catch {}
      try { console.info  = originals.info;  } catch {}
      try { console.warn  = originals.warn;  } catch {}
      try { console.error = originals.error; } catch {}
      try { console.debug = originals.debug; } catch {}
      try { console.trace = originals.trace; } catch {}
      this._detachConsoleHandlers = null;
    };
  }

