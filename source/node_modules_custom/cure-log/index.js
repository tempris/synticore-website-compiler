/**
 * Build a toggle map with all known methods enabled.
 * @returns {LogTypeToggleMap}
 * @private
 * @since 1.0.0
 */
function _defaultToggleMap() {
  return {
    trace: true,
    fatal: true,
    error: true,
    warn: true,
    log: true,
    info: true,
    detail: true,
    debug: true,
    notice: true,
    success: true,
    begin: true,
    end: true,
    init: true,
    shutdown: true
  };
}

/**
 * Overlay a base toggle map with an input object’s boolean values.
 * @param {Partial<LogTypeToggleMap>} input
 * @param {LogTypeToggleMap} fallbackMap
 * @returns {LogTypeToggleMap}
 * @private
 * @since 1.0.0
 */
function _normalizeToggle(input, fallbackMap) {
  const base = { ...fallbackMap };
  if (!input || typeof input !== 'object') return base;
  for (const k of Object.keys(base)) {
    if (input[k] !== undefined) base[k] = Boolean(input[k]);
  }
  return base;
}

/**
 * Normalize timestamp config with sane defaults.
 * @param {any} input
 * @param {{format:string, zone:'local'|'utc', locale:string}} fallback
 * @returns {{format:string, zone:'local'|'utc', locale:string}}
 * @private
 * @since 1.1.0
 */
function _normalizeTimestamp(input, fallback = { format: 'iso', zone: 'utc', locale: 'auto' }) {
  const source = (input && typeof input === 'object') ? input : {};
  const format = (typeof source.format === 'string' && source.format.trim())
    ? source.format.trim()
    : fallback.format;
  const zone = String(source.zone ?? fallback.zone).toLowerCase() === 'local' ? 'local' : 'utc';
  const locale = (typeof source.locale === 'string' && source.locale.trim())
    ? source.locale.trim()
    : fallback.locale;
  return { format, zone, locale };
}

/**
 * Normalize a raw config-like object into a full {@link LogConfig}.
 * @param {any} [input={}]
 * @returns {LogConfig}
 * @private
 * @since 1.0.0
 */
function _normalizeConfig(input = {}) {
  function normalizeList(val) {
    if (!val) return [];
    if (Array.isArray(val)) return val;
    if (typeof val === 'string') return [val];
    throw new Error("Filter include/exclude must be string or array of strings");
  }

  const typeTogglesConsole = _normalizeToggle(input?.type?.console, _defaultToggleMap());
  const typeTogglesFile    = _normalizeToggle(input?.type?.file,    _defaultToggleMap());
  const timestamp = _normalizeTimestamp(input?.timestamp);

  return {
    debug: Boolean(input.debug ?? false),
    level: {
      console: _levelFromName(input.level?.console ?? 'DETAIL'),
      file:    _levelFromName(input.level?.file    ?? 'ALL')
    },
    // optional per-output type toggles (console/file)
    type: {
      console: typeTogglesConsole,
      file:    typeTogglesFile
    },
    filter: {
      include: normalizeList(input.filter?.include),
      exclude: normalizeList(input.filter?.exclude)
    },
    timestamp
  };
}

/**
 * Merge two configs by normalizing the result (preserves thresholds, toggles, filters).
 * @param {LogConfig} base
 * @param {Partial<LogConfig>} [override={}]
 * @returns {LogConfig}
 * @private
 * @since 1.0.0
 */
function _mergeConfig(base, override = {}) {
  return _normalizeConfig({
    debug:     (override.debug          !== undefined ? override.debug           : base.debug),
    level: {
      console: (override.level?.console !== undefined ? override.level.console   : base.level.console),
      file:    (override.level?.file    !== undefined ? override.level.file      : base.level.file),
    },
    type: {
      console: (override.type?.console !== undefined ? override.type.console : base.type.console),
      file:    (override.type?.file    !== undefined ? override.type.file    : base.type.file)
    },
    filter: {
      include: (override.filter?.include !== undefined ? override.filter.include : base.filter.include),
      exclude: (override.filter?.exclude !== undefined ? override.filter.exclude : base.filter.exclude),
    },
    timestamp: (override.timestamp !== undefined ? override.timestamp : base.timestamp)
  });
}

