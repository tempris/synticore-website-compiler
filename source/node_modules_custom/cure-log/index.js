  /**
   * @returns {string} Absolute path of the first external callsite file,
   * or this file as a fallback.
   * @private
   */
  _getCallerFile() {
    const originalPrepare = Error.prepareStackTrace;
    try {
      const err = new Error();

      // Ask V8 to give us structured CallSite objects instead of a string.
      Error.prepareStackTrace = (_, stack) => stack;

      // In JS with // @ts-check, assert the runtime type so the checker knows
      // stack[i] has getFileName().
      /** @type {NodeJS.CallSite[] | undefined} */
      const stack = /** @type {any} */ (err).stack;

      // Guard in case some runtime returned a string stack.
      if (!Array.isArray(stack)) {
        return __filename;
      }

      // Find the first frame not pointing at this file.
      for (const frame of stack) {
        const fileName = frame && typeof frame.getFileName === 'function'
          ? frame.getFileName()
          : null;

        if (fileName && fileName !== __filename) {
          return fileName;
        }
      }
    } catch (_) {
      // ignore and fall through to fallback
    } finally {
      // Always restore the original hook.
      Error.prepareStackTrace = originalPrepare;
    }
    return __filename; // fallback
  }

  /** Remove ANSI escape codes from a string. @param {string} input @returns {string} @private */
