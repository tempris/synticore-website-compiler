/**
 * Build a single message string in the same way the logger joins args, then strip ANSI.
 * Used solely for include/exclude filter decisions.
 *
 * @param {Function} formatArgFn
 * @param {Function} joinArgsFn
 * @param {Function} ansiRemoveFn
 * @param {any[]} args
 * @returns {string}
 * @private
 * @since 1.0.0
 */
function _buildFullTextForFilter(formatArgFn, joinArgsFn, ansiRemoveFn, args) {
  // Use the same join formatting your logger uses, then strip ANSI
  const joined = joinArgsFn.apply(null, args);
  return ansiRemoveFn(joined);
}

/**
 * Decide if a full (de-ANSI’d) message passes include/exclude substring filters.
 *
 * @param {string} fullText
 * @param {string[]} includeList
 * @param {string[]} excludeList
 * @returns {boolean}
 * @private
 * @since 1.0.0
 */
function _passesFiltersFullText(fullText, includeList, excludeList) {
  // Include: if non-empty, require one substring present
  const includeOk = includeList.length
    ? includeList.some((needle) => fullText.includes(needle))
    : true;
  if (!includeOk) return false;

  // Exclude: reject if any substring present
  const excludeHit = excludeList.length
    ? excludeList.some((needle) => fullText.includes(needle))
    : false;
  return !excludeHit;
}

