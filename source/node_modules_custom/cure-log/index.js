  /**
   * Emit a message **now**, and also queue it to be re-emitted at process exit
   * with a reference to the original entry number.
   * @param {string} functionName
   * @param {...any} args
   * @returns {void}
   * @since 1.0.0
   */
  replayOnExit(functionName, ...args) {
    if (this._closed) return;

    const fn = this[functionName];

    if (typeof fn !== 'function') {
      this.warn(
        LOG_TAG_LOG_REPLAY,
        'Skipping queued message: no such log method:',
        {functionName}
      );
      return;
    }

    fn.call(this, LOG_TAG_REPLAY, ...args);

    // Emit the original message now (so it gets a real [Entry N])
    const beforeIndex = this._entryIndex;
    this[functionName](
      ...args,
      "\nNOTICE: (Queued this message to be replayed on script exit)"
    );

    // If a new entry was created, capture its number; otherwise null
    const originalEntry = (this._entryIndex !== beforeIndex) ? this._entryIndex : null;

    // Store for exit replay (keep the original args; omit the notice on replay)
    this._messageQueue.push({ functionName, args, originalEntry });
  }

  /**
   * Route a stream to Cure-Log with auto severity using Node's readline.
   * Safer than manual slicing, handles CRLF/ANSI/multibyte cleanly.
   * @param {NodeJS.ReadableStream | null | undefined} stream
   * @param {'stdout'|'stderr'} source
   * @param {string} tag
   * @since 1.1.0
   */
  routeStream(stream, source, tag) {
    if (!stream || typeof stream.on !== 'function') return;
    // const NODE_READLINE = NODE_CURE_MODULE.loadSync('readline', { preferDefault: true });
    const NODE_READLINE = require('readline');
    const rl = NODE_READLINE.createInterface({ input: stream, crlfDelay: Infinity });

    /**
     * Classify a single CLI output line into a log level.
     * Heuristic: reserve "error" for hard signals; treat stderr as warn by default.
     * @param {string} line   One logical line (ANSI allowed).
     * @param {'stdout'|'stderr'} source
     * @returns {'debug'|'info'|'warn'|'error'}
     * @private
     */
    function _classifySeverity(line, source) {
      // Hard error signals seen across npm/pnpm/yarn/git/node/curl/etc.
      if (/\b(error|err!|fatal|traceback|exception|failed|fail|abort(ed)?|E[A-Z_]{3,})\b/i.test(line)) {
        return 'error';
      }
      // npm/yarn/pnpm warning conventions
      if (/^\s*(npm|yarn|pnpm)\s+WARN\b/i.test(line)) return 'warn';
      // deprecations & advisories
      if (/\b(deprecated|deprecation|audit|advisory|funding|deprecate)\b/i.test(line)) return 'warn';
      // progress/noise → keep it lower unless it contains scary words
      if (/\b(progress|fetch|downloading|resolving|installing|updating|checking|scanning)\b/i.test(line)) {
        // return source === 'stderr' ? 'info' : 'info';
        return 'info';
      }
      // Default: stdout=info, stderr=warn
      return source === 'stderr' ? 'debug' : 'info';
    }

    /**
     * Emit to Cure-Log with correct `this` binding and safe fallbacks.
     * @param {'debug'|'info'|'warn'|'error'} level
     * @param {string} tag
     * @param {unknown} message
     * @private
     */
    const _route = (level, tag, message) => {
      /** @type {(msgTag:string, msg:string)=>void} */
      const fn = (typeof this[level] === 'function' ? this[level] : this.info).bind(this);
      // stringify non-strings defensively (avoid circulars)
      let line;
      if (typeof message === 'string') {
        line = message;
      } else {
        try { line = JSON.stringify(message); }
        catch { line = String(message); }
      }
      fn(tag, line);
    }

    rl.on('line', (line) => {
      try {
        const level = _classifySeverity(line, source);
        _route(level, tag, `[${source}] ${line}`);
      } catch (e) {
        _route('warn', tag, `Logging pipeline error while processing line: ${e && e.message || e}`);
      }
    });

    rl.on('error', (e) => _route('warn', tag, `readline error on ${source}: ${e}`));
    rl.on('close', () => { /* final lines already handled */ });
  }

