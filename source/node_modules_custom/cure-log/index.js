/**
 * Convert a level name or number to a numeric level with fallback.
 * @param {string|number} nameOrNum
 * @param {number} [fallback=LOG_LEVEL.DETAIL]
 * @returns {number}
 * @private
 * @since 1.0.0
 */
function _levelFromName(nameOrNum, fallback = LOG_LEVEL.DETAIL) {
  if (typeof nameOrNum === 'number') return nameOrNum;
  const key = String(nameOrNum || '').toUpperCase();
  return LOG_LEVEL[key] ?? fallback;
}

/**
 * Format a level value as `"num (NAME)"` for diagnostics.
 * @param {string|number} levelValue
 * @returns {string}
 * @private
 * @since 1.0.0
 */
function _formatLevel(levelValue) {
  const num = typeof levelValue === 'number' ? levelValue : _levelFromName(levelValue);
  const name = LOG_LEVEL_NAME[num] ?? String(levelValue);
  return `${num} (${name})`;
}

/**
 * Deep sort object keys (arrays mapped; primitives returned as-is).
 * @param {any} obj
 * @returns {any}
 * @private
 * @since 1.0.0
 */
function _sortKeysDeep(obj) {
  if (Array.isArray(obj)) {
    return obj.map(_sortKeysDeep);
  } else if (obj && typeof obj === 'object') {
    return Object.keys(obj)
      .sort()
      .reduce((acc, key) => {
        acc[key] = _sortKeysDeep(obj[key]);
        return acc;
      }, {});
  }
  return obj;
}

/**
 * Produce a presentation-friendly config snapshot (levels rendered as `"num (NAME)"`).
 * @param {Partial<LogConfig>} config
 * @returns {Partial<LogConfig>}
 * @private
 * @since 1.0.0
 */
function _formatConfig(config) {
  if (!config || typeof config !== 'object') return config;

  const out = {
    ...config,
    level: {
      ...config.level,
      console: _formatLevel(config.level?.console),
      file:    _formatLevel(config.level?.file)
    }
  };

  return _sortKeysDeep(out);
}

/**
 * Build a filesystem-safe timestamp for log filenames.
 * Format: YYYYMMDD-HHMMSS-MMM
 *
 * @param {Date} [date=new Date()]
 * @returns {string}
 * @private
 * @since 1.0.0
 */
function _fileSafeTimestamp(date = new Date()) {
  const y = date.getFullYear();
  const mo = String(date.getMonth() + 1).padStart(2, '0');
  const d = String(date.getDate()).padStart(2, '0');
  const h = String(date.getHours()).padStart(2, '0');
  const mi = String(date.getMinutes()).padStart(2, '0');
  const s = String(date.getSeconds()).padStart(2, '0');
  const ms = String(date.getMilliseconds()).padStart(3, '0');
  return `${y}${mo}${d}-${h}${mi}${s}-${ms}`;
}

/**
 * Derive a run-specific log path from a base log path.
 *
 * @param {string} baseLogPath
 * @param {Date} [date=new Date()]
 * @returns {string}
 * @private
 * @since 1.0.0
 */
function _buildRunLogPath(baseLogPath, date = new Date()) {
  const parsed = NODE_PATH.parse(baseLogPath);
  const ext = parsed.ext || '.log';
  const stamp = _fileSafeTimestamp(date);
  return NODE_PATH.join(parsed.dir, `${parsed.name}-${stamp}${ext}`);
}

