/**
 * Convert all backslashes to forward slashes.
 *
 * Semantics & side effects:
 * - Does not touch the filesystem; purely transforms the input string.
 * - Idempotent if the input already uses `/`.
 *
 * @function slashForward
 * @memberof module:cure-path
 * @param {string} path - Any path string (absolute or relative).
 * @returns {string} The same path using only `/` separators.
 * @throws {TypeError} If `path` is not a string.
 * @since 1.1.0
 *
 * @example
 * const { slashForward } = require('@custom/cure-path');
 * slashForward('C:\\Users\\me\\file.txt'); // 'C:/Users/me/file.txt'
 */
function slashForward(path) {
  if (typeof path !== 'string') throw new TypeError('slashForward: path must be a string');
  return path.replace(/\\/g, '/');
}

/**
 * Join path segments and normalize separators to `/`.
 *
 * Semantics & side effects:
 * - Delegates to `path.join`, then converts `\` → `/`.
 *
 * @function join
 * @memberof module:cure-path
 * @param {...string} paths - Path segments to join.
 * @returns {string} Joined path with `/` separators.
 * @throws {TypeError} From `path.join` when any segment is not a string.
 * @since 1.1.0
 *
 * @example
 * const { join } = require('@custom/cure-path');
 * join('a', 'b', 'c'); // 'a/b/c'
 */
function join(...paths) {
  return slashForward(NODE_PATH.join(...paths));
}

/**
 * Resolve path segments to an absolute path and normalize separators to `/`.
 *
 * Semantics & side effects:
 * - Delegates to `path.absolute`, then converts `\` → `/`.
 *
 * @function absolute
 * @memberof module:cure-path
 * @param {...string} paths - Path segments to absolute.
 * @returns {string} Absolute path with `/` separators.
 * @throws {TypeError} From `path.absolute` when any segment is not a string.
 * @since 1.1.0
 *
 * @example
 * const { absolute } = require('@custom/cure-path');
 * absolute('..', 'project'); // '/abs/path/project' (platform-specific root)
 */
function absolute(...paths) {
  return slashForward(NODE_PATH.resolve(...paths));
}

/**
 * Compute a relative path from `from` to `to` and normalize separators to `/`.
 *
 * Semantics & side effects:
 * - Delegates to `path.relative`, then converts `\` → `/`.
 *
 * @function relative
 * @memberof module:cure-path
 * @param {string} from - Base path.
 * @param {string} to - Target path.
 * @returns {string} Relative path with `/` separators.
 * @throws {TypeError} From `path.relative` when args are not strings.
 * @since 1.1.0
 *
 * @example
 * const { relative } = require('@custom/cure-path');
 * relative('/a/b', '/a/b/c/d'); // 'c/d'
 */
function relative(from, to) {
  return slashForward(NODE_PATH.relative(from, to));
}

/**
 * Split a path string (after normalizing to `/`) into components.
 *
 * Semantics & side effects:
 * - Normalizes with {@link module:cure-path.slashForward} before splitting.
 * - Does **not** remove empty components; callers may `filter(Boolean)` if desired.
 *
 * @function split
 * @memberof module:cure-path
 * @param {string} path - Any path string.
 * @returns {string[]} Array of path segments.
 * @throws {TypeError} If `path` is not a string.
 * @since 1.1.0
 *
 * @example
 * const { split } = require('@custom/cure-path');
 * split('a\\b/c'); // ['a','b','c']
 */
function split(path) {
  if (typeof path !== 'string') throw new TypeError('split: path must be a string');
  return slashForward(path).split('/');
}

