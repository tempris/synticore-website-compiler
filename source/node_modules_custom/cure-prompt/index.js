'use strict';

/**
 * @fileoverview
 *
 * cure-prompt — minimal, consistent CLI prompt helper with Cure Log integration.
 *
 * Purpose:
 * - Provide a small, dependency-free wrapper around Node's `readline` to ask a single
 *   question on STDIN/STDOUT and resolve the reply as a **normalized** string.
 * - Emit structured, emoji-tagged logs via Cure Log Hub so prompts are visible
 *   in both CLI and file logs with consistent tags.
 *
 * Runtime & module system:
 * - Runtime: Node.js ≥ 14
 * - Module system: CommonJS (CJS)
 *
 * Side effects on import:
 * - Initializes a Cure Log Hub logger and computes a module-scoped log file path.
 *   No console output occurs unless {@link module:cure-prompt.promptUser} is called.
 *
 * Concurrency & async:
 * - Each call to {@link module:cure-prompt.promptUser} creates its own `readline.Interface`
 *   and tears it down after the question is answered. Safe to call sequentially. For parallel
 *   prompts on the same TTY, prefer sequencing to avoid interleaved output.
 *
 * Security & permission assumptions:
 * - Reads from `process.stdin` and writes to `process.stdout`. No elevated permissions required.
 *
 * @module cure-prompt
 * @version 1.0.0
 * @since 1.0.0
 * @license MIT
 * @author Cure Interactive
 */

// =============================================================================
// Dependencies
// =============================================================================

const NODE_FS = require('fs');
const NODE_PATH = require('path');
const NODE_READLINE = require('readline');

// =============================================================================
// Private utilities
// =============================================================================

/**
 * Resolve a path relative to the repository root (two levels up from this file).
 *
 * @private
 * @function _getRootPath
 * @memberof module:cure-prompt
 * @param {string} [path=''] - Path to append to the root.
 * @default ''
 * @returns {string} Absolute path under the repo root.
 * @since 1.0.0
 */
function _getRootPath(path = '') {
  return NODE_PATH.resolve(__dirname, '../../../', path);
}

// =============================================================================
// Log - Begin
// =============================================================================

// -----------------------------------------------------------------------------
// Log (Cure Log Hub)
// -----------------------------------------------------------------------------

/** @private */
const NODE_CURE_LOG_HUB = require('@custom/cure-log-hub');

/**
 * Process-wide logger via Cure Log Hub.
 * Exposes methods such as `init`, `begin`, `info`, `detail`, `debug`, `notice`, `warn`, `error`, `success`, `end`, `shutdown`.
 * @type {Record<string, Function>}
 * @since 1.0.0
 */
const log = NODE_CURE_LOG_HUB.use({
  filePath: NODE_PATH.resolve(_getRootPath('_log'), NODE_PATH.parse(__filename).name, '.log')
});

// -----------------------------------------------------------------------------
// Log Tag
// -----------------------------------------------------------------------------

/** Emoji-tagged module tag for log lines in this module. @type {string} @since 1.0.0 */
const LOG_TAG_PROMPT_USER = '[⌨️ Prompt User]';

// =============================================================================
// Log - End
// =============================================================================

// =============================================================================
// Public API
// =============================================================================

/**
 * Ask the user a single question on STDOUT and resolve their answer.
 *
 * Behavior:
 * - Prints the prompt using Cure Log at `notice` level (for visibility in files/CLI).
 * - Waits for a single line of input from STDIN.
 * - Logs both the raw and normalized answers at `debug` level.
 * - Resolves the **normalized** string: `trim().toLowerCase()`.
 *
 * Normalization:
 * - `actual`: The verbatim text the user typed (including case and surrounding whitespace).
 * - `interpreted`: `actual.trim().toLowerCase()`.
 * - The returned value is the `interpreted` form.
 *
 * Limitations:
 * - Designed for single-question flows. For complex multi-step forms, compose multiple calls
 *   or adopt a higher-level wrapper that sequences prompts.
 *
 * @async
 * @function promptUser
 * @memberof module:cure-prompt
 * @param {string} question - The question to show the user (no trailing space added).
 * @returns {Promise<string>} Resolves to the lowercased, trimmed input.
 * @throws {TypeError} If `question` is not a non-empty string.
 * @since 1.0.0
 *
 * @example
 * // Basic (top-level await supported in ESM; in CJS wrap in an async IIFE)
 * (async () => {
 *   const answer = await promptUser('Proceed with deployment? [y/N]');
 *   if (['y', 'yes'].includes(answer)) {
 *     // …deploy
 *   } else {
 *     // …abort
 *   }
 * })();
 *
 * @example
 * // Interpreting common yes/no patterns
 * const yn = await promptUser('Continue? (yes/no)');
 * const isYes = /^(y|yes|true|1)$/i.test(yn); // note: yn is already lowercased/trimmed
 *
 * @example
 * // Chaining multiple prompts
 * const name = await promptUser('Your name:');
 * const env  = await promptUser('Target env (dev|staging|prod):');
 * log.info('[🧪 Example]', 'Collected values', { name, env });
 */
function promptUser(question) {
  return new Promise((resolve) => {
    const rl = NODE_READLINE.createInterface({
      input: process.stdin,
      output: process.stdout
    });

    log.notice(LOG_TAG_PROMPT_USER, question);

    rl.question('', (answer) => {
      rl.close();
      const PROMPT_INPUT_INTERPRETATION = answer.trim().toLowerCase();
      log.debug(LOG_TAG_PROMPT_USER, 'User Response:', {
        "Actual": answer,
        "Interpreted": PROMPT_INPUT_INTERPRETATION
      });
      resolve(PROMPT_INPUT_INTERPRETATION);
    });
  });
}

// =============================================================================
// Exports
// =============================================================================

module.exports = {
  /**
   * Ask a question on STDOUT and resolve the normalized answer.
   *
   * @see module:synticore-prompt~promptUser
   * @since 1.0.0
   */
  promptUser,

  /**
   * The emoji-tagged log label used for prompt-related log entries.
   *
   * Exposed so callers can reuse the same visual tag in related logs for
   * consistent grouping.
   *
   * @type {string}
   * @since 1.0.0
   * @example
   * log.info(LOG_TAG_PROMPT_USER, 'Starting interactive setup…');
   */
  LOG_TAG_PROMPT_USER
};

// =============================================================================
// Notes & Rationale
// =============================================================================
// - We deliberately normalize the returned value to reduce boilerplate in typical
//   yes/no or small choice prompts. If the raw text is needed, capture from logs
//   (debug level) or adapt this helper to return both forms.
// - The logger’s file path nests under `_log/<file-basename>/.log` for parity with
//   other custom modules and to keep per-module logs isolated.
// - For unit testing, inject a fake TTY by temporarily replacing `process.stdin`/`stdout`
//   or by wrapping the core logic into a helper that accepts streams. For simplicity,
//   we keep the direct `readline` usage here.
// =============================================================================
