/**
 * Escape a string so it can be used literally inside a RegExp source.
 *
 * Escapes metacharacters: - / \ ^ $ * + ? . ( ) | [ ] { }
 *
 * @function escape
 * @memberof module:cure-regex
 * @param {string} input - Arbitrary string to escape.
 * @returns {string} Escaped string safe for RegExp source.
 * @throws {TypeError} If `input` is not a string.
 * @since 1.0.0
 */
function escape(input) {
  if (typeof input !== 'string') throw new TypeError('escape: input must be a string');
  // eslint-disable-next-line no-useless-escape
  return input.replace(/[-\/\\^$*+?.()|[\]{}]/g, '\\$&');
}

module.exports = { escape };

