const DATETIME_TOKENS = [
  'YYYY', 'zzzz', 'dddd', 'MMMM', 'MMM', 'SSS', 'ddd',
  'YY', 'MM', 'DD', 'HH', 'hh', 'mm', 'ss', 'ZZ',
  'M', 'D', 'H', 'h', 'm', 's', 'A', 'a', 'Z', 'z'
];

const MONTHS_EN_SHORT = ['Jan', 'Feb', 'Mar', 'Apr', 'May', 'Jun', 'Jul', 'Aug', 'Sep', 'Oct', 'Nov', 'Dec'];
const MONTHS_EN_LONG = ['January', 'February', 'March', 'April', 'May', 'June', 'July', 'August', 'September', 'October', 'November', 'December'];

function _pad(num, len = 2) {
  return String(num).padStart(len, '0');
}

function _regexEscape(text) {
  return String(text).replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

function _isValidDate(date) {
  return date instanceof Date && Number.isFinite(date.getTime());
}

function _normalizeZone(zone) {
  return String(zone || 'local').toLowerCase() === 'utc' ? 'utc' : 'local';
}

function _resolveLocale(locale) {
  const raw = String(locale == null ? 'auto' : locale).trim();
  if (!raw || raw.toLowerCase() === 'auto') {
    try {
      return Intl.DateTimeFormat().resolvedOptions().locale || 'en-US';
    } catch (_) {
      return 'en-US';
    }
  }
  return raw;
}

function _normalizeDateTimeInput(input) {
  if (input instanceof Date) return new Date(input.getTime());
  if (typeof input === 'number' && Number.isFinite(input)) return new Date(input);
  if (typeof input === 'bigint') return new Date(Number(input));
  if (typeof input === 'string') {
    const d = new Date(input);
    return _isValidDate(d) ? d : null;
  }
  return null;
}

function _tokenizeFormat(format) {
  const src = String(format || '');
  const out = [];
  let i = 0;

  while (i < src.length) {
    if (src[i] === '[') {
      const end = src.indexOf(']', i + 1);
      if (end !== -1) {
        out.push({ type: 'literal', value: src.slice(i + 1, end) });
        i = end + 1;
        continue;
      }
    }

    let hit = null;
    for (const token of DATETIME_TOKENS) {
      if (src.slice(i, i + token.length) === token) {
        hit = token;
        break;
      }
    }
    if (hit) {
      out.push({ type: 'token', value: hit });
      i += hit.length;
      continue;
    }

    out.push({ type: 'literal', value: src[i] });
    i += 1;
  }

  return out;
}

function _localOffsetMinutes(date, zone) {
  if (zone === 'utc') return 0;
  return -date.getTimezoneOffset();
}

function _offsetToText(offsetMinutes, withColon) {
  const sign = offsetMinutes >= 0 ? '+' : '-';
  const abs = Math.abs(offsetMinutes);
  const hh = _pad(Math.floor(abs / 60), 2);
  const mm = _pad(abs % 60, 2);
  return withColon ? `${sign}${hh}:${mm}` : `${sign}${hh}${mm}`;
}

function _getParts(date, zone) {
  if (zone === 'utc') {
    return {
      year: date.getUTCFullYear(),
      month: date.getUTCMonth() + 1,
      day: date.getUTCDate(),
      hour24: date.getUTCHours(),
      minute: date.getUTCMinutes(),
      second: date.getUTCSeconds(),
      ms: date.getUTCMilliseconds()
    };
  }

  return {
    year: date.getFullYear(),
    month: date.getMonth() + 1,
    day: date.getDate(),
    hour24: date.getHours(),
    minute: date.getMinutes(),
    second: date.getSeconds(),
    ms: date.getMilliseconds()
  };
}

function _weekdayName(date, locale, zone, width) {
  try {
    const fmt = new Intl.DateTimeFormat(locale || 'en-US', {
      weekday: width,
      timeZone: zone === 'utc' ? 'UTC' : undefined
    });
    return fmt.format(date);
  } catch (_) {
    const short = ['Sun', 'Mon', 'Tue', 'Wed', 'Thu', 'Fri', 'Sat'];
    const long = ['Sunday', 'Monday', 'Tuesday', 'Wednesday', 'Thursday', 'Friday', 'Saturday'];
    const idx = zone === 'utc' ? date.getUTCDay() : date.getDay();
    return width === 'long' ? long[idx] : short[idx];
  }
}

function _monthName(date, locale, zone, width) {
  try {
    const fmt = new Intl.DateTimeFormat(locale || 'en-US', {
      month: width,
      timeZone: zone === 'utc' ? 'UTC' : undefined
    });
    return fmt.format(date);
  } catch (_) {
    const idx = zone === 'utc' ? date.getUTCMonth() : date.getMonth();
    return width === 'long' ? MONTHS_EN_LONG[idx] : MONTHS_EN_SHORT[idx];
  }
}

function _timeZoneName(date, locale, zone, width) {
  try {
    const fmt = new Intl.DateTimeFormat(locale || 'en-US', {
      timeZone: zone === 'utc' ? 'UTC' : undefined,
      timeZoneName: width === 'long' ? 'long' : 'short'
    });
    const parts = fmt.formatToParts(date);
    const part = parts.find(p => p.type === 'timeZoneName');
    if (part && part.value) return part.value;
  } catch (_) {}

  if (zone === 'utc') return 'UTC';
  return _offsetToText(_localOffsetMinutes(date, zone), true);
}

function _formatDateByTokens(date, format, zone, locale) {
  const parts = _getParts(date, zone);
  const hour12 = (parts.hour24 % 12) || 12;
  const meridiemUpper = parts.hour24 >= 12 ? 'PM' : 'AM';
  const meridiemLower = meridiemUpper.toLowerCase();
  const offsetMin = _localOffsetMinutes(date, zone);
  const tokens = _tokenizeFormat(format);

  return tokens.map((entry) => {
    if (entry.type === 'literal') return entry.value;

    switch (entry.value) {
      case 'YYYY': return _pad(parts.year, 4);
      case 'YY': return _pad(parts.year % 100, 2);
      case 'MMMM': return _monthName(date, locale, zone, 'long');
      case 'MMM': return _monthName(date, locale, zone, 'short');
      case 'MM': return _pad(parts.month, 2);
      case 'M': return String(parts.month);
      case 'DD': return _pad(parts.day, 2);
      case 'D': return String(parts.day);
      case 'dddd': return _weekdayName(date, locale, zone, 'long');
      case 'ddd': return _weekdayName(date, locale, zone, 'short');
      case 'HH': return _pad(parts.hour24, 2);
      case 'H': return String(parts.hour24);
      case 'hh': return _pad(hour12, 2);
      case 'h': return String(hour12);
      case 'mm': return _pad(parts.minute, 2);
      case 'm': return String(parts.minute);
      case 'ss': return _pad(parts.second, 2);
      case 's': return String(parts.second);
      case 'SSS': return _pad(parts.ms, 3);
      case 'A': return meridiemUpper;
      case 'a': return meridiemLower;
      case 'ZZ': return _offsetToText(offsetMin, false);
      case 'Z': return _offsetToText(offsetMin, true);
      case 'zzzz': return _timeZoneName(date, locale, zone, 'long');
      case 'z': return _timeZoneName(date, locale, zone, 'short');
      default: return entry.value;
    }
  }).join('');
}

function _monthFromName(raw) {
  const lower = String(raw || '').toLowerCase();
  const idxLong = MONTHS_EN_LONG.findIndex(m => m.toLowerCase() === lower);
  if (idxLong >= 0) return idxLong + 1;
  const idxShort = MONTHS_EN_SHORT.findIndex(m => m.toLowerCase() === lower);
  if (idxShort >= 0) return idxShort + 1;
  return null;
}

function _parseOffsetToMinutes(value) {
  const v = String(value || '').trim();
  if (!v || /^z$/i.test(v)) return 0;

  let m = v.match(/^([+-])(\d{2}):(\d{2})$/);
  if (m) {
    const sign = m[1] === '+' ? 1 : -1;
    return sign * (Number(m[2]) * 60 + Number(m[3]));
  }

  m = v.match(/^([+-])(\d{2})(\d{2})$/);
  if (m) {
    const sign = m[1] === '+' ? 1 : -1;
    return sign * (Number(m[2]) * 60 + Number(m[3]));
  }

  return null;
}

function _parseDateByFormat(input, format, zone) {
  const entries = _tokenizeFormat(format);
  const parseParts = [];
  let pattern = '^';

  for (const entry of entries) {
    if (entry.type === 'literal') {
      pattern += _regexEscape(entry.value);
      continue;
    }

    switch (entry.value) {
      case 'YYYY': pattern += '(\\d{4})'; break;
      case 'YY': pattern += '(\\d{2})'; break;
      case 'MMMM': pattern += '([A-Za-z]+)'; break;
      case 'MMM': pattern += '([A-Za-z]{3})'; break;
      case 'MM':
      case 'DD':
      case 'HH':
      case 'hh':
      case 'mm':
      case 'ss': pattern += '(\\d{2})'; break;
      case 'M':
      case 'D':
      case 'H':
      case 'h':
      case 'm':
      case 's': pattern += '(\\d{1,2})'; break;
      case 'SSS': pattern += '(\\d{1,3})'; break;
      case 'A':
      case 'a': pattern += '(AM|PM|am|pm)'; break;
      case 'Z': pattern += '(Z|[+-]\\d{2}:\\d{2})'; break;
      case 'ZZ': pattern += '(Z|[+-]\\d{4})'; break;
      // Allow weekday tokens in input as pass-through parse hints.
      case 'dddd': pattern += '([A-Za-z]+)'; break;
      case 'ddd': pattern += '([A-Za-z]{3})'; break;
      default: pattern += _regexEscape(entry.value); break;
    }
    parseParts.push(entry.value);
  }

  pattern += '$';
  const rx = new RegExp(pattern, 'i');
  const m = String(input || '').match(rx);
  if (!m) return null;

  let year = 1970;
  let month = 1;
  let day = 1;
  let hour = 0;
  let minute = 0;
  let second = 0;
  let ms = 0;
  let meridiem = null;
  let offsetMinutes = null;

  let captureIndex = 1;
  for (const part of parseParts) {
    const value = m[captureIndex++];
    switch (part) {
      case 'YYYY': year = Number(value); break;
      case 'YY': year = 2000 + Number(value); break;
      case 'MMMM':
      case 'MMM': {
        const parsedMonth = _monthFromName(value);
        if (!parsedMonth) return null;
        month = parsedMonth;
        break;
      }
      case 'MM':
      case 'M': month = Number(value); break;
      case 'DD':
      case 'D': day = Number(value); break;
      case 'HH':
      case 'H':
      case 'hh':
      case 'h': hour = Number(value); break;
      case 'mm':
      case 'm': minute = Number(value); break;
      case 'ss':
      case 's': second = Number(value); break;
      case 'SSS': ms = Number(value); break;
      case 'A':
      case 'a': meridiem = String(value).toUpperCase(); break;
      case 'Z':
      case 'ZZ': {
        offsetMinutes = _parseOffsetToMinutes(value);
        if (offsetMinutes == null) return null;
        break;
      }
      default: break;
    }
  }

  if (month < 1 || month > 12) return null;
  if (day < 1 || day > 31) return null;
  if (minute < 0 || minute > 59) return null;
  if (second < 0 || second > 59) return null;
  if (ms < 0 || ms > 999) return null;

  const has12HourToken = parseParts.includes('hh') || parseParts.includes('h');
  if (has12HourToken) {
    if (hour < 1 || hour > 12) return null;
    if (meridiem) {
      if (hour === 12) hour = 0;
      if (meridiem === 'PM') hour += 12;
    }
  } else if (hour < 0 || hour > 23) {
    return null;
  }

  let date;
  if (offsetMinutes != null) {
    const ts = Date.UTC(year, month - 1, day, hour, minute, second, ms) - (offsetMinutes * 60000);
    date = new Date(ts);
  } else if (zone === 'utc') {
    date = new Date(Date.UTC(year, month - 1, day, hour, minute, second, ms));
  } else {
    date = new Date(year, month - 1, day, hour, minute, second, ms);
  }

  if (!_isValidDate(date)) return null;
  return date;
}

/**
 * Parse a date/time input into a Date object.
 *
 * @param {Date|string|number|bigint} input
 * @param {{ inputFormat?: string|null, zone?: 'local'|'utc' }} [options]
 * @returns {Date|null}
 * @since 1.1.0
 */
function parseDateTime(input, { inputFormat = null, zone = 'local' } = {}) {
  const normalizedZone = _normalizeZone(zone);
  if (inputFormat == null || inputFormat === '') {
    const d = _normalizeDateTimeInput(input);
    return _isValidDate(d) ? d : null;
  }
  return _parseDateByFormat(input, inputFormat, normalizedZone);
}

/**
 * Format an input date/time using token-based output formats.
 *
 * Supported output tokens:
 * - `YYYY`, `YY`
 * - `MMMM`, `MMM`, `MM`, `M`
 * - `DD`, `D`, `dddd`, `ddd`
 * - `HH`, `H`, `hh`, `h`, `mm`, `m`, `ss`, `s`, `SSS`, `A`, `a`
 * - `Z`, `ZZ`, `z`, `zzzz`
 * - Literal text via square brackets: `[at]`
 *
 * Special output format values:
 * - `'iso'` or `'ISO'` -> `Date#toISOString()`
 *
 * @param {Date|string|number|bigint} input
 * @param {{
 *   inputFormat?: string|null,
 *   outputFormat?: string,
 *   zone?: 'local'|'utc',
 *   locale?: string,
 *   invalid?: string
 * }} [options]
 * @returns {string}
 * @since 1.1.0
 */
function formatDateTime(input, {
  inputFormat = null,
  outputFormat = 'YYYY-MM-DD HH:mm:ss',
  zone = 'local',
  locale = 'auto',
  invalid = 'Invalid DateTime'
} = {}) {
  const normalizedZone = _normalizeZone(zone);
  const normalizedLocale = _resolveLocale(locale);
  const date = parseDateTime(input, { inputFormat, zone: normalizedZone });
  if (!_isValidDate(date)) return String(invalid);

  const format = String(outputFormat || '');
  if (!format || format.toLowerCase() === 'iso') {
    return date.toISOString();
  }

  return _formatDateByTokens(date, format, normalizedZone, normalizedLocale);
}

