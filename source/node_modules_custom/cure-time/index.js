/**
 * Format a duration (in **nanoseconds**) into a human-readable string.
 *
 * Behavior:
 * - For **compact** style, chooses the largest unit with value ≥ 1 (or the smallest unit if all < 1),
 *   applies a sensible decimal strategy, and returns `"1.29ms"`-style output (or long labels when `long=true`).
 * - For **multi** style, returns a concise breakdown across units. After reaching milliseconds or microseconds,
 *   at most one smaller sub-unit is added to keep output short (e.g., `"3ms 250µs"`).
 * - Non-positive or invalid values return `"0ms"` (or `"0 milliseconds"` when `long=true`).
 *
 * Invariants:
 * - Always includes a unit; never returns an empty string.
 * - Never throws on numeric input; out-of-range/invalid numbers produce a `"0..."` representation.
 *
 * @function formatDuration
 * @memberof module:cure-time
 * @param {number} ns
 *   Duration in **nanoseconds**.
 * @param {DurationFormatOptions} [options]
 *   Formatting options.
 * @returns {string}
 *   Human-readable representation (e.g., `"1h 2m 3s 45ms"`, `"1.29ms"`, or `"0ms"`).
 * @since 1.0.0
 *
 * @example <caption>Compact style for precise single-unit output</caption>
 * formatDuration(1290000, { style: 'compact', decimals: 2 }); // "1.29ms"
 *
 * @example <caption>Multi-unit breakdown (short labels)</caption>
 * formatDuration(3723045000000); // "1h 2m 3s 45ms"
 *
 * @example <caption>Multi-unit with long labels</caption>
 * formatDuration(1500000, { long: true }); // "1 millisecond 500 microseconds"
 */
function formatDuration(ns, { long = false, style = 'multi', decimals = 2 } = {}) {
  // Guard
  if (!Number.isFinite(ns) || ns <= 0) return long ? '0 milliseconds' : '0ms';

  // Unit table in nanoseconds
  const UNITS = [
    { short: 'y',  long: 'year',        ns: 365 * 24 * 60 * 60 * 1e9 },
    { short: 'mo', long: 'month',       ns: 30  * 24 * 60 * 60 * 1e9 },
    { short: 'w',  long: 'week',        ns: 7   * 24 * 60 * 60 * 1e9 },
    { short: 'd',  long: 'day',         ns: 24  * 60 * 60 * 1e9 },
    { short: 'h',  long: 'hour',        ns: 60  * 60 * 1e9 },
    { short: 'm',  long: 'minute',      ns: 60  * 1e9 },
    { short: 's',  long: 'second',      ns: 1e9 },
    { short: 'ms', long: 'millisecond', ns: 1e6 },
    { short: 'µs', long: 'microsecond', ns: 1e3 },
    { short: 'ns', long: 'nanosecond',  ns: 1   },
  ];

  // --- COMPACT: one best unit with decimals ---------------------------------
  if (style === 'compact') {
    // Pick the biggest unit with value >= 1 (or the smallest unit if all < 1)
    let u = UNITS.find(u => ns >= u.ns) || UNITS[UNITS.length - 1];
    let value = ns / u.ns;

    // Minor ergonomic tweaks: keep integers for big units, decimals for small
    const d = (value < 10) ? decimals : (value < 100 ? Math.min(decimals, 1) : 0);
    const str = (d > 0 ? value.toFixed(d) : Math.round(value).toString());

    if (long) {
      const label = u.long + (Number(str) === 1 ? '' : 's');
      return `${str} ${label}`;
    } else {
      return `${str}${u.short}`;
    }
  }

  // --- MULTI: breakdown (original behavior, but without losing sub-ms) -------
  let rem = ns;
  const parts = [];

  for (const u of UNITS) {
    const amt = Math.floor(rem / u.ns);
    if (amt > 0) {
      const label = long ? `${u.long}${amt !== 1 ? 's' : ''}` : u.short;
      parts.push(long ? `${amt} ${label}` : `${amt}${label}`);
      rem -= amt * u.ns;

      // Keep the output concise: after we hit ms or smaller, add one more subunit and stop
      if (u.short === 'ms' || u.short === 'µs') {
        const next = UNITS[UNITS.findIndex(x => x.short === u.short) + 1];
        if (next && rem >= next.ns) {
          const sub = Math.floor(rem / next.ns);
          const subLabel = long ? `${next.long}${sub !== 1 ? 's' : ''}` : next.short;
          parts.push(long ? `${sub} ${subLabel}` : `${sub}${subLabel}`);
        }
        break;
      }
    }
  }

  if (parts.length === 0) {
    // smaller than 1ns (impractical), or ns < 1
    return long ? '0 milliseconds' : '0ms';
  }

  return long ? parts.join(' ') : parts.join(' ');
}

/**
 * Normalize a duration-like input into **nanoseconds** (Number).
 *
 * Accepted inputs:
 * - **BigInt**: already nanoseconds, e.g., `process.hrtime.bigint()`.
 * - **Array** `[seconds, nanoseconds]`: Node `process.hrtime()` tuple.
 * - **Number**: heuristic:
 *   - ≥ 1e7 → treat as nanoseconds (already ns)
 *   - > 60 and integer → seconds
 *   - fractional and < 60 → seconds
 *   - otherwise → milliseconds
 * - **Clock string** `"hh:mm:ss(.ms)"`, where `.ms` is 1–3 digits.
 * - **Unit-suffixed string**: `"123ms"`, `"5s"`, `"2m"`, `"250us"`/`"250μs"`.
 * - **Bare numeric string**: treated as seconds (legacy gulp-cli timing).
 *
 * Output & edge cases:
 * - Returns a Number in nanoseconds. Invalid/unknown inputs return `0`.
 * - Decimal comma in strings is normalized to dot (e.g., `"0,25s"` → `"0.25s"`).
 *
 * @function normalizeDurationToNs
 * @memberof module:cure-time
 * @param {bigint|number|string|[number,number]|any} input
 *   Duration-like value to normalize.
 * @returns {number}
 *   Duration in **nanoseconds** (Number). `0` when the input cannot be parsed.
 * @since 1.0.0
 *
 * @example <caption>From hrtime tuple</caption>
 * normalizeDurationToNs([1, 250000000]); // -> 1250000000
 *
 * @example <caption>From human strings</caption>
 * normalizeDurationToNs('01:02:03.045'); // -> 3723045000000
 * normalizeDurationToNs('250ms');        // -> 250000000
 * normalizeDurationToNs('3.2s');         // -> 3200000000
 *
 * @example <caption>Numbers with heuristics</caption>
 * normalizeDurationToNs(0.75); // -> 750000000   (fractional < 60 → seconds)
 * normalizeDurationToNs(250);  // -> 250000000   (integer small → milliseconds)
 */
function normalizeDurationToNs(input) {
  if (input == null) return 0;

  // BigInt ns (process.hrtime.bigint)
  if (typeof input === 'bigint') return Number(input);

  // [seconds, nanoseconds] tuple (process.hrtime)
  if (Array.isArray(input) && input.length === 2) {
    const s  = Number(input[0] || 0);
    const ns = Number(input[1] || 0);
    return s * 1e9 + ns;
  }

  // Number (ambiguous). Heuristic:
  // - If it's very large, assume ns
  // - If < 1e4 and has decimals, likely seconds from CLI timing
  // - If < 1e4 and integer-ish, could be ms; prefer seconds only if < 60 and has decimals
  if (typeof input === 'number' && Number.isFinite(input)) {
    if (input >= 1e7) return Math.round(input);            // ~≥10ms in ns scale → treat as ns
    if (input > 60 && Number.isInteger(input)) return input * 1e9; // large ints → seconds
    // Favor seconds if fractional and small
    if (!Number.isInteger(input) && input < 60) return Math.round(input * 1e9);
    // Otherwise assume milliseconds
    return Math.round(input * 1e6);
  }

  // String parsing
  let s = String(input).trim();
  // Normalize decimal comma → dot (e.g., "0,1099200")
  if (s.includes(',') && !s.includes('.')) s = s.replace(/,/g, '.');

  // Clock format: hh:mm:ss(.ms)
  {
    const m = s.match(/^(\d{1,2}):([0-5]\d):([0-5]\d)(?:\.(\d{1,3}))?$/);
    if (m) {
      const h = Number(m[1]) || 0;
      const mi = Number(m[2]) || 0;
      const se = Number(m[3]) || 0;
      const ms = Number(m[4] || 0) || 0;
      return (((h * 60 + mi) * 60) + se) * 1e9 + ms * 1e6;
    }
  }

  // Unit-suffixed: μs/us, ms, s, m
  {
    // Accept both "μs" and "us"
    const micro = s.match(/^\s*([\d.]+)\s*(μs|us)\s*$/i);
    if (micro) return Math.round(parseFloat(micro[1]) * 1e3); // μs → ns

    const milli = s.match(/^\s*([\d.]+)\s*ms\s*$/i);
    if (milli) return Math.round(parseFloat(milli[1]) * 1e6); // ms → ns

    const sec = s.match(/^\s*([\d.]+)\s*s\s*$/i);
    if (sec) return Math.round(parseFloat(sec[1]) * 1e9);     // s → ns

    const min = s.match(/^\s*([\d.]+)\s*m\s*$/i);
    if (min) return Math.round(parseFloat(min[1]) * 60 * 1e9);// m → ns
  }

  // Bare number string → assume seconds (legacy gulp-cli)
  {
    const v = parseFloat(s);
    if (Number.isFinite(v)) return Math.round(v * 1e9);
  }

  // Fallback: zero
  return 0;
}

