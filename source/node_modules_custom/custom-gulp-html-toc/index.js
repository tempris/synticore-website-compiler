/**
 * Decode a Vinyl buffer as UTF-8 and fail fast on invalid decode artifacts.
 *
 * @param {Buffer} buffer
 * @param {string} filePath
 * @returns {string}
 */
function decodeUtf8Strict(buffer, filePath) {
  const text = Buffer.isBuffer(buffer) ? buffer.toString('utf8') : String(buffer || '');
  if (text.includes('\uFFFD')) {
    throw new Error(`Invalid UTF-8 while parsing file: ${filePath}`);
  }
  return text.replace(/^\uFEFF/, '');
}

