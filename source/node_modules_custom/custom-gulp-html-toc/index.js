'use strict';

/**
 * @fileoverview
 * custom-gulp-html-toc — Synticore-scoped fork of `gulp-html-toc` used to inject an in-page Table of Contents (TOC)
 * into HTML files during Synticore builds. Derived from the general plugin but tailored for Synticore conventions,
 * logging, and skip heuristics.
 *
 * Purpose:
 * - Parse each HTML vinyl file, strip HTML comments, and decide if a TOC should be generated (requires ≥1 `<h1>` and ≥2 total headings).
 * - Within Synticore docs/pages, replace a marker (default `<!-- toc -->`) with a Synticore-style container (default `<div id="toc"></div>`).
 * - Defer actual TOC markup generation to {@link module:custom-html-toc}, passing through non-wrapper options unchanged.
 * - Integrate with Cure Log Hub for consistent build logs inside Synticore.
 *
 * Synticore-specific notes:
 * - **Scope:** Intended only for Synticore pipelines; not published as a general Gulp plugin.
 * - **Marker policy:** Uses a literal HTML comment marker by default; teams may override via `replaceText` or `replaceTextTemplate`.
 * - **Skip policy:** Skips single-heading or heading-less documents (and files matching `ignore_patterns`) to avoid noisy TOCs.
 * - **Logging:** Uses the Synticore shared logger through `cure-log-hub` with the tag `[📚 Gulp HTML TOC]`.
 *
 * Runtime & module system:
 * - Runtime: Node.js (LTS).
 * - Module system: CommonJS (CJS).
 *
 * Side effects on import:
 * - None. The transform stream is created only when the exported factory is invoked.
 *
 * Globals & environment:
 * - Uses Node globals: `process`, `__dirname`.
 * - No environment variables are read directly.
 *
 * Security & permission assumptions:
 * - Operates purely on in-memory vinyl files; no direct filesystem writes in this module.
 * - HTML is treated as data; DOM-like manipulation is via Cheerio (no script execution).
 *
 * I/O contract:
 * - Input: vinyl `file` objects whose `contents` are HTML (Buffer/string).
 * - Output: same vinyl `file` with `contents` replaced by HTML augmented with a TOC (if applicable).
 * - Errors: emits `PluginError('custom-gulp-html-toc', ...)` on parsing/generation failures; logs context via Cure Log Hub.
 *
 * Concurrency & async:
 * - Transform is synchronous per file (string ops + Cheerio); backpressure handled by `through2`.
 * - No timers; no shared mutable state.
 *
 * Compatibility & performance:
 * - Designed for Synticore’ Gulp pipelines; cost is O(N) in HTML size.
 * - Heading detection runs on a **comment-stripped** HTML string to avoid counting commented headings.
 *
 * Versioning policy:
 * - SemVer within Synticore. Changes that alter default marker/skip behavior or option semantics are considered breaking.
 *
 * @module custom-gulp-html-toc
 * @version 2.0.0
 * @since 1.0.0
 * @license MIT
 * @author Cure Interactive
 *
 * @example <caption>Basic Synticore usage in a Gulpfile</caption>
 * // const toc = require('custom-gulp-html-toc');
 * // gulp.src('src/**\/*.html')
 * //   .pipe(toc({
 * //     // Optional: skip files by micromatch patterns (relative path)
 * //     ignore_patterns: ['**\/index.html', '**\/landing/*.html'],
 * //     // Optional: explicit marker to replace (defaults to '<!-- toc -->')
 * //     replaceText: '<!-- toc -->',
 * //     // Optional: provide a Synticore-styled container
 * //     replaceTextTemplate: () => '<div id="toc" class="toc toc--synticore"></div>'
 * //     // Any other options are forwarded to `custom-html-toc`
 * //   }))
 * //   .pipe(gulp.dest('dist'));
 */

