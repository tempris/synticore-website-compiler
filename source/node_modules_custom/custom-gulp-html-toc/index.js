/**
 * Options for {@link module:custom-gulp-html-toc}.
 *
 * Recognized by this Synticore wrapper:
 * - `ignore_patterns` — micromatch patterns applied to `file.relative` to skip processing.
 * - `replaceText` — marker to find in the HTML (defaults to `'<!-- toc -->'`).
 * - `replaceTextTemplate()` — callback returning the replacement container markup; if present, it overrides `replaceText`.
 *
 * All **other** properties are passed through to {@link module:custom-html-toc} unchanged.
 *
 * @typedef {Object} GulpHtmlTocOptions
 * @property {string|string[]} [ignore_patterns] Patterns against the vinyl file’s relative path to skip TOC generation.
 * @property {string} [replaceText='<!-- toc -->'] Marker in the HTML replaced by the TOC container.
 * @property {() => string} [replaceTextTemplate] If provided, invoked to obtain the container markup (e.g., `<div id="toc"></div>`).
 * @default '<!-- toc -->'
 * @since 2.0.0
 */

/**
 * Resolve a path relative to the repository root (two levels up from this file).
 *
 * @private
 * @function _getRootPath
 * @memberof module:custom-gulp-html-toc
 * @param {string} [path=''] - Path to append to the root.
 * @default ''
 * @returns {string} Absolute path under the repo root.
 * @since 1.0.0
 */
function _getRootPath(path = '') {
  return NODE_PATH.resolve(__dirname, '../../../', path);
}

