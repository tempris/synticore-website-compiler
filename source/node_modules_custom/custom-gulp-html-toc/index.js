/**
 * Compute the HTML snippet that will replace the marker before TOC generation.
 *
 * Resolution order:
 * - If `options.replaceTextTemplate` is a function, call it and return its string.
 * - Otherwise return the default `<div id="toc"></div>`.
 *
 * @private
 * @function replaceTextTemplateFromOptions
 * @memberof module:custom-gulp-html-toc
 * @param {GulpHtmlTocOptions} [options={}] Options possibly containing `replaceTextTemplate`.
 * @returns {string} The container markup to insert prior to TOC creation.
 * @since 2.0.0
 */
function replaceTextTemplateFromOptions(options = /** @type {GulpHtmlTocOptions} */ ({})) {
  if (typeof options.replaceTextTemplate === 'function') {
    return options.replaceTextTemplate();
  }
  return '<div id="toc"></div>';
}

/**
 * Create a Gulp transform that injects a Table of Contents into HTML files (Synticore fork).
 *
 * Behavior:
 * - If `options.ignore_patterns` matches the vinyl file’s `relative` path, the file is passed through unchanged.
 * - Strips **HTML comments** before heading counting, so commented headings don’t inflate counts.
 * - Skips TOC generation when the document has **no `<h1>`** or **fewer than 2 total headings** (`<h1>`–`<h6>`).
 * - Replaces the **first** occurrence of `options.replaceText` (default `'<!-- toc -->'`) with HTML from
 *   {@link module:custom-gulp-html-toc~replaceTextTemplateFromOptions|replaceTextTemplateFromOptions(options)} (default `<div id="toc"></div>`).
 * - Delegates TOC HTML creation to {@link module:custom-html-toc}, passing the (possibly modified) HTML string and `options`.
 *
 * Error handling:
 * - Logs with file context and emits a {@link PluginError} named `'custom-gulp-html-toc'`.
 *
 * @function customGulpHtmlToc
 * @memberof module:custom-gulp-html-toc
 * @param {GulpHtmlTocOptions} [options={}] Configuration for skipping and marker replacement; other keys are forwarded.
 * @returns {import('stream').Transform} A `through2.obj` transform suitable for Gulp pipelines.
 * @throws {PluginError} Emitted on the stream when processing fails for a file.
 * @since 1.0.0
 *
 * @example <caption>Synticore docs pipeline</caption>
 * // const toc = require('custom-gulp-html-toc');
 * // gulp.src('docs\/**\/*.html')
 * //   .pipe(toc({
 * //     ignore_patterns: ['**\/index.html'],
 * //     replaceTextTemplate: () => '<nav id="toc" class="toc toc--docs"></nav>'
 * //   }))
 * //   .pipe(gulp.dest('build/docs'));
 */
module.exports = function(options = /** @type {GulpHtmlTocOptions} */ ({})) {
  return NODE_THROUGH2.obj(function(file, enc, next) {
    if (file.isNull()) {
      next(null, file);
      return;
    }

    try {
      if (options.ignore_patterns) {
        const fileName = file.relative; // Full file name with extension

        if (NODE_MICROMATCH.isMatch(fileName, options.ignore_patterns)) {
          log.debug?.(LOG_TAG_GULP_HTML_TOC, `Skipping TOC generation for file: ${file.relative}`);
          next(null, file); // Skip processing this file
          return;
        }
      }

      let str = decodeUtf8Strict(file.contents, file.path); // MAYBE: Content-Based Skip

      // Remove HTML comments
      const $ = (/** @type {any} */ (NODE_CHEERIO)).load(str, { decodeEntities: false });
      $('*').contents().each(function() {
        if (this.type === 'comment') {
          $(this).remove();
        }
      });

      const cleanedHtml = $.html();

      // Check heading counts
      const h1Count = (cleanedHtml.match(/<h1[^>]*>/g) || []).length;
      const headingCount = (cleanedHtml.match(/<h[1-6][^>]*>/g) || []).length;

      if (h1Count === 0 || headingCount < 2) {
        next(null, file);
        return;
      }

      const marker = options.replaceText ? options.replaceText : '<!-- toc -->';
      str = str.replace(marker, replaceTextTemplateFromOptions(options));

      file.contents = Buffer.from(NODE_CUSTOM_HTML_TOC(str, /** @type {any} */(options)), 'utf8');
    } catch (err) {
      log.error?.(LOG_TAG_GULP_HTML_TOC, err, { fileName: file.path });
      this.emit('error', new NODE_PLUGIN_ERROR('custom-gulp-html-toc', err, { fileName: file.path }));
      return;
    }

    next(null, file);
  });
};

