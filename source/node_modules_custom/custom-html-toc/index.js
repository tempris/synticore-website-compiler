'use strict';

/**
 * @fileoverview
 * custom-html-toc — Synticore-scoped fork of `html-toc` that builds a nested, collapsible in-page Table of Contents (TOC)
 * from heading elements and injects header anchors. Adds Synticore-friendly defaults (collapsible `<details>`, link class,
 * emoji handling, depth limits) while remaining a simple string→string utility.
 *
 * Purpose:
 * - Parse an HTML string and generate TOC markup into a target container (e.g., `#toc`), preserving existing content via append.
 * - Create stable, hierarchical slugs (IDs) from heading text (emoji stripped for the ID, but optionally wrapped for visual text).
 * - Insert anchor links at headings (configurable template) and optional “header prepend” markup (e.g., icons, badges).
 * - Support depth limits independently for TOC tree depth, anchor injection, and header prepends.
 *
 * Runtime & module system:
 * - Runtime: Node.js (LTS).
 * - Module system: CommonJS (CJS).
 *
 * Side effects on import:
 * - None. All work is performed when the exported function is invoked.
 *
 * Globals & environment:
 * - Uses Node globals only for module loading. No env vars are read.
 *
 * Security & permission assumptions:
 * - Treats input HTML as data. Uses Cheerio to manipulate DOM; **no script execution**.
 * - Does not fetch remote content or touch the filesystem.
 *
 * I/O contract:
 * - Input: raw HTML string and an optional options object.
 * - Output: a new HTML string with:
 *   - IDs injected on headings (within depth policy),
 *   - anchor elements appended to headings (template-driven, within depth policy),
 *   - optional header-prepend markup added to headings (within depth policy),
 *   - TOC markup appended into the `id` container.
 *
 * Concurrency & async:
 * - Purely synchronous per call. No timers, no shared mutable state.
 *
 * Compatibility & performance:
 * - O(N) in HTML size. Slugging uses an in-memory cache for stable de-duplication.
 *
 * Versioning policy:
 * - SemVer within Synticore. Changes to default depth/structure or option semantics are breaking.
 *
 * @module custom-html-toc
 * @version 1.0.0
 * @since 1.0.0
 * @license MIT
 * @author Cure Interactive
 *
 * @example <caption>Minimal usage</caption>
 * const html = `
 *   <div id="toc"></div>
 *   <h1>Intro</h1>
 *   <h2>Getting Started</h2>
 * `;
 * const out = require('custom-html-toc')(html, { selectors: 'h1,h2' });
 * // `out` now has IDs on headings, anchors appended, and TOC items inside #toc.
 */

