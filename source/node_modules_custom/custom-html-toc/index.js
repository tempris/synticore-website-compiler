/**
 * @typedef {Object} CustomHtmlTocOptions
 * @property {string} [selectors='h1,h2'] CSS selector list determining which headings contribute to the TOC.
 * @property {string} [id='#toc'] CSS selector of the TOC container **within `str`** to append into (e.g., `'#toc'`).
 * @property {string} [linkClass='toc__link'] Class placed on each TOC `<a>` element.
 * @property {(text:string, ctx?:{cache?:Object})=>string} [slugger=markdownSlug] Function used to slugify heading text (after emoji strip).
 * @property {string} [header=''] Optional HTML prepended to the TOC container before the generated list.
 * @property {string|null} [wrapEmoji=null] Optional template with `*` placeholder to wrap emoji clusters in visible link text.
 * @property {boolean} [parentLink=true] If `true`, child slugs are prefixed with their parent slug (`parent-child` style).
 * @property {boolean} [collapsible=true] If `true`, TOC uses `<details>` sections; otherwise nested `<ul>`.
 * @property {number} [maxDepth=6] Maximum TOC nesting depth (relative to the first heading).
 * @property {number} [anchorMaxDepth=6] Add anchors only for headings at or above this depth (absolute level, e.g., h1..h6).
 * @property {number} [headerPrependMaxDepth=6] Add header-prepend markup only for headings at or above this depth (absolute).
 * @property {string} [ignore_class] Class name; headings with this class (or inside an element with it) are ignored entirely.
 * @property {string} [headerPrependSelectors] Optional selector list overriding `selectors` for header-prepend insertion.
 * @property {false|string|function(string):string} [headerPrepend] Markup (or factory) to prepend inside each eligible heading. Use `false` to disable.
 * @property {false|string|function(string):string} [anchorTemplate] Markup (or factory) appended to each eligible heading. Use `false` to disable. Default renders an anchored link + icon.
 * @property {import('cheerio')} [$] Optional Cheerio instance to reuse instead of creating a new one.
 */

/* =============================================================================
 * Dependencies
 * ============================================================================= */

var cheerio = require('cheerio');
var extend = require('extend-shallow');
var slugify = require('markdown-slug');

/* =============================================================================
 * Internal helpers (private)
 * ============================================================================= */

/**
 * Remove emoji and certain symbol ranges from a string.
 *
 * Rationale:
 * - Emoji clusters can produce cumbersome or unstable IDs.
 * - We keep them in **visual** text but strip from **ID generation**.
 *
 * @private
 * @function _stripEmoji
 * @param {string} text - Raw text from a heading.
 * @returns {string} Text with emoji/symbol ranges removed.
 * @since 1.0.0
 */
// Covers common emoji blocks, dingbats/symbols (including U+26A0 WARNING SIGN),
// keycaps, and basic ZWJ sequences so wrap/strip stay consistent.
const EMOJI_CLUSTER_REGEX = /((?:[\u2300-\u23FF]|[\u2600-\u27BF]|[\uE000-\uF8FF]|[\uD83C-\uDBFF][\uDC00-\uDFFF])(?:\uFE0F)?(?:\u200D(?:[\u2300-\u23FF]|[\u2600-\u27BF]|[\uE000-\uF8FF]|[\uD83C-\uDBFF][\uDC00-\uDFFF])(?:\uFE0F)?)*)|([#*0-9]\uFE0F?\u20E3)|[\u2194-\u21FF]/g;

const _stripEmoji = (text) => text.replace(EMOJI_CLUSTER_REGEX, '');

/**
 * Optionally wrap emoji clusters inside a Cheerio element with a provided template string.
 * The template must contain a single `*` placeholder, which will be replaced by the emoji cluster itself.
 *
 * Example: `wrap = '<span class="emoji">*</span>'` → wraps each emoji in `<span class="emoji">…</span>`.
 *
 * @private
 * @function _wrapEmojiClustersInElement
 * @param {*} $element - Cheerio element whose text nodes will be processed.
 * @param {string|null} wrap - Template string containing `*` where the emoji should be placed; falsy disables wrapping.
 * @returns {void}
 * @since 1.0.0
 */
function _wrapEmojiClustersInElement($element, /* string|null */ wrap) {
  if (!wrap) return;

  $element.contents().each((_, node) => {
    if (node && node.type === 'text' && typeof node.data === 'string') {
      node.data = node.data.replace(EMOJI_CLUSTER_REGEX, match => wrap.replace('*', match));
    }
  });
}

/**
 * Compute the anchor HTML appended to a heading (default: a named anchor and link icon).
 * `false` disables; a function receives the heading ID and must return a string.
 *
 * @private
 * @function _anchorTemplate
 * @param {string} id - Heading ID.
 * @param {CustomHtmlTocOptions} options - Option bag controlling behavior.
 * @returns {string} HTML string to append (may be empty).
 * @since 1.0.0
 */
function _anchorTemplate(id, options) {
  if (options.anchorTemplate === false) {
    return '';
  }

  if (typeof options.anchorTemplate === 'function') {
    return options.anchorTemplate(id);
  }

  return '<a href="#' + id + '" name="' + id + '" class="anchor">\n'
  + '  <span class="anchor-target" id="' + id + '"></span>\n'
  + '  <span class="glyphicon glyphicon-link"></span>\n'
  + '</a>';
}

/**
 * Compute the header-prepend HTML to place at the **start** of a heading element.
 * `false` disables; a function receives the heading ID and must return a string.
 *
 * @private
 * @function _headerPrepend
 * @param {string} id - Heading ID (already set on the element).
 * @param {CustomHtmlTocOptions} options - Option bag controlling behavior.
 * @returns {string} HTML string to prepend (may be empty).
 * @since 1.0.0
 */
function _headerPrepend(id, options) {
  if (options.headerPrepend === false) {
    return '';
  }

  if (typeof options.headerPrepend === 'function') {
    return options.headerPrepend(id);
  }

  if (options.headerPrepend !== undefined) {
    return options.headerPrepend;
  }

  return '';
}

/* =============================================================================
 * Public API
 * ============================================================================= */

/**
 * Build and inject an in-page Table of Contents (TOC) into the provided HTML string,
 * adding heading IDs, anchors, and optional header-prepend markup.
 *
 * **Selector & depth policies**
 * - `selectors` controls which heading levels participate (default `'h1,h2'`).
 * - The **TOC nesting depth** is limited by `maxDepth`.
 * - The **anchor injection depth** is limited by `anchorMaxDepth`.
 * - The **header prepend depth** is limited by `headerPrependMaxDepth`.
 *
 * **Hierarchy & slugs**
 * - The first encountered heading defines the base level for nesting (e.g., first `h2` → base=2).
 * - IDs are derived from **emoji-stripped** text using `slugger`, and optionally prefixed by the parent ID when `parentLink` is true.
 * - The visual link text inside the TOC uses the heading’s **HTML** (post-emoji-wrap), not just plain text.
 *
 * **Collapsible output**
 * - When `collapsible: true`, the TOC uses `<details><summary>…</summary>…</details>` blocks; otherwise simple nested `<ul><li>…`.
 *
 * @function customHtmlToc
 * @memberof module:custom-html-toc
 * @param {string} str - The full HTML document/string to process.
 * @param {CustomHtmlTocOptions} [options] - Behavior and template controls. Unknown keys are ignored.
 * @returns {string} A modified HTML string with the TOC appended into `options.id` and anchors/IDs added to headings.
 * @throws {TypeError} If `str` is not a string.
 * @since 1.0.0
 *
 * @example <caption>Collapsible TOC up to h4, anchors up to h6</caption>
 * const out = require('custom-html-toc')(html, {
 *   selectors: 'h1,h2,h3,h4',
 *   id: '#toc',
 *   linkClass: 'toc__link',
 *   collapsible: true,
 *   maxDepth: 4,
 *   anchorMaxDepth: 6
 * });
 */
module.exports = function(str, options) {
  if (typeof str !== 'string') {
    throw new TypeError('expected a string');
  }

  var opts = extend({
    selectors: 'h1,h2',
    id: '#toc',
    linkClass: 'toc__link',
    slugger: slugify,
    header: '',
    wrapEmoji: null, // Optional: wrap emojis in output text using string with "*" placeholder
    parentLink: true,
    collapsible: true,
    maxDepth: 6, // Controls max depth for TOC links
    anchorMaxDepth: 6, // Controls max depth for anchors
    // headerPrependMinDepth: 2, // Controls min depth for headerPrepends
    headerPrependMaxDepth: 6 // Controls max depth for headerPrepends
  }, options);

  var $ = opts.$ || (/** @type {any} */ (cheerio)).load(str, { decodeEntities: false });

  // get all the anchor tags from inside the headers
  var headings = $(opts.selectors);
  if (opts.ignore_class) {
    const ignoreSelector = `.${opts.ignore_class}`;
    headings = headings.filter(function () {
      return !($(this).hasClass(opts.ignore_class) || $(this).parents(ignoreSelector).length);
    });
  }
  var firstHeading = headings.first()[0];
  if (!firstHeading) return str;

  var base = +firstHeading.name.slice(1);
  var navigation = [];
  var slugs = {};

  /**
   * Find the location (array) in the nested `navigation` structure where a node of a given relative depth should be placed.
   * Depth 0 → top level; depth 1 → children of last top-level node; etc.
   *
   * @private
   * @function _findLocation
   * @param {Array<Object>} navigation - The accumulating tree.
   * @param {number} depth - Relative nesting depth from the base heading.
   * @returns {Array<Object>} The array that should receive the next node.
   * @since 1.0.0
   */
  function _findLocation(navigation, depth) {
    if (depth <= 0) {
      return navigation;
    }
    var loc = navigation[navigation.length - 1];
    if (!loc) {
      loc = { children: [] };
      navigation.push(loc);
    } else if (!loc.children) {
      loc.children = [];
    }
    return _findLocation(loc.children, depth - 1);
  }

  headings.map(function(i, ele) {
    var $ele = $(ele);
    // var level = +ele.name.slice(1);

    // if (level > opts.maxDepth) return; // Skip headings beyond max depth

    var text = $ele.text().trim();
    if (!text) return;

    if (opts.wrapEmoji) {
      _wrapEmojiClustersInElement($ele, opts.wrapEmoji);
    }

    var idText = _stripEmoji(text); // Cleaned for ID/href only
    var slug = opts.slugger(idText, { cache: slugs });

    // Use the current $ele HTML for the TOC link
    var visualHTML = $ele.html();  // After emoji wrapping

    var node = {
      text: visualHTML,
      link: slug,
      level: +ele.name.slice(1) - base,
      $ele: $ele
    };

    var location = _findLocation(navigation, node.level);
    location.push(node);
  });

  /**
   * Recursively build the TOC HTML for a navigation subtree.
   *
   * @private
   * @function _buildHTML
   * @param {Array<Object>} navigation - Subtree to render.
   * @param {boolean} first - Whether this is the top level (adds `sidenav` class for collapsible mode).
   * @param {string} [sParentLink] - Parent slug (used only for legacy compatibility; not needed for rendering here).
   * @returns {string} HTML string for the subtree.
   * @since 1.0.0
   */
  function _buildHTML(navigation, first, sParentLink) {
    return navigation.map(function(loc) {
      if (!loc || !loc.link) return '';

      var visible_children = loc.level + 1 < opts.maxDepth && loc.children;

      // Build the HTML for the current header
      var headerHTML = '<a class="' + opts.linkClass + '" href="#' + loc.link + '">' + loc.text + '</a>';

      // Build list item or collapsible item
      if (opts.collapsible) {
        // Collapsible list
        return (visible_children ? '<details class="nav' + (first ? ' sidenav' : '') + '"><summary>' : '')
          + headerHTML
          + (visible_children ? '</summary>' + _buildHTML(loc.children, false, loc.link) + '</details>' : '');
      } else {
        // Simple list
        return '<li>'
          + headerHTML
          + (visible_children ? '<ul>' + _buildHTML(loc.children, false, loc.link) + '</ul>' : '')
          + '</li>';
      }
    }).join('\n');
  }

  /**
   * Assign IDs to headings and (optionally) prefix child slugs with parent slugs when `parentLink` is enabled.
   * Respects `anchorMaxDepth` to avoid adding IDs at deep levels.
   *
   * @private
   * @function _addID
   * @param {Array<Object>} navigation - Subtree whose nodes correspond to headings.
   * @param {string} [sParentLink] - Parent slug to prefix (if `parentLink`).
   * @returns {void}
   * @since 1.0.0
   */
  function _addID(navigation, sParentLink) {
    navigation.forEach(function(loc) {
      if (!loc || !loc.link || loc.level >= opts.anchorMaxDepth) return; // Skip adding ID beyond anchor max depth
      const cleanParent = sParentLink ? _stripEmoji(sParentLink) : '';
      loc.link = (opts.parentLink && cleanParent ? cleanParent + '-' : '') + loc.link;
      loc.$ele.attr('id', loc.link);
      if (loc.children) _addID(loc.children, loc.link);
    });
  }

  _addID(navigation);
  $(opts.id).append(opts.header);
  $(opts.id).append(_buildHTML(navigation, true));

  // Append anchors and optional “source-link” relocation for eligible headings
  headings.map(function(i, ele) {
    var $ele = $(ele);
    var id = $ele.attr('id');
    var level = +ele.name.slice(1); // Extract the heading level (h1 -> 1, h2 -> 2, etc.)

    // Skip adding the anchor if the heading level exceeds anchorMaxDepth
    if (level > opts.anchorMaxDepth) return;

    // Anchor template
    $ele.append(_anchorTemplate(id, opts));

    if ($ele.prev().children().hasClass('source-link')) {
      var sourceLink = $ele.prev().children('.source-link');
      $ele.append(sourceLink);
    }
  });

  // Optional header prepends (e.g., icons) on a separate selector set if provided
  $(opts.headerPrependSelectors || opts.selectors).map(function(i, ele) {
    var $ele = $(ele);
    var id = $ele.attr('id');
    var level = +ele.name.slice(1);

    if (level > opts.headerPrependMaxDepth) return; // Skip headerPrepends outside depth range
    // if (!(level > opts.headerPrependMinDepth && level < opts.headerPrependMaxDepth)) return; // Skip headerPrepends outside depth range

    // Header prepend
    $(this).prepend(_headerPrepend(id, opts));
  });

  return $.html();
};

